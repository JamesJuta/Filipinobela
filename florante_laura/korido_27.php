                                                ang aking matalap na papupugutan <br>
                                                ang abang sinta kong nasa bilangguan, <br>
                                                nagdapa sa yapak ng haring sukaban. <br>
                                                <br>
                                                [362] "Inihinging tawad ng luha at daing <br>
                                                ang kaniyang anak na mutya ko't giliw; <br>
                                                ang sagot kung di kusa kong tanggapin <br>
                                                ang pagsinta niya'y di patatawarin. <br>
                                                <br>
                                                [363] "Anong gagawin ko sa ganitong bagay, <br>
                                                ang sinta ko baga'y bayaang mamatay? <br>
                                                napahinuhod na ako't nang mabuhay <br>
                                                ang prinsipeng irog na kahambal-hambal.<br>
                                                <br>
                                                [364] "Ang nabalinong matibay kong dibdib <br>
                                                sa suyo ng hari, bala at paghibik, <br>
                                                nanlambot na kusa't humain sa sakit  <br>
                                                at nang mailigtas ang buhay ng ibig. <br>
                                                <br>
                                                [365] "Sa tuwa ng hari'y pinawalan agad <br>
                                                ang dahil ng aking luhang pumapatak.  <br>
                                                datapwat tadhanang umalis sa s'yudad<br>
                                                at sa ibang lupa'y kusang mawakawak. <br>
                                                <br>
                                                [366] "Pumanaw sa Pers'ya ang irog ko't buhay <br>
                                                na hindi man kami nagkasalitaan, <br>
                                                tingni kung may luha akong ibubukal, <br>
                                                na maitutumbas sa dusa kong taglay! <br>
                                                <br>
                                                [367] "Nang iginagayak sa loob ng reyno <br>
                                                yaong pagkakasal na kamatayan ko, <br>
                                                aking naakalang magdamit-gerero <br>
                                                at kusang magtanan sa real palasyo. <br>
                                                <br>
                                                [368] "Isang hatinggabing kadilima'y lubha, <br>
                                                lihim na naghugos ako sa bintana; <br>
                                                walang kinasama kung hindi ang nasa - <br>
                                                matunton ang sinta kung nasaang lupa. <br>
                                                <br>
                                                [369] "May ilan nang taon akong naglagalag <br>
