                                    <br><center><h2>SA PAGTATAKIPSILIM</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Sinasadya talagang higitan ni Kapitan Tiyago ang paghahanda sa kapistahan dahil ikinatutuwa niya mabangong pagtanggap ng mga tao kay Ibarra na kanyang mamanugangin. Hindi lingid sa kaalaman ng Kapitan na tanyag na tanyag ang binata sa Maynila kaya sinasamantala niya ang mga ganitong pagkakataon upang kasama siyang mapuri sa mga pahayagan. Sari-saring pagkain at inumin mula pa sa ibang bansa ang handa ni Kapitan Tiyago. Si Maria Clara nama’y pinasalubungan niya ng mga kagamitang may mamahaling bato. Bandang hapon na ng magkita sina Ibarra at Kapitan Tiyago.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Si Maria nama’y nagpaalam na mamasyal kasama ang mga kaibigan nitong dalaga. Niyaya ng mga ito si Ibarra at pinaunlakan naman ng binata ang paanyaya. Ang Kapitan nama’y inanyayahan din si Ibarra na doon na sa kanila maghapunan dahil darating daw si Padre Damaso ngunit magalang naman itong tinanggihan ng binata. Lumakad na kasama ng mga kadalagahan ang magkatipan at nang mapadaan sila kina Simang ay kanila rin itong niyaya na kaagad namang sumama sa kanila.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa liwasang bayan ay sinalubong sila ng isang ketongin na pinandidirihan ng lahat. Si Maria’y naawa dito kaya ibinigay niya ang iniregalo ng ama sa kabila ng pagtataka ng mga kaibigan. Lumapit naman si Sisa at kinausap ang ketongin. Kanyang itinuro ang kampanaryo at sinabing nandoon daw ang mga anak nito. Umalis ng pakanta-kanta si Sisa samantalang ang ketongin nama’y umalis na din dala ang bigay sa kanya ni Maria Clara. Napag-isip-isip ni Maria na marami palang mga mahihirap at kapus-palad at iyon ay naging lingid sa kanyang kaalaman.</p>
