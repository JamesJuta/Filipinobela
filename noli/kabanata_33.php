                                    <br><center><h2>MALAYANG KAISIPAN</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Si Elias ay palihim na dumating sa bahay ni Ibarra at sila’y nag-usap tungkol sa mga kaaway ng katipan ni Maria. Kahit na ang hangad ay kabutihan, pinayuhan ni Elias si Ibarra dahil nagkalat ang kanyang kaaway. Nasabi rin nito ang kanyang pagkakatuklas sa balak na pagpatay ng taong dilaw kay Ibarra sa araw ng pagpapasinaya ng paaralan.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Lihim pa lang sinubaybayan ni Elias ang taong dilaw at nalaman niyang prinisinta nito ang sarili kay Nol Juan kahit maliit ang sahod kapalit ng kanyang mga kaalaman. Si Ibarra nama’y nanghinayang sa maagang pagkawala ng taong dilaw dahil marami pa raw sana siyang matutuklasan kung ito ay nabubuhay lamang. Salungat naman dito si Elias dahil tiyak niyang makaliligtas sa hukuman ang taong dilaw dahil sa kabulagan ng hustisya sa bayan. Tila nagkaroon naman ng interes si Ibarra kay Elias dahil sa kanyang mga kaisipan na kakaiba kaysa ordinaryong mamamayan. Napadpad naman ang kanilang usapan sa paniniwala sa Diyos na ‘di tinanggi ni Elias na siya’y nawawalan na ng tiwala dito. Maya-maya pa’y nagpaalam na rin si si Elias baon ang kanyang pangako ng katapatan kay Ibarra.</p>
