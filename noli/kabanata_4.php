                                    <br><center><h2>EREHE AT PILIBUSTERO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa plasa ng Binondo ay nagpalakad-lakad si Ibarra at napansin nito na wala man lang pinagbago ang kanyang bayan sa kabila ng matagal na panahon niyang pangingibang-bansa. Tila wala man lang ipinag-unlad ang bayang iyon. Habang naglalakad ay palaisipan pa rin kay Ibarra ang sinapit ng ama. Sinundan naman siya ni Tinyente upang kwentuhan ng tungkol sa kanyang ama.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Si Don Rafael na ama ng binata ay isa sa pinakamayaman sa bayan ng San Diego. Siya ay matulungin at marami ang nagmamahal sa kanya. Sa kabila ng pagiging mabait ay marami din umano ang naiinggit dito. Kabilang na diyan ang mga pari sa simbahan sa pangunguna ni Padre Damaso. Dahil dito, minabuti diumano ni Don Rafael na hindi mangumpisal, bagay na lalong ikinagalit ng mga pari.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        May isang Kastila noon na walang kaalam-alam, palaboy, at pinagkakatuwaan ng lahat na hinirang ni Don Rafael bilang isang kolektor. Isang araw ay ‘di nakapagpigil ang kolektor sa mga batang nagtatawa sa kanya kaya naman inakma niyang saktan ang mga ito. Tumakbo ng mabilis ang mga bata ng nang hindi niya maabutan ay ibinalibag niya ang baton at may tinamaang isa. Natumba ang bata at walang-awang pinagsisipa ng artilyero. Nakita ito ni Don Rafael kaya inawat niya ito.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ayon sa mga sabi-sabi, sinaktan diumano ng Don ang Kastila hanggang sa tumama ang ulo nito sa malaking bato. Natulungan ni Don Rafael ang binata samantalang ang Kastila ay sumuka ng dugo hanggang sa natuluyang mamatay. Dahil sa pangayayaring iyon ay nagkaroon ng imbestigasyon ang mga gwardya sibil. Ikinulong nila si Don Rafael at dito na naglabasan ang mga lihim niyang kaaway.</p>
                                    <br> 
                                    <p> &nbsp;&nbsp;
                                        Pinaratangan siya ng pagiging erehe at pilibustero, pangangamkam ng lupain at iba pang ilegal na paraan sa pagpapayaman, ang pagbabasa ng El Correo de Ultramar at iba pang ipinagbabawal na babasahin, pagtatago diumano ng mga sulat at larawan mula sa isang binitay na pari, pakikipagkaibigan at pagkupkop sa mga tulisan, at ang pagsusuot ng Barong Tagalog.</p>
                                    <br> 
                                    <p> &nbsp;&nbsp;
                                        Nangawala at tumalikod ang mga dating kaibigan ng Don. Sa kabila ng paniniwala ng taong bayan na si Tinyente Guevarra ay nasisiraan ng bait, tanging siya na lamang ang naging kakampi ni Don Rafael. Ayon sa pakiusap ng Don, ang Tinyente rin ang humanap ng Kastilang abugado para dito. Sa kabila ng kahusayan ng abogado ay tumagal at naging masalimuot ang kaso dahil na rin sa pagsulputan ng mga kalaban ng Don.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang masaklap pa dito, hindi pa man tapos ang paglilitis ay nakakulong na at nagdadanas ng hirap sa loob ng rehas si Don Rafael. Dahil sa mga pangyayaring iyon ay labis na naapektuhan ang Don kung kaya’t ito ay nagkasakit. Tuluyang namatay sa bilangguan si Don Rafael. Wala man lamang nakiramay na kapamilya o kaibigan ang nasirang Don.</p>
