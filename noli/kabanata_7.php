                                    <br><center><h2>SUYUAN SA ASOTEA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sina Maria Clara at Tiya Isabel ay maagang nakapagsimba ng araw na iyon. Makatapos mag-almusal ay nagkanya-kanya na sila ng gawain. Naglinis ng bahay si Tiya Isabel dahil sa mga kalat bunga ng hapunan ng nakaraang gabi. Si Kapitan Tiyago ay nagbuklat ng mga kasulatan tungkol sa kabuhayan samantalang si Maria Clara ay nanahi habang kausap ang ama upang malibang ang sarili sapagkat ngayong araw ang nakatakdang pagkikita nila ni Ibarra. Hindi mapakali ang dalaga dahil sa pananabik na masilayan ang kanyang sinisinta. Napagpasyahan nilang magbakasyon sa San Diego dahil nalalapit na ang pista doon. Maya-maya pa’y dumating na si Ibarra at hindi maikakailang nataranta ang dalaga.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Pumasok pa sa silid si Maria Clara at tinulungan naman siya ng kanyang Tiya Isabel na ayusin ang sarili. Lumabas rin ito at nagkita ang dalawa sa bulwagan. Kapwa nagkaroon ng kaligayahan sa mga mata ng dalawa ng magtama ang kanilang paningin sa isa’t isa. Upang makapag-sarili at makaiwas na rin sa alikabok na likha ng pagwawalis ni Tiya Isabel ay nagtungo ang dalawa sa Asotea.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Doon ay masinsinan silang nag-usap tungkol sa kanilang nararamdaman at sa kanilang mga sinumpaan sa isa’t-isa. Binalikan nila ang ala-ala ng kanilang kamusmusan, ang kanilang naging tampuhan at mabilis na pagbabati. Pareho din nilang itinago ang mga ala-ala at bagay na ibinigay nila sa isa’t-isa. Kabilang dito ang dahon ng sambong na inilagay ni Maria Clara sa sumbrero ni Ibarra upang hindi ito mainitan, at ang sulat ni Ibarra kay Maria Clara bago ito umalis papuntang Europa. Binasa naman ng dalaga sa katipan ang laman ng sulat. Ayon sa sulat, layunin ni Don Rafael na pag-aralin si Ibarra sa malayong lugar upang makapaglingkod ng mataas na kalidad sa bayang sinilangan. Handa diumano itong magtiis na mawalay sa anak upang sa bandang huli ay maibigay nito sa bayan ang kanyang hangarin.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Natigilan naman si Ibarra dahil naalala nito na bukas ay undas at marami siyang kailangang gawain. Nagpaalam na si Ibarra na binilinan ng Kapitan na sabihan ang kanyang katiwala na sila ay magbabakasyon doon. Si Maria Clara naman ay hindi napigilang maluha dahil sa pangungulila kay Ibarra. Sinabihan siya ng kanyang ama na ipagtulos si Ibarra ng dalawang kandila at ialay sa santo ng manlalakbay.</p>
