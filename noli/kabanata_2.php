                                    <br><center><h2>SI CRISOSTOMO IBARRA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa pagdating ni Kapitan Tiyago kasabay ng isang binata na halatang nagluluksa ayon sa kasuotan nito, agad niyang binati ang mga panauhin at humalik naman sa kamay ng mga pari.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nabigla ang mga pari lalo na si Padre Damaso ng makilala nito na ang binata. Ipinakilala ng Kapitan ang binata bilang anak ng kanyang nasirang kaibigan na si Don Rafael Ibarra. Pitong taon diumanong nag-aral ang kagalang-galang na binata sa Europa na kadarating lamang.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kusa namang nagpakilala si Ibarra bilang Juan Crisostomo Ibarra y Magsalin kasabay ng pakikipag-kamay nito, isang kaugaliang natutunan niya sa bansang Alemanya. Ngunit tumangging makipag-kamay si Padre Damaso at sa halip ay tinalikuran nito si Ibarra.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Samantala, lumapit naman ang Tinyente kay Ibarra at nagpasalamat sa ligtas niyang pagdating. Pinuri rin niya ang kabaitan ng ama ni Ibarra na siya namang nagpanatag sa kalooban ng binata.</p>
                                    <br> 
                                    <p> &nbsp;&nbsp;
                                        Palihim na tiningnan ni Padre Damaso ang Tinyente na tila ba nagbabanta kaya naman tinapos na ng Tinyente ang pakikipag-usap nito kay Ibarra.</p>
                                    <br> 
                                    <p> &nbsp;&nbsp;
                                        Nang malapit ng maghapunan ay inanyayahan naman ni Kapitan Tinong si Ibarra sa pananghalian kinabukasan ngunit magalang na tumanggi ang binata dahil papunta daw ito sa San Diego sa araw na iyon. Si Kapitan Tinong ay malapit na kaibigan ni Kapitan Tiyago at kaibigan din ng ama ni Ibarra.</p>
