                                    <br><center><h2>SI SISA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Abalang-abala si Sisa na ina nina Basilio at Crispin sa isang maliit na dampa sa labas ng bayan. Siya’y salat sa kabuhayan. May likas mang ganda ngunit pinatanda na siya ng panahon at pagdurusa.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nakapag-asawa si Sisa ng isang lalaking walang idinulot sa kanya kundi sakit sa kalooban. Tamad ito, mahilig sa sugal, iresponsable at nagpapagala-gala lang sa lansangan. Wala siyang pakialam sa kanyang mag-iina. Madalas pa nga ay binubugbog nito si Sisa kapag siya ay umuuwi sa kanila. Patuloy lamang na tinitiis ni Sisa ang ginagawa sa kanila ng asawa. Minamahal pa rin niya ito at sinasamba na akala mo’y diyos.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nung araw na iyon ay naghanda ng masarap na hapunan si Sisa para sa kanyang dalawang anak. Minsan lamang itong mangyari dahil na rin sa kasalatan nila sa buhay. Inihain niya ang paborito ni Crispin na tuyong tawilis at sariwang kamatis samantalang tapang baboy-damo naman at isang hita ng patong bundok ang para kay Basilio. Galing ang lahat ng iyon kay Pilosopo Tasyo. Ngunit sa kasamaang palad ay naunang dumating ang asawang tamad at iresponsable ni Sisa. Inubos niyang lahat ng inihain ng asawa para sa mga anak at hindi man lamang nagtira para sa sa kanila. Ni hindi niya itinanong kung kamusta na ba ng kanyang mag-iina, sa halip ay nagbilin pa ito na bigyan siya ng kwarta mula sa kita ng dalawang bata. Wala nang nagawa si Sisa kundi maghinagpis sa pag-aalalang wala nang masarap na hapunan ang kanyang dalawang supling. Nagluto siyang muli para naman may makain ang mga anak pagdating galing sa simbahan. ‘Di nagtagal ay nainip si Sisa sa kahihintay sa mga anak. Nakarinig na lamang siya ng malalakas na tawag mula kay Basilio.</p>
