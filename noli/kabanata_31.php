                                    <br><center><h2>ANG SERMON</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Si Padre Damaso ay nagsimulang magsermon sa wikang Tagalog at Kastila mula sa isang sipi sa Bibliya. Ang kabuuan ng kanyang sermon ay tumatalakay sa kanyang pagpupuri sa mga banal na santo ng simbahan, ang pagtulad kay Haring David, ang mapagwaging si Gideon, at si Roldan na isang tapat na mananampalataya. Ngunit higit sa lahat, kasama sa kanyang sermon ang panlilibak ng pari sa mga Pilipino na binigkas niya sa wikang Kastila kaya walang kamalay-malay ang nakararami sa kahulugan ng kanyang mga sinasabi. Nagpatusada din si Padre Damso sa mga taong hindi niya gusto para ipahiya ang mga ito sa karamihan. Dahil marami sa mga naroon ay walang naiintindihan sa pinagsasasabi ng pari, hindi na nila napigilang antukin at maghikab, kabilang sa mga ito si Kapitan Tiyago.
                                    <br><br>
                                    <p> &nbsp;&nbsp;
                                       Palihim namang nagsusulyapan sina Maria at Ibarra na kapwa ang mga mata’y nangungusap. Sa wakas ay sinimulan na rin ni Padre Damaso ang kanyang sermon sa wikang Tagalog. Dito’y walang pakundangan nyang tinuligsa si Ibarra. Bagaman hindi niya pinangalanan kung sino ang kanyang tinutukoy ay halata namang si Ibarra ang kanyang pinatatamaan. Hindi natuwa si Padre Salvi sa mga nagaganap kaya nagpakuliling na ito, hudyat para tapusin na ng pari ang kanyang sermon. Tila walang narinig si Padre Damaso kaya nagpatuloy lamang ito sa kanyang walang kwentang sermon na umabot pa ng kalahating oras. Samantala, sa loob ng simbahan ay palihim na nakalapit si Elias kay Ibarra at binalaan ito na mag-ingat at huwag lalapit sa bato na ibabaon sa hukay sapagkat maaari niya itong ikamatay. Walang nakapansin ni isa sa pagdating at pag-alis ni Elias.</p>
