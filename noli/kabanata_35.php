                                    <br><center><h2>MGA USAP-USAPAN</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Naging mainit na usapan sa buong bayan ng San Diego ang mga kaganapan sa nangyaring pananghalian. Karamihan ay pinanigan si Padre Damaso dahil kung nagtimpi lamang daw si Ibarra ay hindi sana iyon nangyari. Tanging si Kapitan Martin lamang ang nakaunawa sa ikinilos ni Ibarra dahil walang sinuman ang hindi makapapagpipigil kung ang ama ang lalapastanganin. Ipinapalagay naman ni Don Filipo na hinihintay daw ni Ibarra na tulungan siya sa taumbayan bilang pagtanaw ng utang na loob sa kabutihang nagawa niya at ng kanyang ama. Samantala, nanindigan ang kapitan ng bayan na wala silang magagawa sapagkat laging nasa katwiran ang mga prayle. Ani Don Filipo, nangyayari ito dahil hindi nagkakaisa at watak-watak ang mga taumbayan samantalang ang mga prayle at mayayaman ay nagkakabuklod-buklod.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ani Don Filipo, nangyayari ito dahil hindi nagkakaisa at watak-watak ang mga taumbayan samantalang ang mga prayle at mayayaman ay nagkakabuklod-buklod. Natakot naman ang mga matatandang babae sa bayan dahil kung ‘di nila panigan si Padre Damaso ay baka mapunta sila sa impyerno. Nalugod naman si Kapitana Maria sa pagtatanggol ng binata sa kanyang ama. Ang mga magsasaka’y biglang nawalan ng pag-asa dahil baka hindi matuloy ang paaralan. Kapag nangyari iyon ay baka hindi makatapos sa pag-aaral ang kanilang mga anak. May kumakalat na baka hindi na matutuloy ang pagpapatayo ng simbahan sapagkat tinawag na pilibustero ng prayle si Ibarra. Hindi naman maintindihan ng mga magsasaka ang kahulugan ng Pilibustero.</p>
