                                    <br><center><h2>MGA BALAK O PANUKALA</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Tuloy-tuloy sa silid ng dalaga si Padre Damaso na halata sa mukha ang pag-aalala kay Maria Clara. Siya’y nanangis at sinabi sa anak na hindi ito mamamatay. Nagtaka naman ang lahat sa ipinakita ng pari at ‘di nila akalain na sa kabila ng magaspang nitong pag-uugali ay marunong pala itong umiyak at malambot din ang kalooban. Kanila ring naisip na mahal na mahal ni Padre Damaso si Maria Clara. Ilang sandali pa’y tumayo na ito at pumunta sa silong ng balag upang doon managhoy. Nang naibsan ang damdamin ni Padre Damaso ay sinamantala ito ni Donya Victorina at ipinakilala si Linares.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Anang Donya, inaanak si Linares ni Carlicos na bayaw ni Padre Damaso. Iniabot na rin ni Linares ang sulat sa pari kung saan nakasaad na naghahanap siya ng mapapangasawa at trabaho. Ayon kay Padre Damaso, madali lamang matatanggap si Linares dahil ito ay naging abogado sa Universidad Central. Sa usapin naman ng mapapangasawa ay iminungkahing kakausapin ng pari si Kapitan Tiyago. Ito nama’y ikinalungkot ni Padre Salvi. Samantala, si Lucas ay pumunta kay Padre Salvi para isangguni ang marapat na katarungan para sa kanyang kapatid. Pilit na pinapatulo ni Lucas ang kanyang luha at umarte pang tila kawa-awa upang kahabagan siya ng pari. Sinabi pa niya na binigyan lamang siya ng limang-daang piso ni Ibarra kapalit ng buhay ng kanyang yumaong kapatid. Di naman natuwa ang pari sa ipinakitang kaartehan ni Lucas kaya pinagtabuyan niya ito. Walang nagawa ang bubulong-bulong na oportunistang si Lucas na napahiyang nilayasan ang pari.</p>
