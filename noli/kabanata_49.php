                                    <br><center><h2>ANG TINIG NG MGA PINAG-UUSIG</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Wari’y hindi nasisiyahan si Ibarra nang lumulan sa bangka ni Elias kaya kaagad na humingi ito ng paumanhin sa pagkagambala niya sa binata. Ayon kay Ibarra ay nakasalubong niya ang Alperes at gusto siya nitong makausap. Nag-aalala siya na baka makita si Elias kaya ito’y nagdahilan na lamang. Naalala rin niya ang pangako na dalawin si Maria Clara. Hindi na nag-aksaya pa ng panahon si Elias at sinabi na sa binata ang kanyang pakay. Ayon sa kanya’y siya ang sugo ng mga sawimpalad. Bagama’t ipinaliwanag ni Elias ang napagkasunduan ng puno ng mga tulisan (Kapitan Pablo) na hindi niya binanggit ang pangalan, sinubukan pa rin niyang sabihin ang mga kahilingan ng mga sawimpalad na humihingi ng pagbabago sa pamahalaan tulad ng paglalapat ng katarungan, pagbibigay ng dignidad sa mga tao, at pagbawas ng kapangyarihan sa mga guwardiya sibil na nagiging dahilan ng kanilang pag-aabuso sa karapatang pangtao.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Handa man si Ibarra na gamitin ang pera para humingi ng tulong sa mga kaibigan niya sa Madrid at pati sa Kapitan Heneral ay iniisip niya na sa halip makabuti ay baka lalong makasama ang kanilang balak. Ayon kay Ibarra, ang pagbawas ng kapangyarihan ng sibil ay makasasama dahil baka malagay naman sa panganib ang mga tao. Dagdag pa niya, para magamot ang sakit ay kailangang gamutin ang sakit mismo at hindi ang sintomas lang dahil kapag malala na ang sakit, kung kailangan ang dahas para ito ay masugpo ay kailangang ilapat ang panlunas kahit na mahapdi.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nagdebate sina Elias at Ibarra tungkol sa buting nagawa ng simbahan at ang sanhi ng panunulisan ng mga tao. Bagaman kapwa mahal ng dalawa ang bayan, sa bandang huli ay hindi napapayag ni Elias si Ibarra sa kaniyang pakiusap kaya sinabi nitong sasabihin niya sa mga sawimpalad na umasa na lang sa Diyos.</p>
