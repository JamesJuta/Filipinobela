                                    <br><center><h2>MGA ALAALA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Maganda ang araw noong panahong iyon. Binabagtas ni Ibarra ang kahabaan ng Maynila sakay sa kalesa at ang tanawin sa paligid ay nakapagpabalik ng kanyang mga alaala. Katulad pa rin ng dati ang kanyang namasdan. Mga kalesa at karumatang walang humpay sa pagbyahe, salimbayan ng mga taong abala sa pangangalakal at kanya-kanyang mga gawain: may mga Europeo, Intsik, Pilipino; may mga lalaking kargador, ang iba ay mga kababaihang nagtitinda ng prutas. Ang mga tindahan at mga hayop na kasama sa paghahanapbuhay ay nandoon din. Walang pinagbago ang puno ng talisay sa San Gabriel samantalang imbes na umunlad ay pumangit naman ang Escolta.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nagmamadali namang ihatid ng mga karwahe ang mga kawani sa tanggapan at mga pari, kabilang na si Padre Damaso. Siya’y nakita ni Kapitan Tinong kaya binati siya nito. Sa kalye ng Arroceros ay napadako si Ibarra at naalalang minsan ay nahilo siya sa napakasamang amoy ng tabako doon. Naikumpara din niya ang mga hardin sa Europa ng mapadaan siya sa Hardin ng Botaniko.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang buong Maynila para sa kanya ay walang pinag-unlad, bagkus ang mga gusali ay nilulumot lamang ng panahon. Dahil dito, sumagi sa isipan ni Ibarra ang sinabi ng kanyang gurong pari. Una; Ang karunungan ay matatamo kapag hinangad ng puso, pangalawa; Ang karunungan ay dapat linangin at isalin sa susunod na henerasyon at ang pangatlo; Dapat lamang na magkaroon ng pakinabangan – kung ang mga kastila ay nanatili dito upang kuhanin ang yaman ng bansa, marapat lamang na ibigay naman ng bansang dayuhan ang karunungan at edukasyon.</p>
