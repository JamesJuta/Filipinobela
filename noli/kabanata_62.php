                                    <br><center><h2>ANG PAGTATAPAT NI PADRE DAMASO</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kinaumagahan, kahit na maraming regalong nakabunton sa itaas ng hapag ay ‘di naman ito pansin ni Maria Clara. Nakapako ang kanyang mga mata sa dyaryong nagbabalita tungkol sa pagkamatay o pagkalunod ni Ibarra. Ngunit hindi naman iyon binasa ng dalaga. Ilang sandali pa’y dumating na si Padre Damaso na hinilingan kaagad ni Maria na sirain ang kasunduan ng kanyang kasal kay Linares at pangalagaan ang kapakanan ng ama. Dagdag pa ng dalaga, ngayong patay na si Ibarra ay wala nang simumang lalaki ang kanyang pakakasalan dahil para sa kanya, dalawang bagay na lang ang mahalaga; ang kamatayan o ang kumbento.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Napag-isip-isip ng pari na paninindigan ni Maria Clara ang kanyang sinabi kaya naman humingi ito ng tawad sa kanya. Napaiyak pa ito ng malakas habang binibigyan diin ang walang kapantay na pagtingin niya kay Maria. Wala namang nagawa ang pari kundi piliing pahintulutan ang dalaga na pumasok sa kumbento kaysa piliin ang kamatayan. Malungkot na umalis si Padre Damaso. Tumingala siya sa langit sabay bulong na totoo nga umanong may Diyos na nagpaparusa. Hiniling niya sa Diyos na siya na lang daw ang parusahan kaysa ang anak niyang walang malay at nangangailangan ng kanyang kalinga. Ramdam ng pari ang labis na kalungkutan ng kanyang anak na si Maria Clara.</p>
