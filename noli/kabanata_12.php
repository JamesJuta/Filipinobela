                                    <br><center><h2>ARAW NG MGA PATAY</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Matatagpuan sa isang malawak na palayan na nababakuran ng lumang pader at kawayan ang sementeryo ng San Diego. May malaking krus na nakatirik sa kalagitnaan nito. Masukal ang libingan at makipot ang daan patungo sa sementeryo. Maputik kung tag-ulan at maalikabok naman kung tag-araw.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Noong gabing iyon ay napakalakas ng buhos ng ulan. Dalawang tao ang abala sa paghuhukay sa isang bahagi ng sementeryo. Ang isa ay matagal nang sepulturero samantalang ang katulong nito ay bago pa at hindi mapakali sa kanilang ginagawa. Iniahon ng dalawa ang bangkay na kanilang hinukay na dalawampung araw pa lamang naililibing mula ng mamatay. Ang utos na paghukay sa bangkay at paglipat nito sa libingan ng mga Intsik ay mula kay Padre Garrote na walang iba kundi si Padre Damaso, ang kura paroko ng panahong iyon.</p>
