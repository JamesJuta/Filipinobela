                                    <br><center><h2>ISANG TALA SA GABING MADILIM</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nang araw na iyon ay nagpunta sa Maynila si Ibarra at nanuluyan sa Fonda de Lala. Sa loob ng kanyang silid ay nagmuni-muni ito tungkol sa sinapit ng ama. ‘Di nagtagal ay napatingin ito sa durunguwan. Sa kabila ng ilog ay tanaw na tanaw ng binata ang nagliliwanag na bahay ni Kapitan Tiyago. Tila naririnig pa niya ang kasayahan sa loob ng bahay, ang tugtog ng mga orkestra at kalansingan ng mga pinggan at kubyertos.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        May nagaganap muling isang kasiyahan sa bahay ng Kapitan noong gabing iyon. Dumating ang nag-iisang anak na dalaga ni Kapitan Tiyago na si Maria Clara. Sinalubong naman ang dalaga ng kanyang mga kaibigan, kababata, mga Kastila at paring malalapit sa ama, mga Pilipino, Intsik, at militar.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nakasuot ng isang marangyang kasuotan si Maria Clara na napapalamutian ng alahas na diyamante at ginto. Ang lahat ay nakatuon ang paningin sa kagandahan ng dalaga. Matiyaga namang inaayos ni Donya Victorina ang buhok ni Maria Clara. Mahilig sa magagandang dilag si Padre Salvi kung kaya’t siya ay masayang-masaya at kadaupang palad niya ang mga dalaga roon. May lihim din itong pagtingin kay Maria Clara.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Madali namang nakatulog si Ibarra ng gabing iyon. Kabaligtaran ni Padre Salvi sapagkat hindi mawala sa kanyang isipan ang magandang dalaga na si Maria Clara.</p>
