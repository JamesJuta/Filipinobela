                                    <br><center><h2>LIWANAG AT DILIM</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ngayon na ang araw ng pagdating nina Maria Clara at Tiya Isabel sa San Diego upang mamalagi doon para sa pista ng bayan. Ang balitang pagdating ni Maria Clara ay agad na kumalat dahil kinagigiliwan siya ng lahat ng mga tao doon. Kumalat din umano ang madalas na pagkikita nila ni Ibarra na ikinagalit naman ni Padre Salvi. Napapansin pala ng dalaga ang pagbabago ng kilos at mga titig nito tuwing siya ay kaharap. Samantala, plano nina Maria Clara at Ibarra na mag-piknik kasama ang kanilang mga kaibigan sa ilog. Iminungkahi ng dalaga na huwag isama si Padre Salvi dahil nababahala siya kapag ito ay nasa paligid. Hindi naman ito sinang-ayunan ni Ibarra dahil hindi raw ito magandang tingnan. Bigla namang dumating ang pari habang nag-uusap ang dalawa kaya nagpaalam na ang dalaga upang mamahinga.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Pagdaka’y inimbitahan ni Ibarra si Padre Salvi sa piknik na kanilang pinagpa-planuhan at kaagad naman itong sinang-ayunan ng pari. Umuwi na rin si Ibarra makalipas ang ilang oras. Sa daan habang naglalakad pauwi ay may nakasalubong si Ibarra na isang lalaki na humihingi ng tulong. Agad naman itong tinulungan ng binata.</p>
