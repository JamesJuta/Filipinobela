                                    <br><center><h2>MGA SULIRANIN NG MGA GURO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang mga tao na Sa tabi ng lawa ay nagkita sina Ibarra at ang guro sa San Diego. Itinuro ng guro kung saan naitapon ang bangkay ng ama ng binata. Isa diumano si Tinyente Guevarra sa iilang nakipaglibing sa kanyang ama. Sinabi ng guro ang ginawang pagtulong ni Don Rafael sa ikauunlad ng edukasyon sa kanilang bayan at naitulong nito sa kanyang kapakanan. Tumulong kasi ang ama ni Ibarra sa pangangailangan ng guro sa pagtuturo noong siya ay nagsisimula pa lamang. Inisa-isa din ng guro ang ang mga kinakaharap na suliranin ng San Diego tungkol sa edukasyon.
                                    <br><br>
                                    Aniya, ang mga ito raw ay ang mga sumusunod:<br>
                                       ○ ang kawalang ng panggastos para sa mga kagamitan sa pag-aaral,<br>
                                       ○ ang kawalan ng silid aralan na akma upang makapag-aral ng walang balakid ang mga bata,<br>
                                       ○ ang kakaibang pananaw ng mga pari sa paraan ng pagtuturo,<br>
                                       ○ ang mga patakaran ng simbahan tungkol sa nilalaman ng kanyang mga aralin,<br>
                                       ○ at ang kawalan ng pagkakaisa ng mga magulang ng mag-aaral at ng mga taong may katungkulan.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kahit anong tiyaga ng guro na ituro sa kanyang mga estudyante ang nilalaman ng mga libro na nakasulat sa wikang Kastila ay pilit itong pinanghihimasukan ni Padre Damaso. Pinapalo at minumura diumano ng pari ang mga bata sa tuwing makaririnig ito ng ingay mula sa tapat ng kwadra kung saan nag-aaral ang mga bata dahil nga wala silang silid-aralan. Pinapanigan naman ng mga magulang ang mga pari tungkol sa pagpalo bilang epektibong paraan ng pagdidisiplina at pagtuturo sa kanilang mga anak. Dahil sa pakikialam ng pari at mga balakid sa pagtuturo ang siyang sinisising dahilan ng guro sa kanyang pagkakasakit. Nang bumalik naman siya sa pagtuturo ay napansin niyang nabawasan ang bilang ng kanyang mga mag-aaral.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang guro ay natuwa ng malaman na hindi na si Padre Damaso ang kura sa San Diego kaya minabuti niyang iangkop ang nilalaman ng mga aralin sa kalagayan ng kanyang mga mag-aaral. Kahit pa nagkaroon ang kalayaan ang guro na iangkop ang kanyang mga aralin ay higit pa ring pinahalagahan ng simbahan ang pagtuturo tungkol sa relihiyon. Nangako naman si Ibarra na gagawin ang kanyang makakaya upang matulungan ang guro at maiangat ang kalagayan ng edukasyon sa bayan. Kanya raw babanggitin sa araw ng pulong sa paanyaya ni Tinyente Mayor ang mga napag-usapan nila ng guro.</p>
