                                    <p> &nbsp;&nbsp;
                                        Ang mga naganap sa kapistahan ng San Diego ay nalathala sa pahayagan sa Maynila. Kasama sa naiulat ang marangyang kapistahan ng bayan, mga kilalang tao sa San Diego, ang naganap na palatuntunan pati na rin ang musiko. Nabalita rin ang mga pari at ang komedyang naganap sa bayan pati na ang mga mahuhusay nitong mga artista. Ang mga Kastila lamang ang nasiyan sa komedya dahil ito ay idinaos sa wikang Kastila. Nasiyahan naman ang mga Pilipino sa komedyang tagalog. Samatala, si Ibarra’y walang dinaluhan sa mga palabas na iyon.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nagkaroon ng prusisyon para sa mga santo at santa kinabukasan kung saan ang misa ay pinamunuan ni Padre Manuel Martin. Nagkaroon din ng sayawan na pinangunahan ng mag-ama na sina Kapitan Tiyago at Maria na ikinayamot naman ng dalaga. Pagdaka’y sinulatan ni Maria Clara si Ibarra dahil ilang araw na niya itong hindi nakikita. Hiniling niyang siya ay dalawin ng binata at imbitahan siya nito sa pagpapasinaya ng bahay-paaralan na kanyang ipinatayo.</p>
