                                    <br><center><h2>ANG DALAWANG PANAUHIN</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Nang gabing iyon ay hindi dinalaw ng antok si Ibarra. Siya’y balisa sa kaguluhang naganap kaya nilibang na lamang nito ang sarili sa paggawa sa kanyang laboratoryo. Maya-maya’y biglang dumating si Elias upang ipaalam kay Ibarra na si Maria ay may sakit at kung may ipagbibilin ito bago siya pumunta sa Batangas. Ipinaliwanag din niya kay Ibarra kung paano niya napatigil ang kaguluhan noong gabing iyon. Aniya, kilala daw umano niya ang magkapatid na gwardia sibil kaya naman napahinuhod ang dalawa dahil sa kanilang utang na loob dito. Ilang sandali pa’y umalis na rin si Elias.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Si Ibarra nama’y nagmamadaling gumayak upang pumunta sa bahay ni Kapitan Tiyago. Habang naglalakad ay nakasalubong ni Ibarra ang kapatid ng taong dilaw na si Lucas. Kinulit siya nito tungkol sa makukuhang salapi ng kanilang pamilya dahil sa pagkamatay ng kanyang kapatid. Siya nama’y sinagot ng maayos ni Ibarra na magbalik na lamang sa isang araw dahil papunta siya sa maysakit. Ngunit talagang mapilit si Lucas kaya bago pa man mawala ang pagtitimpi ni Ibarra ay tumalikod na lamang ito. Naiwang nagpupuyos ang kalooban ni Lucas at inisip na iisa ang dugong nananalaytay sa ugat nina Ibarra at ng lolo nito na nagparusa sa kanilang ama.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Para sa kanya’y maari lamang silang maging magkaibigan kung magkakasundo sila sa salaping ibabayad ni Ibarra.</p>
