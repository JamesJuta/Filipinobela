                                    <br><center><h2>MGA SULIRANIN TUNGKOL SA BAYAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nang araw na iyon ay nakatakdang kuhanin ni Maria Clara ang kanyang kagamitan sa kumbento. Ang kanyang Tiya Isabel ay naghihintay na sa karawahe upang tuluyan na silang makaalis. Siya namang pagdating ni Padre Damaso. Hindi minabuti ng pari ang kanilang pag-alis kaya bubulong-bulong itong umakyat papunta sa bahay ng Kapitan. Agad naman siyang sinalubong ng Kapitan at akmang magmamano sa kamay nito ngunit tinanggihan ito ng pari. Sa halip ay sinabi na agad nito ang kanyang pakay sa Kapitan. Ani Padre Damaso, hindi raw dapat maglihim sa kanya si Kapitan Tiyago dahil siya ang pangalawang ama ni Maria Clara. Sinabi din niyang dapat na raw itigil ng dalaga ang pakikipag-mabutihan nito kay Ibarra. Dagdag pa ng pari, hindi daw dapat maghangad ng kabutihan ang Kapitan para sa kanyang mga kaaway. Ang Kapitan naman ay nakumbinsi sa mga sinabi ni Padre Damaso kung kaya pag-alis nito ay agad na pinatay ng Kapitan ang mga kandilang itinulos ni Maria Clara para sa paglalakbay ni Ibarra pauwi sa bayan ng San Diego. </p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Samantala, si Padre Sybila ay nagtungo sa kumbento ng Dominikano sa Puerta de Isabel II. Dito ay dinalaw niya ang paring may matinding sakit. Kanyang ibinalita ang mga nakaraang kaganapan kagaya ng pang-aaway na ginawa ni Padre Damaso sa bahay ni Kapitan Tiyago, ang pagpanig diumano ng Tinyente sa Kapitan Heneral, at pakikipag-alyansa kay Padre Damaso. Ang matandang may sakit ay nakipagpalitan din ng saloobin at sinabing ang pagtaas ng buwis ang dahilan ng pagkaubos nang kanilang mga kayamanan. Aniya, natuto na ang mga Pilipino sa paghawak ng ari-arian.</p>
