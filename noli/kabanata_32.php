                                    <br><center><h2>ANG PAGHUGOS</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Ang taong dilaw ay nagkaroon ng demonstrasyon kay Nol Juan tungkol sa paggamit ng panghugos bago ganapin ang pagpapasinaya sa paaralan. Ang nasabing istruktura ay may walong metro ang taas at ang apat na haligi ay nakabaon sa ilalim ng lupa. May apat na haligi ito na nasasabitan ng malalaking lubid na kapag tiningnan ay para bang napakatibay ng pagkakayari. Ang taong dilaw naman ay ipinagmamalaki na kanyang natutuhan ang ganitong paraan sa ninuno pa ni Ibarra na si Don Saturnino. Iminustra niya kung papaano itinataas at ibinababa ang batong malaki na siyang ibabaon sa hukay na napapagitnaan ng apat na haligi. Pinuri ito ng mga tao sa paligid at maging si Nol Juan ay hinangaan siya.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Dumating ang araw ng pagpapasinaya sa bahay-paaralan. Naghanda ang mga guro at mag-aaral ng mga pagkain para sa mga panauhin. May banda din ng musiko. Nagsimula nang basbasan ni Padre Salvi ang bahay-paaralan.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang mga mahahalagang kasulatan maging ang relikya at iba pang mahahalagang bagay ay inilulan sa isang kahang bakal na ipinasok naman sa bumbong na yari sa tingga. Ang nagko-kontrol sa bato ay ang lubid na may hukay sa gitna kung saan ilalagay ang tingga. Ang taong dilaw naman ang may hawak ng nasabing lubid. Nagsibabaan ang lahat ng importanteng tao upang sumaksi nang magsimula na ang pari sa seremonya. Isa sa mga taong naroroon upang sumaksi sa okasyon ay si Elias at ‘di winaglit ang tingin sa taong dilaw. Pagbaba ni Ibarra upang makiisa sa pagsaksi ay humulagpos ang lubid mula sa kalo at kasabay nito ay ang pagkagiba ng balangkas. Ilang saglit lang ay nasaksihan ng lahat na si Ibarra ay nakatayo sa pagitan ng nasirang kalo at ng malaking bato ngunit ang taong dilaw ay namatay. Ipahuhuli sana ng alkalde si Nol Juan ngunit pinigilan siya ni Ibarra at sinabing siya na ang bahala sa lahat.</p>
