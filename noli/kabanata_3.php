                                    <br><center><h2>ANG HAPUNAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa hapag-kainan, ang mga panauhin ay may kanya-kanyang kilos at nadarama na kung panonorin ay parang isang komedya. Si Padre Sibyla na nasisiyahan ay kabaligtaran naman ni Padre Damaso na walang pakundangan kung makapagdabog at natamaan pa ang isang kadete.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ito naman ay hindi pinansin ng Tinyente, sa halip ay masusi nitong pinagmasdan ang kulot na buhok ni Donya Victorina. Hindi niya namalayan na natapakan na niya ang kola ng saya ng Donya na ikinainis naman nito.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Samantala, ang ibang mga bisita naman ay may kanya-kanyang usapan at papuri sa masarap na handa ng Kapitan. Dahil sa ang hapunan ay para sa pagsalubong sa binatang si Ibarra, karapat-dapat naman na siya ay maupo sa kabisera. Ang dalawang pari naman ay nagtalo kung sino ang dapat maupo sa kabilang dulo ng kabisera.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ani Padre Damaso, si Padre Sibyla ang dapat maupo sa dulo ng kabisera dahil siya ang kura sa lugar na iyon. Bagay naman na sinalungat ni Padre Sybila dahil ayon sa kanya, si Padre Damaso ang padre kumpesor ng pamilya ni Kapitan Tiyago. Sa kalaunan, inalok ni Padre Sybila ang upuan sa Tinyente na agad naman nitong tinanggihan. Tumanggi rin si Kapitan Tiyago ng siya namang inanyayahan ni Ibarra.</p>
                                    <br> 
                                    <p> &nbsp;&nbsp;
                                        Nang ihain ang pagkain ay ‘di sinasadyang mapunta ang hindi masasarap na bahagi ng manok kay Padre Damaso. Lalo itong nag-alburoto dahil mga leeg at pakpak ang laman ng kanyang tinola. Lingid sa kaalaman ng pari, ang espesyal na tinola ay para lamang kay Ibarra.</p>
                                    <br> 
                                    <p> &nbsp;&nbsp;
                                        Sa kainan ay nagbahagi ng kanyang buhay ang binata tulad ng pag-aaral nito sa Europa ng pitong taon, ang pagpunta niya sa iba’t ibang bansa at pag-aaral niya ng kasaysayan, ang pagsasalita ng iba’t ibang wika, ang ‘di nito paglimot sa kanyang bayan sa kabila ng kaunlarang namasdan sa ibang bansa, maging ang hindi pagkaka-alam sa tunay na dahilan na nangyari sa kanyang ama. Dahil dito, nakumpirma ang hinala ng Tinyente na wala ngang alam ang binata sa nangyari sa ama nito.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Naikwento rin ni Ibarra na sa kanyang mga bansang napuntahan, iisang antas lamang ang kabuhayan, pulitika at relihiyong tinatamasa doon. Ito ay sa kadahilanang pinahihintulutan ito ng sarili nilang kalayaan at kakayanang pamahalaan ang kanilang sariling bansa.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Agad namang binatikos at ininsulto ni Padre Damaso ang binata na ayon sa kanya ay kahit paslit ay kaya itong matutunan. Dagdag pa ng pari, ang pagpunta diumano ni Ibarra sa Europa ay maliwanag na pag-aksaya ng salapi.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Magalang na tinanggap ni Ibarra ang sinabi ng pari. Binanggit na lamang ng binata ang mga ala-ala niya tungkol kay Padre Damaso na ayon sa kanya ay karaniwan nang kasalo sa kanilang hapag-kainan at malapit din na kaibigan ng kanyang ama. Hindi na nakakibo ang pari dahil sa mga naging kaganapan sa pagitan nila ng ama ni Ibarra.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Samantala, matapos ang pagtitipon ay maagang nagpaalam si Ibarra kaya’t hindi sila nagkita ni Maria Clara, anak na dalaga ni Kapitan Tiyago. Nagpatuloy pa rin sa pag-alipusta si Padre Damaso kay Ibarra. Ang obserbasyon ng binata noong gabing iyon ay kanya namang isinulat sa pahayagan ng Estudios Coloniales.</p>
