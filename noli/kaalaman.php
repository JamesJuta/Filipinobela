                            <h1 class="katanungan_title title"><i class='bx bx-help-circle icon'></i> Ano ang Kabanata?</h1>
                                
                                <div class="katanungan_second_container">

                                    <p> &nbsp;&nbsp;
                                        Ang "kabanata" ay isang bahagi o yunit ng isang aklat, nobela, o iba pang akda na mayroong pagkakasunod-sunod ng mga pangyayari o temang nauugnay sa isa't isa. Ito ay karaniwang ginagamit upang hatiin ang isang aklat o nobela sa mga mas maliit na bahagi para mas madaling maorganisa at maunawaan ng mga mambabasa.</p>
                                    <br>
                                    <p> &nbsp;  
                                        Sa mga nobela, ang kabanata ay madalas na may sariling pamagat o pangalan, at ito ay naglalaman ng bahagi ng kwento na may kinalaman sa pangunahing plot o tema ng nobela. Ito ay isang paraan ng pagpapakumbaba ng malalim na talaarawan sa mga pag-usbong ng kuwento.</p>
                                    <br><br>

                                    <h1>Uri ng Kabanata</h1><br>
                                    <p>&nbsp;&nbsp;
                                        May iba't ibang uri ng kabanata na maaaring matagpuan sa mga aklat, nobela, o iba pang akda, depende sa layunin ng may-akda at sa genre ng akda. Narito ang ilan sa mga uri ng kabanata:</p>
                                    <br>
                                    <p> 
                                        - <strong>Pagsasalaysay o kuwento:</strong> Ito ay ang uri ng kabanata kung saan isinasalaysay ang mga pangunahing pangyayari o pagkakabukas ng kuwento.</p>
                                    <p>
                                        - <strong>Pamagat o Introduksyon:</strong> Ito ay nagpapakilala sa mambabasa sa pangunahing temang tatalakayin sa kabanata o sa buong akda.</p>
                                    <p> 
                                        - <strong>Pagpapakilala sa mga Tauhan:</strong> Ito ay kabanata kung saan inilalarawan ang mga pangunahing tauhan, kanilang mga karakteristik, at background.</p>
                                    <p> 
                                        - <strong>Pag-aambag o pagsusuri:</strong> Maaaring may mga kabanatang naglalaman ng mga pagsusuri, repleksyon, o mga opinyon ng mga tauhan ukol sa mga pangyayari.</p>
                                    <p>
                                        - <strong>Paksa o Tema:</strong> Ito ay kabanata kung saan ipinakikilala o tinalakay ang pangunahing tema o mensahe ng akda.</p>
                                    <p> 
                                        - <strong>Pag-unlad ng Plot:</strong> Sa mga nobela, may mga kabanatang nagpapakita ng pag-unlad ng kuwento at pangunahing mga pangyayari.</p>
                                    <p> 
                                        - <strong>Pagpapakita ng Konflikto:</strong> May mga kabanatang nagpapakita ng pagkakaroon ng pagtutunggali o konflikto sa kwento.</p>
                                    <p> 
                                        - <strong>Paglalapat ng mga Palaisipan:</strong> Ito ay mga kabanata na naglalaman ng mga palaisipan o misteryo na nagpapalalim sa kwento.</p>
                                    <p> 
                                        - <strong>Paglalahad ng Solusyon:</strong> Kung ito ay isang nobela na may malalim na konflikto, may mga kabanatang nagpapalabas ng mga solusyon o paglutas ng problema.</p>
                                    <p> 
                                        - <strong>Pagwawakas:</strong> Ito ang kabanata kung saan iniuukit ang resolusyon ng kuwento, at kung paano natatapos ang mga kwento ng mga tauhan.</p>
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang uri at layunin ng mga kabanata ay maaaring mag-iba depende sa awtor, genre, at tema ng akda. Ang mga ito ay ginagamit upang magbigay ng organisasyon at kaayusan sa pagkukuwento.</p>
                                    <br><br>

                                    <h1>Halimbawa ng Korido</h1><br>
                                    <p> &nbsp;&nbsp;
                                        Isa sa mga halimbawa ng nobela ay ang “Noli Me Tangere” ni Dr. Jose Rizal.</p>
                                    <br>
                                    <p> &nbsp;
                                        Ang nobelang ito ay nagpapakita ng mga pangunahing tauhan, tulad ni Juan Crisostomo Ibarra, na bumalik mula sa Europa at naghahanap ng katotohanan ukol sa nangyari sa kanyang pamilya at sa kanyang bayan. Siya ay nasaksihan ang mga injustisya, pang-aabuso, at katiwalian sa lipunan.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Isa pang halimbawa ng korido ay ang “El Filibusterismo” ni Dr. Jose Rizal.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang nobelang ito ay naglalaman ng mga pangunahing tema tulad ng katiwalian, pang-aapi, at pagtutol sa mga mapanupil na sistema ng kolonyalismo. Ipinapakita nito ang matinding pagkadismaya at galit ng mga Pilipino sa mga pang-aabuso ng mga Kastila. Sa pamamagitan ng kwento ni Simoun, ito ay nagiging simbolo ng pagkakaroon ng boses at pagkilos upang harapin ang mga pag-aapi at abuso sa lipunan.</p>
