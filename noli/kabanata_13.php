                                    <br><center><h2>MGA BABALA NG BAGYO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nagtungo si Ibarra sa sementaryo ng San Diego kasama ang kanilang matandang katiwala upang hanapin ang puntod ng kanyang ama na si Don Rafael. Ayon sa katiwala, kanyang tinaniman ng mga bulaklak ng adelpa at sampaga ang libingan ng ama ng binata. Sa daan ay nasalubong nila ang sepulturero at tinanong nila rito kung saan naroroon ang libingan ng kanyang ama.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ikinasindak ni Ibarra ang ipinagtapat ng sepulturero. Ayon dito, itinapon nila ang bangkay ng kanyang ama sa lawa dahil sa kabigatan nito at hindi na nailibing sa libingan ng mga Intsik. Dagdag pa ng sepulturero, ang utos na kanyang sinunod ay galing sa kura paroko. Napaiyak na lamang ang matandang katiwala sa mga narinig. Matinding galit at poot naman ang naramdaman ni Ibarra kaya iniwan nito ang kausap.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nang makasalubong niya si Padre Salvi ay humingi siya ng paliwanag dito kung bakit nagawa nitong lapastanganin ang bangkay ng kanyang ama. Nalaman naman ni Ibarra sa bandang huli na si Padre Damaso pala ang may kagagawan ng lahat ng iyon.</p>
