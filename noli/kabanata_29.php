                                    <br><center><h2>ANG KAPISTAHAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Handa na ang mga banda ng musiko umaga pa lang upang magbigay saya at salubungin ang kapistahan. Sinabay pa ang tunog ng kampana at mga siklab at pasabog ng paputok. Samantalang ang mga tao sa bayan ay nagising na at nagsigayak para makiisa. Suot nila ang kanilang pinakamainan na kasuotan at mga alahas. Hinatak nila ang mga tao upang tikman ang masasarap na pagkaing kanilang hinanda.
                                    <br><br>
                                    <p> &nbsp;&nbsp;
                                        Hindi naman sang-ayon dito si Pilosopo Tasyo. Aniya, paglulustay lamang ng salapi at pagpapakitang tao lamang ang pagsasaya sa araw na ito. Mas marami umano ang may kabuluhan na dapat pagkagastusan at isama pa diyan ang maraming hinaing ng bayan na hindi natutugunan. Sang ayon naman naman si Don Filipo sa pananaw ng Pilosopo ngunit wala siyang lakas ng loob para salungatin ang mga pari. Naghihintay na ang mga tao sa simbahan pati na rin ang at mga tanyag na tao sa San Diego.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sinadya naman ni Padre Damaso na magkasakit upang higit na makakuha ng importansya mula sa lahat. Ang taga-pangasiwa ng simbahan ang nag-aalaga sa pari habang siya ay may sakit. Bandang alas-otso ng umaga, sinimulan ang mahabang prusisyon ng iba’t-ibang santo. Sa prusisyon ay makikita ang pagkakaiba ng antas o diskriminasyon sa lipunan. Kahit na ang mga nagpuprusisyon ay mga ginggon. Sa tapat ng bahay ni Kapitan Tiyago natapos ang prusisyon na inaabangan naman ng ibang mga Kastila pati na rin nina Maria Clara at Ibarra.</p>
