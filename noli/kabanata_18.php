                                    <br><center><h2>MGA KALULUWANG NAGHIHIRAP</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Matamlay na nagdaos ng misa si Padre Salvi ng araw na yaon. Ang mga matatanda ay abala sa nalalapit na pista sa kanilang bayan habang naghihintay naman na makausap ang pari. May nais silang malaman. Si Padre Damaso ba, si Padre Martin o ang koordinator ang magmimisa?</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang pagbili ng indulgencia para sa kaligtasan ng mga namatay na kaanak na patuloy na nagdurusa sa purgatoryo ay napag-usapan ng mga matatanda. Mahigit isang libong taon na kaligtasan mula sa pagdurusa sa purgatoryo ang katumbas ng isang indulgencia. Dahil sa kaabalahan ng mga matatanda sa kanilang pagpapalitan ng kuro-kuro ay hindi nila namalayan ang pagdating ni Sisa. May dala siyang handog para sa mga pari. Mga sariwang gulay mula sa kanyang mga tanim at pako na paborito ng kura ang dala ni Sisa. Nagtuloy siya sa kusina ng kumbento upang iayos ang kanyang mga dala-dalahan. Ni hindi siya pinansin ng mga sakristan at mga tauhan sa kumbento ngunit nakausap naman niya ang tagaluto doon. Nalaman niyang maysakit ang pari at hindi niya ito makakausap. Nagulat din siya ng malaman na ang anak niyang si Crispin ay tumakas kasama ni Basilio pagkatapos nitong magnakaw ng dalawang onsa. Alam na raw ito ng mga gwardiya sibil kaya papunta na ang mga ito sa kanilang bahay upang hulihin ang kanyang mga anak. Tinuya rin nito si Sisa dahil hindi daw niya tinuruan ng kabutihang asal ang magkapatid at higit sa lahat ay nagmana raw ang kanyang mga anak sa kanyang asawang walang silbi.</p>
