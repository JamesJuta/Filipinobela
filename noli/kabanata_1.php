                                        <center><h2>ANG PAGTITIPON</h2></center> 

                                        <br>
                                        <p> &nbsp;&nbsp;
                                            Sa gabing iyon ay nakatakdang ganapin ang marangyang handaan sa bahay ni Don Santiago Delos Santos o mas kilala bilang si Kapitan Tiyago upang magsilbing salubong sa isang binatang kagagaling lamang sa Europa. Hindi naman iba sa Kapitan ang binata dahil ito ay anak ng kanyang matalik na kaibigan.</p>
                                        <br>
                                        <p> &nbsp;&nbsp;
                                            Ang bahay ni Kapitan Tiyago na matatagpuan sa Kalye Anluwage ay napuno ng mga bisita. Isang malaking karangalan ang maging panauhin ng Kapitan. Si Kapitan Tiyago ay kilala sa pagiging matulungin sa mga mahihirap at nabibilang sa mataas na lipunan.</p>
                                        <br>
                                        <p> &nbsp;&nbsp;
                                            Ang pinsan ng Kapitan na si Tiya Isabel ang taga-istima ng mga bisita at ang mga panauhing babae at lalake ay sadyang magkakahiwalay. Nagpahuli namang dumating ang ibang mga panauhin kabilang na ang magkabiyak na sina Dr. de Espadaña at Donya Victorina. Hindi naman nagpahuli sa mga panauhin ng Kapitan ang kinatawan ng simbahan sa pangunguna nina Padre Sibyla, ang kura paroko ng Binundok; Si Padre Damaso na sadyang magaslaw kung kumilos at magsalita; dalawang paisano; at si Tinyente Guevarra, ang tenyente ng gwardya sibil.</p>
                                        <br>
                                        <p> &nbsp;&nbsp;
                                            Ang bawat grupo sa mga panauhin ay may kanya-kanyang paksa upang ilabas ang kani-kanilang saloobin, makipag-tagisan ng kuro-kuro, at humanap ng papuri. Ilan sa mga napag-usapan ay ang mga Indio o ang mga Pilipino; ang pagkakaalis ni Padre Damaso sa Parokya ng San Diego kahit na matagal itong nagsisilbi doon; ang monopolyo ng tabako, mga pulbura at armas at marami pang iba.</p>
                                        <br>
                                        <p> &nbsp;&nbsp;
                                            Samantala, gumawa naman ng paraan si Padre Sibyla upang maiba ang usapan at dito ay pinasok niya pagkakatanggal ni Padre Damaso bilang kura paroko sa loob ng dalawampung taon.</p>
                                        <br> 
                                        <p> &nbsp;&nbsp;
                                            Ipinaliwanag din ng Tinyente na kaya nilipat si Padre Damaso ay dahil sa pinahukay nito ang bangkay ng isang marangal na lalaki na pinagbintangang erehe dahil lamang sa hindi pangungumpisal.</p>
                                        <br> 
                                        <p> &nbsp;&nbsp;
                                            Dahil dito ay nagalit si Padre Damaso lalo na nang maalala niya ang tungkol sa mga nawalang mahahalagang kasulatan. Namagitang muli si Padre Sibyla upang pakalmahin si Padre Damaso. Kalauna’y lumawak muli ang talakayan sa pagtitipon.</p>
                                        
