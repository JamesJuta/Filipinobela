                                    <br><center><h2>ANG MGA SABI AT KURO-KURO</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Ang buong bayan ng San Diego ay sakmal pa rin ng takot kinabukasan. Walang makitang taong naglalakad sa gitna ng daan. Ang buong paligid ay tahimik. Maya-maya ay isang bata ang naglakas ng loob na nagbukas ng kanilang bintana. Gumaya ang ibang mga kapitbahay at sila ay nagkabalitaan. Kalagim-lagim daw ang nagdaang gabi tulad noong mandambong si Balat. Sa kanilang pag-uusap ay lumilitaw na si Kapitan Pablo raw ang sumalakay. Anang iba, ang mga kuwadrilyero raw ang sumalakay kaya dinakip si Ibarra. Dagdag pa nila, ang mga lalaki ay nagpunta sa kuwartel at sa may tribunal. Lumabas pa sa usapan na tinangka diumano ni Ibarra na itanan si Maria Clara upang hindi matuloy ang pakikipag-isang dibdib niya kay Linares at kaya lang sinansala ni Kapitan Tiyago ang kanilang pagtatanan sa tulong ng mga sibil.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Samantala, isang lalaking kagagaling lamang sa tribunal ang nakausap ni Hermana Pute. Sinabi nitong nagtapat na si Bruno at pinatunayan ang balita tungkol sa magkasintahang Ibarra at Maria. Pati daw simbahan ay nais paghigantihan ni Ibarra. Mabuti na nga lamang daw at nasa bahay ni Kapitan Tiyago si Padre Salvi. Ang mga sibil din daw ang sumunog sa bahay ng binata. Isang babae naman ang nagsabi na nakita niyang nakabitin sa ilalim ng puno ng santol si Lucas.</p>
