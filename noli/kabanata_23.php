                                    <p> &nbsp;&nbsp;
                                        Masiglang gumayak ang mga kababaihan at kabinataan madaling araw pa lamang ng araw na iyon. Naglalakad ng magkakasama ang mga kababaihan samantalang hiwalay naman sa kanila ang mga kalalakihan. Ang mga kawaksi at matatandang babae ay kasama rin sa kanilang piknik. Dalawang bangka ang sasakyan nila kung saan ang matatalik na magkakaibigan na sina Maria Clara, Iday, Victorina, Neneng at Sinang ay magkakasama sa isang Bangka. Di mapigil ang tawanan at kwentuhan ng mga dalaga kung kaya si Tiya Isabel ay sinaway sila. Nagkaroon naman ng butas ang bangkang sinasakyan ng mga kalalakihan kaya sila ay napalipat sa bangka ng mga dalaga. Tumahimik tuloy ang mga dalaga sapagkat sila ay inatake ng hiya. Patuloy lamang sa pagsagwan si Elias at upang hindi mainip ang mga kasama ay umawit ng kundiman si Maria Clara.Masiglang gumayak ang mga kababaihan at kabinataan madaling araw pa lamang ng araw na iyon.  Nang malapit ng maluto ang agahan ay gumayak na ang mga kalalakihan upang mangisda. Ngunit walang nahuli ang mga ito kahit isa dahil sa biglang pagsulpot ng buwaya sa ilog. Ang mga kababaihan ay nabahala sa paglitaw ng buwaya lalo na ng ito’y nilundag ni Elias. Higit na malakas ang buwaya kaysa kay Elias kaya tinulungan siya ni Ibarra na taluhin ito. Natalo ng dalawa ang buwaya kaya nagpatuloy na sila sa pangigisda hanggang sa sila ay makahuli ng sariwang isda. Sa isang puno malapit sa batisan ay masayang nananghalian ang magkakaibigan.</p>
