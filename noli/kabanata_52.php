                                    <br><center><h2>ANG BARAHA NG PATAY AT ANG MG ANINO</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Sa ilalim ng pinto ng libingan ay may tatlong anino na paanas na nag-uusap. Naitanong ng isang anino sa kausap kung nakaharap na daw nito si Elias. Ang sagot nito ay hindi ngunit siguradong kasama siya sapagkat nailigtas na minsan ni Ibarra ang buhay nito. Sumagot ang unang anino na ito nga ay pumayag na sumama sapagkat ipapadala ni Ibarra sa Maynila ang kanyang asawa upang ipagamot. Siya din ang sasalakay sa kumbento upang makaganti sa kura. Binigyang diin naman ng ikatlong anino na kasama ng lima, lulusob sila sa kwartel upang ipakilala sa mga gwardya sibil na ang kanilang ama ay may mga anak na lalaki. Isa pa, sinabi ng alila ni Ibarra na sila ay magigng dalawampung katao na. Saglit na huminto sa pag-uusap ng mga anino nang mabanaagan nilang may dumarating na isang anino na namamaybay sa bakod.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Pagdating sa lugar ng tatlo ay nagkakilala sila. Ipinaliwanag ng bagong dating na anino na sinusubaybayan siya kaya’t naghiwa-hiwalay na sila at sinabihan ang mga dinatnan na kinabukasan na ng gabi nila tatanggapin ang mga sandata. Kasabay nito ang sigaw na “Mabuhay Don Crisostomo!” Nawala sa likod ng pader ang tatlong samantalang ang bagong dating ay naghintay sa sulok ng pintuan. Maya-maya pa’y dumating ang ikalawang anino at nagmasid ito sa kanyang paligid. Umaambon noon kaya sumilong siya sa pintuan. Doon ay nagkita sila ng unang sumilong. Napagkasunduan nilang magsugal at kung sino man ang manalo sa kanila ay maiiwan upang makipagsugal naman sa mga patay. Pumasok sila sa loob ng libingan at sa ibabaw ng puntod ay umupong magkaharap ang dalawa para magsugal. Ang isa sa kanila na mas mataas ay si Elias samantalang ang may pilat sa mukha ay si Lucas. Sa kanilang pagsusugal ay natalo si Elias kaya umalis itong hindi kumikibo. Pagdaka’y nilamon siya ng kadiliman.</p>
