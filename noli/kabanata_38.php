                                    <br><center><h2>ANG PRUSISYON</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Ang sunod-sunod na pagtunog ng kampana at mga paputok ay hudyat ng pagsisimula ng prusisyon. May dala-dalang kandila at parol ang lahat ng nakiisa dito. Para sa mga santong sina San Juan Bautista, San Francisco, Santa Maria Magdalena, San Diego De Alcala at Mahal na Birhen ang prusisyon. Hinihila ng anim na Hermano Tercero ang karo ni San Diego.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Magkakasamang naglalakad ang Kapitan Heneral, mga Kagawad, Kapitan Tiyago, Alkalde, Alperes at Ibarra na napilitan lamang sumama dahil sa pag-imbita sa kanya ng Heneral. Inihinto ang mga karo at andas ng mga santo sa tapat ng bahay ni Kapitan Tiyago kung saan may isang kubol sa pagdadarausan ng pagbigkas ng tulang papuri o loa sa pintakasi ng bayan.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang pila ng prusisyon ay pinangungunahan ng tatlong sakristan na sinundan ng guro, mag-aaral at mga batang may dala-dalang parol na papel. Isang batang lalaki na may pakpak, nakabotang pangabayo, nakabanda at may bigkis ang lumabas upang simulan ang pagpupuri sa wikang Latin, Kastila at Tagalog. Kasunod nito’y ang pag-awit ni Maria Clara ng Ave Maria kung saan ang lahat ay nabighani sa ganda ng kanyang tinig. Dama naman ni Ibarra ang pighating nararamdaman ng kanyang kasintahan sa boses nito. Sandaling napukaw ang atensyon ng binata ng ito ay kausapin ng Kapitan-Heneral tungkol sa imbitasyong makasalo siya sa pagkain kung saan kanilang pag-usapan ang pagkawala ng dalawang sakristan na sina Crispin at Basilio.</p>
