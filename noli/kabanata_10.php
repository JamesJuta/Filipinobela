                                        <br><center><h2>ANG SAN DIEGO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang San Diego ay matatagpuan sa baybayin ng lawa at may malalawak na kabukiran. Isa itong maalamat na bayan sa Pilipinas. Pagsasaka ang ikinabubuhay ng mga tao sa bayang ito na kulang sa edukasyon at kaalaman sa pagnenegosyo. Dahil dito’y napaglalamangan sila ng mga dayuhang Tsino. Kapansin-pansin dito mula sa tuktok ng simbahan ang isang gubat na nasa kalagitnaan ng kabukiran. Wala itong pinagkaiba sa ibang mga bayan sa Pilipinas na pinamumunuan ng simbahan kung saan ang pamahalaan ay sunud-sunuran lamang dito. Bago mailipat si Padre Damaso sa ibang bayan dahil sa ginawa nito kay Don Rafael Ibarra ay siya ang tumatayong kura paroko sa simbahan doon.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kabilang ang mga Kastila at ilang mga mayayamang Pilipino sa mga kinikilala na may mataas na tungkulin sa bayang iyon. May isa umanong matandang Kastila ang dumating sa bayan ng San Diego ayon sa alamat. Ito raw ay matatas magtagalog at malalalim ang mga mata. Ito diumano ay bumili ng gubat sa pook na iyon kung saan ang kanyang ipinambayad ay ang kanyang mga ari-arian tulad ng damit, alahas at salapi. Kinalaunan ay natagpuan ang matanda na nakabitin sa puno ng isang balete. Ito ay naging sanhi ng pagkatakot ng mga tao doon kaya’t sinunog ng ilan ang mga damit ng matanda at itinapon naman ang kanyang mga alahas sa ilog.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Di nagtagal ay dumating ang anak ng matanda na nagngangalang Saturnino. Sinikap niyang sinupin ang naiwang ari-arian ng ama. Siya ay nakapag-asawa ng isang taga-Maynila at nanirahan sila sa San Diego. Nagkaroon sila ng isang supling at ito ay si Don Rafael na siya namang ama ni Ibarra. Kinagiliwan ng mga magsasaka si Don Rafael at dahil sa pagsusumikap nito ay naging bayan mula sa pagiging nayon ang San Diego. Ang pamumunong ito ni Don Rafael ang sinasabing naging ugat ng inggit at galit sa ilan niyang mga kaibigan kalaunan.</p>
