                                    <br><center><h2>ANG PANANGHALIAN</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Noong araw na iyon ay darating ang Heneral at tutuloy sa bahay ni Kapitan Tiyago. Magkakaharap na nananghalian ang mga taga San Diego. Si Ibarra at ang alkalde mayor ay nasa magkabilang dulo ng hapag. Si Maria ay nasa gawing kanan ni Ibarra samantalang sa kaliwa naman niya ang eskribano. Nasa hapag din sina Kapitan Tiyago, iba pang mga kapitan sa bayan ng San Diego, mga pari, mga kawani ng pamahalaan at mga kaibigan nina Maria Clara at Ibarra.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Ang karamihan naman ay nagtaka dahil wala pa si Padre Damaso. Nag-uusap ang mga nasa hapag habang kumakain. Napag-usapan anila ang hindi pagdating ni Padre Damaso, ang kamangmangan ng mga magsasaka sa mga kubyertos, ang mga kursong nais nilang ipakuha sa kanilang mga anak, at marami pang iba. Maya-maya’y dumating na si Padre Damaso at ang lahat doon ay bumati sa kanya liban kay Ibarra. Habang inihahanda ang serbesa ay nagsimula na ring patutsadahan ng pari si Ibarra. Sisingit sana ang alkalde sa usapan upang maiba ang paksa ngunit ayaw papigil ng mayabang na pari. Tahimik lamang na nakikinig habang nagtitimpi ang binatang si Ibarra. Tila nananadya si Padre Damaso dahil inungkat nito ang pagkamatay ng ama ni Ibarra. Sa pagkakataong ito’y hindi na pinalampas ng binata ang mga sinabi ng pari kaya naman muntik na niya itong saksakin. Mabuti na lamang at pinigilan siya ni Maria kung kaya’t bumalik ang kahinahunan ni Ibarra at umalis na lamang.</p>
