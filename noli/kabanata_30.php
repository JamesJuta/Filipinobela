                                    <br><center><h2>SA SIMBAHAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Ang simbahan ay dinumog ng mga taong nagsisiksikan sa kabila ng init at iyakan ng mga bata. Ang misa ay bayad para sa kabanalan ng lahat sa halagang dalawang daan at limampung piso. Pinaniniwalaan noon na mas mabuti ng magbayad ng mahal sa misa kaysa sa komedya sapagkat ang misa ay makapagdadala ng kaluluwa sa langit samantalang impyerno naman sa komedya. Samantala, hindi sinimulan ang misa hangga’t hindi dumarating ang alkalde mayor na sinadya namang magpahuli upang higit na mapansin ng lahat. Nakasuot siya ng limang medalya na sagisag ng kanyang tungkulin.
                                    <br><br>
                                    <p> &nbsp;&nbsp;
                                       Nang dumating ang alkade mayor ay nagsimula ang pagmimisa ni Padre Damaso. Sinamantala ng walang pakundangang pari ang paglibak sa nagmisa kahapon na si Padre Manuel Martin. Siya daw ay higit na mahusay magmisa kaysa Padre Manuel Martin. Hangga’t di siya natatapos magyabang ay hindi ito nagsimulang mag-sermon.</p>
