                                    <br><center><h2>ANG MGA KAPANGYARIHAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang mga tao na kinikilalang makapangyarihan o casique sa bayan ng San Diego ay mabibilang lamang. Ito diumano ay katulad ng Roma at Italya sa mahigpit na agawan sa kapangyarihan sa pamumuno sa bayan. Hindi naman kabilang dito sina Don Rafael, Kapitan Tiyago, at ilang namumuno sa pamahalaan. Kahit na si Don Rafael ang pinakamayaman, iginagalang ng lahat, at pinagkakautangan ng marami ay hindi pa rin siya ang nagmamay-ari ng kapangyarihan sa bayang iyon.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Wala namang posisyon sa lipon ng mga makapangyarihan si Kapitan Tiyago na may mga ari-arian din, kabilang sa mataas na antas ng lipunan, sinasalubong ng banda ng musiko, at pinagsisilbihan ng masasarap na pagkain. Ang posisyon diumano sa pamahalaan tulad ng gobernadorcillo o kapitan sa bayan ay mabibili sa halagang P5,000 at madalas pa na kagalitan ng alkalde mayor. Kung tatanungin kung sino ba talaga ang makapangyarihan sa sa bayan ng San Diego, ito ay walang iba kundi ang kura paroko sa simbahan at ang Alperes na siyang puno ng mga gwardya sibil.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Si Padre Bernardo Salvi ang kura paroko at batang pransiskano na mukhang masasakitin ang siyang pumalit kay Padre Damaso. Higit siyang may kabaitan kumpara kay Padre Damaso. Lasinggero, mapambugbog sa asawa at malupit sa kanyang mga tauhan ang Alperes. Siya ay nakapag-asawa ng isang Pilipina at ito ay si Donya Consolacion na mahilig magkolorete sa mukha. Natural lamang na may palihim na hidwaang nagaganap dahil sa agawan ng kapangyarihan ng dalawang Kastila. Ngunit kapag nasa pampublikong lugar ay ipinapakita nila ang kanilang pakunwaring pagkakasunduan.</p>
