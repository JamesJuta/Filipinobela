                                    <br><center><h2>ANG PULONG SA TRIBUNAL</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Noong araw na iyon ay panauhin sina Ibarra at ang guro sa pulong. Ang tanging bulwagan na nagsisilbing lugar para sa pagpupulong at pag-uusap ng mga makapangyarihan at mayayaman sa bayan ay ang tribunal. Nahahati sa dalawang kinatawan o lapian ang mga nasa pulong. Ang una ay ang mga konserbador o pangkat ng mga matatanda na pinamumunuan ng Kabesa samantalang ang pangalawa ay ang liberal na binubuo naman ng mga kabataan sa pamumuno ni Don Filipo. Ang pagpupulong ay tungkol sa gaganaping kapistahan labing-isang araw mula sa araw na iyon, mga programa at aktibidad na gagawin para sa pista, at ang pagtatayo ng paaralan para sa bayan. Sa pulong ay sinamantala ng mga mayayaman ang makapagtalumpati kahit na walang katuturan ang kanilang ibang pinagsasabi. Kagaya ni Kapitan Basilyo na nakalaban ni Don Rafael. Si Don Filipo naman ay nagmungkahi na sa bawat gawain ay dapat may talaan ng mga gastos. Iminungkahi din niyang magpagawa ng isang malaking tanghalan sa plasa at magtanghal ng palatuntunan tulad ng komedya sa loob ng isang linggo. Nabanggit din ni Don Filipo ang pagkakaroon ng paputok upang maging lalong masaya ang pista, bagay na ‘di naman sinang-ayunan ng lahat.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang Kabesa naman ay nagbigay ng panukala na dapat ay tipirin ang pagdiriwang. Wala rin dapat na paputok at ang mga gaganap sa programa ay dapat mga taga-San Diego. Aniya, ang sentro ng pagtatanghal ay dapat mga sariling ugaling Pilipino. Ang mga panukalang inihandog ng magkabilang pangkat ay walang bisa dahil nakapagdesisyon na ang kura tungkol sa gaganapin sa pista. Ang mga gagawin ay anim na prusisyon, tatlong sermon, tatlong misa mayor at isang komedya.</p>
