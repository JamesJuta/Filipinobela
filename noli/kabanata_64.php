                                    <br><center><h2>KATAPUSAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nanirahan na si Padre Damaso sa Maynila mula ng pumasok sa kumbento si Maria Clara. Kalaunan ay inilipat din siya ng Padre Provincial sa isang malayong probinsya. Nagunit kinabukan, natagpuan siyang patay sa kanyang kwarto. Ayon sa pagsusuri ng doktor ay bangungot ang sanhi ng kamatayan ni Padre Damaso. Habang hinihintay ang pagiging obispo, si Padre Salvi naman ay pansamantalang nanungkulan sa kumbento ng Sta. Clara na pinasukan ni Maria Clara. Siya’y umalis na rin sa San Diego at nanirahan na sa Maynila. Ilang lingo bago maging ganap na mongha si Maria, ang ama-amahang si Kapitan Tiyago ay dumanas ng sapin-saping paghihirap ng damdamin, nangayayat nang husto, naging mapag-isip at nawalan ng tiwala kahit kanino.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Pagkagaling niya sa kumbento ay sinabihan niya si Tiya Isabel na umuwi na sa Malabon o kaya naman ay sa San Diego dahil gusto na niyang mabuhay mag-isa. Nilimot na ng Kapitan ang lahat ng santo at santa at inatupag na lamang ang paglalaro ng liyempo, sabong at paghitit ng marijuana. Nang lumaon ay napabayaan na nito ang sarili hanggang sa nalimot na din ang Kapitan na dating tanyag at iginagalang ng mga tao.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa kabilang dako, si Donya Victorina naman ay nagdagdag ng mga kulot sa ulo upang mapagbuti ang pagbabalatkayo niyang siya’y isang taga-Andalucia. Nangungutsero na siya ngayon. Di na niya pinakikilos ang asawang si Don Tiburcio. Nakasalamin na ito, wala na ring ngipin at hindi na natatawag na “doktor” para manggamot.</p>
