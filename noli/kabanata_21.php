                                    <br><center><h2>MGA PAGDURUSA NI SISA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kumaripas ng takbo si Sisa pauwi sa kanilang bahay matapos mapakinggan ang mga sinabi ng kusinero. Hindi mawaglit sa isipan ni Sisa ang maaring pagdampot ng mga gwardiya sibil sa kanyang mga anak dahil sa bintang na pagnanakaw ng salapi. Nakita ni Sisa na papalayo na ang mga gwardiya sibil sa kanilang bahay at hindi nito kasama ang kanyang mga anak, bagay na nakapagpagaan sa loob ng ina. Ngunit ng magpang-abot sila ng gwardiya sibil sa daan ay pilit siyang pinaamin na ilabas ang dalawang onsang ninakaw ng mga anak. Nagmakaawa si Sisa ngunit tila bingi ang mga ito sa pakiusap niya. Sa halip ay kinaladkad siya ng mga gwardiya sibil papuntang kwartel. Habang kinakaladkad ay hiyang-hiya si Sisa sa harap ng taong-bayan na sakto namang katatapos lang ng misa at ang mga ito ay kasalukuyang lumalabas ng simbahan. Napatigil ang lahat sa eksenang nagaganap samantalang si Sisa ay walang magawa kundi umiyak at panghinaan ng loob. Inihagis siya ng mga ito pagdating sa kwartel at nagsumiksik na lamang ang naghihinagpis na ina sa isang sulok.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Walang nais makinig sa pagmamakaawa ni Sisa. Tanghali na ng pakawalan siya ng Alperes. Umuwi na si Sisa sa kanilang bahay at hinanap ang dalawang anak. Wala kahit sa bakuran ang anino o kahit tinig ng kanyang mga anak. Pumasok muli sa bahay si Sisa ngunit ang punit at duguang damit lamang ni Basilio ang kanyang natagpuan. Nilamon ng pighati ang kanyang katinuan dahil hindi matanggap ng ina ang kanyang nasilayan. Nagpalaboy-laboy sa kalsada si Sisa habang sinasabi ang pangalan ng kanyang mga anak na sina Basilio at Crispin.</p>
