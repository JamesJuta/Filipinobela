                                    <br><center><h2>ANG BISPERAS NG PISTA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Tuwing ika-sampu ng Nobyembre ay bisperas ng pista sa San Diego. Handa na ang lahat sa bisperas pa lamang kaya nakagayak na ang kani-kanilang mga bahay ng pinakagarbong palamuti, kurtina at iba’t-ibang dekorasyon pati na mga minana at antigong kagamitan. Punong-puno ng iba’t ibang masasarap na putahe ang hapag kainan ng mga mayayaman. Nariyan ang mga kakanin, panghimagas at mga inangkat na mamahaling mga alak mula pa sa Europa. Para sa lahat ang mga pagkain dito upang kahit na mga taga ibang bayan ay maging masaya din sa kapistahan. Panay rin ang pagpapaputok ng mga kwitis, batingaw ng kampana at tugtugan ng mga banda ng musiko. Ang mga matataong lugar kabilang na ang plasa ng San Diego ay pinalamutian ng arkong kawayan. Nilagyan din ng tolda ang harapan ng simbahan para sa prusisyon. May nakalaan ding tanghalan para sa komedya at iba pang palatuntunan. Sina Kapitan Tiyago, Kapitan Joaquin, ang intsik na si Carlos, at iba pang mayayaman sa bayan ng San Diego ay may partisipasyon din sa kasayahan. Nakalaan namang magmisa si Padre Damaso sa umaga.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Iginayak na rin ng mga magsasaka at mahihirap ang kanilang mga pinaka-mainam na ani upang ihandog sa mga may-ari ng kanilang bukirin. Samantala, tinatapos na ang bahay-paaralan na pinapagawa ni Ibarra malapit sa kanyang tahanan sa pamamatnubay ni Nol Juan. Ang lahat ng gastos ay sagot ni Ibarra. Magalang namang tinanggihan ng binata ang alok na tulong ng mga mayayaman at ng pari sa kanya. Katumbas ng mga paaralan sa Europa ang bahay-paaralan na ipinapatayo ni Ibarra kung saan hiwalay ang mga babae sa lalaki. Mayroong lugar para sa pagtatanim ng puno at gulay pati na rin bodega. May silid pang-disiplina din para sa mga batang mag-aaral. Sa ginawang ito ni Ibarra ay maraming humanga sa kanya ngunit sa kabilang banda nama’y marami din ang kanyang naging palihim na mga kaaway.</p>
