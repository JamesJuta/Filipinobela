                                    <br><center><h2>MGA PAGBABAGO</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nakatanggap ng liham mula kay Donya Victorina si Linares na nakapagpabalisa dito. Alam nito na hindi nagbibiro ang Donya. Kailangan niyang hamunin ang Alperes ngunit sino naman kaya ang papayag na maging padrino niya, ang kura kaya o si Kapitan Tiyago? Siya ngayo’y sising-sisi sa paghahambog at pagsisinungaling sa paghahangad lamang na makapagsamantala. Labis kasi siyang nagpatianod sa kapritso ni Donya Victorina.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Dumating si Padre Salvi sa bahay nina Kapitan Tiyago at ito ay nagmano. Masaya nitong ibinalita ang tungkol sa sulat na padala ng arsobispo tungkol sa pag-alis ng ekskomunyon kay Ibarra kasabay ng kanyang pagpuri sa binata na ito’y kalugod-lugod ngunit may kaunting kapusukan. Dagdag pa nito, si Padre Damaso na lamang ang sagabal sa pagpapatawad ka Ibarra ngunit kung si Maria Clara ang kakausap dito ay hindi makatatanggi ang pari. Narinig ni Maria Clara ang pag-uusap na ito at nagtungo siya sa silid kasama si Victoria. Maya-maya’y dumating si Ibarra kasama si Tiya Isabel habang nag-uusap sina Padre Salvi at Kapitan Tiyago. Binati niya ang Kapitan samantalang yumukod naman kay Linares. Buong lugod naman na kumamay si Padre Salvi kay Ibarra at sinabing katatapos pa lamang niyang papurihan ito. Nagpasalamat naman ang binata sa mga narinig.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ilang sandali pa’y lumapit ito kay Sinang upang itanong kung galit sa kanya si Maria Clara. Ayon kay Sinang ay ipinasasabi raw ni Maria na limutin na lamang siya nito ngunit nais ni Ibarra na makausap ng sarilinan ang kasintahan. ‘Di nagtagal ay umalis na rin si Ibarra.</p>
