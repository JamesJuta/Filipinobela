                                    <br><center><h2>ANG TALINGHAGA</h2></center> 
                                    
                                    <br>
                                    <p> &nbsp;&nbsp;
                                       Nang araw na iyon ay dumating si Ibarra sa bahay ni Kapitan Tiyago upang dalawin si Maria Clara. Masaya niyang ibinalita sa Kapitan na tinanggal na ang kanyang pagiging ekskomulgado at ipinabasa ang sulat kay Tiya Isabel. Tuwang-tuwa naman ang Tiya sapagkat magiliw siya kay Ibarra. Pumunta si Ibarra sa balkon ngunit nabigla sa nakita. Magkasama noon sina Maria Clara at si Linares na nasa gawing paanan nito at nag-aayos ng mga bulaklak. Nagulat si Linares samantalang si Maria Clara naman ay namutla. Sinikap mang tumayo ng dalaga ngunit ito ay hindi pa lubos na magaling. Sinabi rin ni Ibarra ang dahilan ng kanyang pagdalaw. Makikita sa mukha ni Maria Clara ang pagkalungkot at dahil dito ay nagpaalam kaagad si Ibarra na sinabing dadalaw muli kinabukasan. May kaguluhan ang isip at damdamin ng umalis na si Ibarra.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Habang naglalakad ay napadaan si Ibarra sa ipinapagawang paaralan. Sinabi niya sa lahat lalo na kay Nol Juan na wala silang dapat ipangamba dahil siya ay tanggap na muli ng simbahan. Ang sabi ni Nol Juan ay hindi naman daw mahalaga para sa kanila ang pagiging ekskomulgado ni Ibarra dahil lahat naman daw sila ay mga ekskomulgado rin.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nakita ni Ibarra si Elias na nagkakarga ng bato sa kariton at nabasa nito sa mukha ni Elias na may nais itong ipaalam sa kanya. Dahil dito’y inutusan ni Ibarra si Nol Juan na ibigay sa kanya ang talaan ng mga obrero. Si Elias naman ay nagmungkahi na mamangka sila ni Ibarra sa lawa para mapag-usapan ang isang napakahalagang bagay. Sumang-ayon naman dito ang binata. Maya-maya pa’y dumating na si Nol Juan dala ang listahan ngunit hindi nakalista doon ang pangalan ni Elias.</p>
