                                    <br><center><h2>SA BAHAY NG MGA MAG-AARAL</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang tinitirahang bahay ni Makaraig ay malaki. Maluwag ito at puro binata ang mga nakatira na pawang nangangasera. Mayaman si Makaraig at kumukuha ng kursong abogasya. Siya ang pinuno ng kilusan para sa Akademya ng wikang Kastila. Inimbitahan ni Makaraig ang mga pangunahing mag-aaral na sina Isagani, Sandoval, Pecson at Pelaez upang pag-usapan ang kanilang pakay. Si Isagani at Sandoval ay naniniwala na maaaprubahan ang pagbukas ng paaralang samantalang si Pecson ay nag-aalinlangan. Nagkaroon sila ng debate sa maaring maging aksiyon sa kanilang paaralan. Si Sandoval ay larawan ng mga kastilang may malasakit at pagpapahalaga sa mga Pilipino.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Isang magandang balita ang ibinahagi ni Makaraig. Si Padre Irene umano ang nagtatanggol sa kanila laban sa mga sumalungat sa kanilang adhikain. Dagdag pa niya, kailangan ng kanilang grupo ang pagpanig ni Don Custodio, isa sa mga kataas-taasang lipon ng paaralan sa kanilang panig. Dalawang tao ang maari umano nilang lapitan upang kumbinsihin si Don Custodio na pumanig sa kanila. Ito raw ay si Ginoong Pasta na isang manananggol at ang mananayaw na si Pepay. Napagkasunduan nila na kay Ginoong Pasta lumapit upang maging marangal ang kapamaraanan.</p>
