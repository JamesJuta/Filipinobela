                                    <br><center><h2>MGA PANGARAP</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang magkasintahang sina Isagani at Paulita ay mag-uusap sa Luneta. Galit si Isagani dahil nakita niyang magkasama sina Paulita at Juanito sa dulaan. Ngunit napag-alaman niya na si Donya Victorina raw ang may ibig kay Pelaez kaya nagkatawanan sila ng kausap.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Nagkapalitan din sila ng mga pangarap sa hinaharap. Nais raw ni Isagani na sa nayon manirahan. Para sa kanya, ang bayang iyon ang tangi niyang kaligayahan bago pa nakilala at nakita si Paulita. Ngunit nabatid niyang parang naging may kulang sa kanya ang bayang iyon at natitiyak niyang ang kulang ay ang kanyang nobya. Ngunit ayaw pumunta doon ni Paulita. Ayaw daw niyang magdaan sa mga bundok na malimatik. Ang nais niyang paglalakbay ay sa pamamagitan ng tren.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Pinaghambing ni Isagani sa isip ang pansin ng mga taong pamahalaan sa nagkaramdam na si Simoun at ang mga sugatang kawal na galing sa digma. Sa mga sugatang kawal ay ni walang pumapansin subalit kay Simoun ay nabahala ang marami. Iba na raw ang mayaman ayon sa binata. Ang bayang nasa isip niya ay ‘di lamang Pilipinas kundi pati ang Espanya. Nang dumating sina Paulita ay nginitian niya ang nobyo. Ngumiti rin si Isagani at tila ba napawi lahat ng kanyang pagkainis. Masaya na sana si Isagani ngunit biglang itinanong ni Donya Victorina kung nasa kanilang nayon ba nagtatago ang asawa niyang si Don Tiburcio. Ipinagkaila ng binata ang kanyang nalalaman. Ngunit may pahabol na tanong ang Donya na ano daw kaya kung pakasal siya kay Juanito. Kinaiinisan man ang kanyang kamag-aral ngunit pinuri pa niya si Juanito sa harap ng Donya. Pinagbigyan ni Donya Victorina na magkausap ang pamangkin at ang nobyo nito. Kung magkakatuluyan nga naman si Paulita at Isagani ay masasarili niya si Juanito.</p>
