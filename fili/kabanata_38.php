                                    <br><center><h2>KASAWIANG-PALAD</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kinuha ng Pamahalaan ang lahat ng pinaghinalaang tulisan para maitago ang kanilang kahinaan. Matapos ang pagsalakay ni Kabesang Tales ay anim o pitong mga magsasaka ang dinakip ng mga sibil saka pinalakad sa init ng araw. Sampu o labindalawang mga gwardia sibil naman na mga Pilipino ang nang-aabuso sa mga bilanggo at pilit na pinapaamin na sila ang mga tulisan. Abot-siko at kabit-kabit ang gapos ng mga ito na inilakad nang tanghaling tapat sa kainitan ng Mayo na walang sombrero at panyapak. Kapag may natumba at mabagal ang paglakad ng mga bilanggo ay binugbog sila at pilit na pinapatayo. Nilalait pa ng ilang sibil ang mga bihag.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                       Ayon kay Mautang, isa sa mga Pilipinong sibil, may karapatan silang abusuhin ang mga bilanggo dahil magkapareho lang ang kanilang nasyonalidad. May ilang mga tulisan na lumusob sa kanilang paglalakbay at sinimulan ang pagpalitan ng putok. Namatay si Mautang at ilang mga gwardiya sibil.May isang tao na nakatayo sa itaas ng bundok na iwinawasiwas ang baril. Inutusan ng kabo si Carolino na barilin iyon. Mukhang kilala ni Carolino kung sino ang taong nasa bundok ngunit hindi niya masyadong maaninagan dahil sa init ng araw. Nang tingnan niya ang mga nakahandusay, nakita niya sa mga iyon ang kanyang lolo Selo. Nakatingin ito kay Carolino habang nakaturo ang daliri sa likod ng talampas. Namutla si Carolino. Hindi niya inaasahang siya ang makakapatay sa kanyang ingkong.</p>
