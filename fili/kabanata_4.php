                                    <br><center><h2>KABESANG TALES</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Si Tandang Selo na umampon noon sa gubat kay Basilio ay matanda na. Ang anak nitong si Kabesang Tales ay isa nang Kabesa de Baranggay. May tatlo itong anak, sina Lucia, Tano, at Juli. Namatay si Lucia at ang kanyang asawa dahil sa malaria. Sina Tano at Juli na lamang ang buhay. Yumaman na sila dahil sa tiyaga. Nakisama si Kabesang Tales sa namumuhunan sa bukid at nang makaipon ng kaunti ay nagbungkal ng lupa sa gubat. Nang maipagtanong na walang may-ari sa naturang lupa ay ginawa niya itong tubuhan. Inisip din niyang pag-aralin na sa kolehiyo si Juli upang mapantay kay Basilio na kasintahan nito.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Nang umunlad ang bukid ay inangkin ito ng mga prayle at pinagbayad ng buwis si Kabesang Tales. Tinaasan ng tinaasan ng mga pari ang buwis at nang ‘di na kinaya ng Kabesa ay nakipag-asunto ito sa mga prayle. Dinala ni Kabesang Tales sa korte upang maayos ang problema. Ngunit natalo ang Kabesa dahil nasa ilalim ng impluwensya ng mga prayle ang gobyerno. Nang tuluyan nang hindi makabayad ng buwis si Kabesang Tales ay ipinaglaban pa rin niya ang lupa sa pamamagitan ng pagbabantay dito.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Sa kanyang pagbabantay ay nagdala siya ng baril na kinumpiska ng prayle. Sumunod ay itak naman ang kanyang dinala ngunit ipinagbawal itong muli ng mga pari. Sa huli ay palakol na lamang ang dinala ng Kabesa sa pagbabantay. Kalaunan ay dinakip ng mga tulisan si Kabesang Tales dahil may perang nakita sa kanya at nakakapagbayad ng abogado para sa kaso niya. Ipinatubos naman siya sa halagang 500. Upang may maipantubos sa ama ay ibinenta ni Juli ang kanyang mga alahas liban sa bigay ng kanyang nobyo na isang laket na pagmamay-ari ni Maria Clara. Nang ‘di sumapat ang perang pantubos ay namasukan siyang katulong sa tahanan ni Hermana Penchang. Bisperas noon ng Pasko kaya kinabukasan ay maninilbihan na siya bilang isang alila. Dahil sa pangyayaring ito ay hindi na nakapag-aral si Juli. Masalimuot naman ang naging panaginip ng dalaga ng gabing iyon.</p>
