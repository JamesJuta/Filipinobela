                                    <br><center><h2>ANG KASAL NI PAULITA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ika-walo na ng gabi. Nasa daan pa si Basilio ng maisip na makituloy sa kaibigang si Isagani. Ngunit hindi pala umuwi ang kaibigan sa buong araw na iyon. Dalawang oras na lang at sasabog na ang ilawan ni Simoun. Marami tiyak ang mamamatay. Sinalat ni Basilio ang kanyang rebolber at naalala ang babala ni Simoun na lumayo siya sa daang Anloague. Ang bahay ni Kapitan Tiyago ay nasa daang iyon. May binanggit na kasayahan si Simoun kung saan sa bahay na iyon idaraos ang piging sa kasal nina Paulita at Juanito.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                       Nakita niya ang pagdating ng sasakyan ng bagong kasal. Naisip niya ang kaibigang si Isagani. Naawa siya rito at naisip na yakaging sumama sa himagsikan. Ngunit naisip din niya na malamang ay ‘di papayag si Isagani dahil hindi pa naman nito naranasan ang mga naranasan ni Basilio. Muli niyang naalala ang kanyang pagkabilanggo, ang kabiguan sa pag-aaral, at ang nangyari kay Juli. Saka muling hinaplos ang puluhan ng rebolber at ninasang dumating na sana ang sandaling hinihintay. Dumating ang sasakyan ni Simoun kasama ang kutserong si Sinong. Sumunod sila sa sasakyan ng bagong kasal. Nagtungo din si Basilio sa Anloage sa bahay ni Kapitan Tiyago. Doon kasi gaganapin ang hapunan at nandoon din ang Kapitan Heneral na ninong sa kasal. Dala naman ni Simoun ang ilawan na kanyang handog. </p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Sa bahay ni Kapitan Tiyago ay makikita sa mga dingding ang mga magagarang palamuting papel, aranya, at mga bulaklak. Ang kurtina sa bahay ay may pulang pelus na nabuburdahan ng ginto at may unang titik ng pangalan ng mag-asawa. Napakagara ng bahay na iyon. Parang hapag ng mga diyoses ang pagdarausan ng hapunan. Para sa mga dakilang panauhin ang mesa at ang mga diyus-diyusan ay sa asotea nakalagay. Pipito ang doon ay nakaupo. Naroon ang pinakamasarap at mahal na alak. Ubos-kaya si Don Timoteo, ang ama ng lalaking ikinasal.</p>
