                                    <br><center><h2>SA IBABAW NG KUBYERTA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Naglalayag sa ilog Pasig ang Bapor Tabo isang umaga ng Disyembre. Patungo ito sa Laguna at lulan sa ibabaw ng kubyerta sina Don Custodio, Ben Zayb, Donya Victorina, Kapitan Heneral, Padre Irene, Padre Salvi, at Simoun.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Mataas ang tingin ng mga tao kay Simoun dahil na rin alam nila, lalo na ng mga tao sa Maynila, na naiimpluwensiyahan nito ang Kapitan Heneral. Dahil sa kabagalan ng bapor habang sila’y naglalakbay ay napag-usapan sa ibabaw ng kubyerta ang pagpapalalim ng ilog Pasig. Sa kanilang usapan ay iminungkahi ni Simoun na gumawa ng tuwid na kanal na mag-uugnay sa lawa ng Laguna at sa look ng Maynila. Nagkasagutan sila ni Don Custodio na isang opisyal na konsehal at nagbigay ng solusyon na kumbinsihin na lang diumano ang mga tao na mag-alaga ng itik. Kinakain daw kasi ng mga itik ang mga suso sa ilog. Sa ganitong paraan, huhukayin daw ng mga tao ang ilog upang may makuhang suso na kanilang ipapakain sa mga alagang itik. Ngunit hindi nagustuhan ni Donya Victorina ang naturang suhestyon dahil nadidiri siya sa balot.
