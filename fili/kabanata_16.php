                                    <br><center><h2>ANG KASAWIAN NG ISANG INTSIK</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang negosyanteng Intsik na si Quiroga ay naghahangad magkaroon ng konsulado ang kanyang bansa kaya siya ay naghandog ng isang hapunan. Dumalo ang mga tanyag na panauhin kabilang na ang mga kilalang mangangalakal, mga pari, mga militar, mga kawani ng pamahalaan pati na din ang kanilang mga suki.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Nang dumating si Simoun ay sinigil niya si Quiroga sa kanyang utang na nagkakahalaga ng siyam na libong piso. Nalulugi daw ang Intsik kaya hindi makakabayad sa mag-aalahas. Inalok siya ni Simoun na babawasan ng dalawang libong piso ang utang kung papayag umano si Quiroga na itago sa kanilang bodega ang mga armas na dumating. Wala daw dapat ipangamba ang Intsik dahil ang mga baril ay unti-unting ililipat sa ibang bahay na pagkatapos ay gagawan ng pagsisiyasat at marami ang mabibilango. Siya ay lalakad sa mga mapipiit upang kumita. Napilitan namang sumang-ayon si Quiroga. Samantala, ang pangkat ni Don Custodio ay nag-uusap tungkol sa komisyong ipapadala sa India para pag-aralan ang paggawa ng sapatos para sa mga sundalo. Ang grupo naman ng mga prayle ay pinag-uusapan ang tungkol sa ulong nagsasalita sa may perya sa Quiapo na pinamamahalaan ni Mr. Leeds.</p>
