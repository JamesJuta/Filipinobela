                                    <br><center><h2>ANG PERYA SA QUIAPO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Pumunta sa peryahan sa Quiapo ang labindalawang galing sa bahay ni Quiroga. Patungo sila sa kubol ni Mr. Leeds. Ikinatuwa ng makamundong pari na si Padre Camorra ang mga nasasalubong nilang magagandang dalaga lalo pa ng makita niya si Paulita Gomez na kasama ng kanyang tiya na si Donya Victorina at ni Isagani na nobyo ng dalaga. Naiinis naman si Isagani sa bawat tumititig kay Paulita.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Dumating sila sa isang tindahan kung saan maraming makikita na estatwang kahoy. Ang isang estatwang mukhang mulato ay kinilala nilang si Simoun dahil sa parang pinaghalong puti at itim ito. Wala roon ang mag-aalahas kaya’t napag-usapan nila ito. Ani Padre Camorra, baka daw natatakot si Simoun na pagbayarin nila sa pagpasok sa peryahan. Wika naman ni Ben Zayb, baka natakot si Simoun na matuklasan nila ang lihim ng kanyang kaibigan na si Mr. Leeds.</p>
