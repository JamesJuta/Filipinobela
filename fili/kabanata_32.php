                                    <br><center><h2>ANG BUNGA NG MGA PASKIL</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Dahil sa mga nangyari sa mga mag-aaral ay hindi na nagpaaral ng mga magulang ang mga anak. Sa unibersidad naman ay maraming bumagsak at bihira ang pumasa sa mga eksamen at sa kanilang kurso. Hindi man nakapasa ay ikinatuwa pa rin iyon ni Tadeo at sinunog pa ang kanyang mga aklat. Hindi rin nakapasa sina Makaraig, Pecson, at Juanito Pelaez. Si Pelaez ay napatali sa negosyo ng ama. Nagmadali namang pumunta sa Europa si Makaraig. Sina Isagani at Sandoval lamang ang nakapasa. Si Basilio naman’y hindi pa nakakakuha ng pagsusulit dahil nasa bilangguan pa. Sa bilangguan na rin niya nalaman ang pagkawala ni Tandang Selo at pagkamatay ni Juli. Sa tulong ng kutserong si Sinong na tanging dumadalaw sa kanya ay nalaman niyang lahat ang mga pangyayari.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                       Samantala, ayon kay Ben Zayb ay mabuti na ang lagay ni Simoun. Mamamahala umano ito ng isang piging sa bahay ni Kapitan Tiyago na nakuha ni Don Timoteo Pelaez sa murang halaga. Mula noo’y madalas si Simoun sa tindahan ng mga Pelaez na usap-usapan ng iba na pinakisamahan na niya.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                       Ilang linggo lang ay nabalitang ikakasal na si Juanito kay Paulita. Bagay umano ang dalawa dahil pareho silang walang-isip at makasarili. Buwan na ng Abril at limot na ang mga pangamba. Hinintay-hintay ng buong Maynila ang piging sa kasal nina Juanito at Paulita. Isang bagay lang ang pinagkakaabalahan at iniisip ng ilan, ang makipagkaibigan kay Simoun at Don Timoteo Pelaez para maimbita sa kasal. Ninong daw ang Kapitan Heneral sa nalalapit na kasal at si Simoun ang mag-aayos at maghahanda nito.</p>
