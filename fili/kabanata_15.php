                                    <br><center><h2>SI SENYOR PASTA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sinadya ni Isagani ang opisina ng manananggol na si Senyor Pasta, isa sa mga may pinakamatalas na pag-iisip sa Maynila na sinasangguni ng mga pari kung ang mga ito’y nasa gipit na kalagayan. Pinakiusapan niya ang Senyor na kung maari ay mamagitan upang kanilang mapasang-ayon kung sakaling sumangguni na sila kay Don Custodio.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Ikinuwento ni Isagani kay Senyor Pasta ang tungkol sa balaking kilusan. Nakinig ng mabuti ang Senyor na tila walang alam at kunwari’y wala siyang pakialam sa gawain ng mga kabataan. Pinakikiramdaman naman ni Isagani ang naging bisa ng kanyang mga salita sa abogado. Nais sana ni Isagani na maaprubahan ng manananggol ang nais nilang akademya ng Wikang Kastila ngunit nabigo siya dahil nagpasiya ng abogado na huwag makialam dahil maselan ang usapan at mas mabuting hayaan daw na ang gobyerno ang kumilos.</p>
