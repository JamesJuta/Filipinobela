                                    <br><center><h2>SA ILALIM NG KUBYERTANG NOCHE BUENA NG ISANG KUTSERO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Gabi na noon at kasabay ng pag-uwi ni Basilio sa bayan ng San Diego ay ang nagaganap na prusisyong pang-noche Buena. Nalimutan ng kutserong si Sinong ang kanyang sedula kaya’t ito’y kinakailangang bugbugin muna ng mga guwardiya sibil.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Pagkatapos ay napag-usapan nila ang imahen ni Metusalem, ang pinaka-matandang taong nabuhay sa mundo. Kasunod nito’y idinaan ang mga imahen ng tatlong Haring Mago. Dahil dito’y naalala ni Sinong si Haring Melchor na kayumanggi ang balat. Itinanong ng kutsero kay Basilio kung nakawala na ang kanang paa ni Bernardo Carpio na naipit sa kabundukan ng San Mateo. Pinaniniwalaan kasi na hari ng mga Pilipino si Bernardo Carpio na makakapagpalaya diumano sa bayan. Pagkaraan ay nadaan rin nila ang ilang mga imahen, ang huli sa mga ito ay ang imahen ng birheng Maria na tila malungkot.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Nahuli muli si Sinong dahil hindi niya namalayang namatay na ang ilaw ng kanyang kalesa. Sa pagkakataong ito ay dinala na sa presinto ang kutsero kaya bumaba na lamang si Basilio at naglakad. Sa paglalakad ni Basilio ay napansin niya na wala masyadong parol ang mga bahay at tahimik ang karamihan maliban sa bahay ni Kapitan Basilio na puno ng kaligayahan. Nakita niya dito ang alperes, ang kura, at si Simoun. Nakarating din si Basilio sa bahay ni Kapitan Tiyago kung saan siya’y binati ng mga katiwala. Ikinuwento ng mga ito kay Basilio ang tungkol sa mga pangyayari sa bukid kung saan maraming namatay na hayop at katiwala at sa balitang nadakip si Kabesang Tales, ang ama ng kanyang nobyang si Juli.</p>
