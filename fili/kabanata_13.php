                                    <br><center><h2>ANG KLASE SA PISIKA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Isang mahaba at rektangular na bulwagan ang silid ng klase sa Pisika, maluluwang ang bintana nito at narerehasan ng bakal. Sa magkabilang tabi ng silid ay may tatlong baitang na batong natatapakan ng kahoy. Doon nakaupo ang mga estudyanteng nakaayos ayon sa pagkakasunud-sunod ng letra ng kanilang mga apelyido. Walang palamuti ang dingding ng silid. May mga kasangkapan nga sa pisika ngunit ito ay nakasusi sa isang aparador na may salamin at kung gamitin man ay ipinakikita lamang sa klase mula sa malayo tulad ng Santisimo ng Pari.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Samantala, ang batang Dominikong pari na si Padre Millon ang guro sa klase ng Pisika. Siya’y napabantog sa pilosopiya sa Kolehiyo ng San Juan de Letran. Katapat ng pintuan, sa ilalim ng larawan ni Santo Tomas de Aquino ay doon nakaupo ang propesor. Tinawag ng propesor ang antuking estudyante na may buhok na parang iskoba. Parang ponograpo itong tumugon ng isang isinaulong leksiyon na ukol sa salamin, bahagi nito, kauriang bubog o kalaing. Pinatigil ng guro ang estudyante at sunod na tinawag si Pelaez. Sumenyas ito kay Placido na tila ba sinasabing, “Makinig ka’t diktahan mo ako.” Sa katatapak sa paa ni Placido ay napasigaw ito sa sakit. Sa kanya tuloy nabaling ang galit ng propesor. Siya ang tinatanong ng propesor matapos na siya’y murahin at sabihang “espiritu sastre”. Nasabihan din siyang “pakialamero” at dahil dito ay pinaupo na si Juanito at siya na ang tinanong.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Nagkandautal si Placido sa pagsagot sa mga tanong ng propesor. Tinawag pa siya nitong Placidong Tagadikta. Wala siyang nabigkas sa mga leksyon kaya naglagay ang propesor ng guhit kay Placido. Tumutol dito si Placido at nagpaliwanag. Inihagis ni Placido ang hawak niyang aklat, tumindig, hinarap ang propesor at walang-galang na umalis sa klase. Natigilan ang klase. ‘Di nila lahat akalain na magagawa iyon ni Placido. Nagsermon at nagmura si Padre Millon hanggang sa tumugtog ang kampanilya, hudyat na tapos na ang klase.</p>
