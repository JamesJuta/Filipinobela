                                    <br><center><h2>ANG MGA KADAYAAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa peryahan ay sinalubong ni Mr. Leeds ang mga panauhin. Isa siyang Amerikano na magaling magsalita ng Kastila dahil sa matagal na namalagi sa Timog-Amerika. Bago naman magsimula ang palabas ay nagsiyasat muna si Ben Zayb at hinanap ang salamin na maaring ginagamit ni Mr. Leeds sa pandaraya. Ito naman ay may pahintulot ng Amerikano ngunit walang natagpuang salamin si Ben Zayb.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Pagkaraan ay pumasok si Mr. Leeds sa isang pinto at paglabas ay may dala na siyang kahong kahoy. Aniya, natagpuan daw niya iyon sa libingang nasa piramid ni Khufu, isang Paraon ng Ehipto. Ang kahon ay may lamang abo at kapirasong papiro na kinasusulatan ng dalawang salita. Kapag binigkas ang unang salita ay nabubuhay ang abo at nakakausap ang isang ulo. Kapag ang pangalawang salita naman ang binanggit ay bumabalik ito sa dating kinalalagyan. Maya-maya, bumigkas ng isang salita ang Amerikano. Lumabas ang isang ulo at sinabi nitong siya si Imuthis. Siya daw ay umuwi sa kanyang bayan pagkatapos ng pag-aaral at mahabang paglalakbay.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Nang dumaan daw siya sa Babilonia ay nalaman niya ang isang lihim na hindi ang tunay na Sumerdis ang namamahala doon kundi si Gautama, isang magnanakaw ng kapangyarihan at namamahala sa tulong ng pandaraya. Dahil sa takot na isumbong siya kay Cambises ay binalak niyang patayin si Imuthis sa tulong ng mga saserdoteng taga-Ehipto. Samantala, si Imuthis ay umibig sa isang anak ng pari at naging kaagaw niya rito ang pari sa Abidos. Nagpanukala ang pari ng kaguluhan at siya ang sinangkalan. Isinakdal si Imuthis, napiit, tumakas, at saka napatay. Ayon sa ulo, siya ay nabuhay muli upang ihayag ang gayong kataksilan. Titig na titig ang ulo kay Padre Salvi habang nagsasalita ito kaya dahil sa takot ay hinimatay ang pari. Kinabukasan ay naglabas ng utos ang Gobernador na nagbabawal sa palabas ni Mr. Leeds ngunit huli na dahil wala na ang Amerikano at nagpunta na sa Hongkong dala ang kanyang lihim.</p>
