                                    <br><center><h2>ANG MATAAS NA KAWANI</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Hindi nabalita sa mga pahayagan ang pagkamatay ni Juli. Ang tanging nabalita sa tulong ni Ben Zayb ay ang kabutihan umano ng Heneral. Nakalaya na sina Makaraig at Isagani at tanging si Basilio na lamang ang hindi. Ipinagtanggol ng Mataas na Kawani si Basilio. Mabuting bata raw ito at malapit nang matapos sa panggagamot. Lalo lamang napahamak si Basilio dahil bawat sabihin ng kawani ay panay ang tutol ng Heneral. Dapat daw ay magkaroon ng halimbawang ‘di dapat tularan ang mga mahilig sa pagbabago. Pinagbintangan si Basilio ng Heneral na gumamit ng bawal na aklat sa medisina.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                       Ayon sa Kawani ay dapat matakot ang Heneral sa bayan. Natawa lamang ang Heneral dahil wala umano siyang pakialam sa bayan sapagkat ang naglagay sa kanya sa pwesto ay ang bayang Espanya at hindi ang bansang Pilipinas. Kapag itinanggi umano sa isang bayan ang liwanag, tahanan, katarungan at kalayaan ay ituturing ng bayan na magnanakaw ang nagtanggi ng mga ito. Pagkaalis ng Kawani makaraan ang dalawang oras ay nagbitiw ito sa tungkulin at nagsabing siya’y uuwi na sa Espanya lulan ng kasunod na bapor.</p>
