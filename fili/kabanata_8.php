                                    <br><center><h2>MALIGAYANG PASKO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Kinaumagahan ay agad na tinungo ni Juli ng kinalalagyan ng Mahal na Birhen upang alamin kung may dalawandaan at limampung piso na sa ilalim nito. Sa kasamaang palad ay hindi naghimala ang Mahal na Birhen kaya inaliw na lamang niya ang sarili at inayos ang damit na dadalhin pagtungo sa tahanan ni Hermana Penchang. Dahil Pasko noon kaya ang mga bata ay binibihisan nang magara ang kanilang mga anak upang magsimba at pagkatapos ay dadalhin sa kanilang mga ninong at ninang upang mamasko.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Nagpunta sa bahay ni Tandang Selo ang kanilang mga kamag-anak upang mamasko ngunit nang babatiin na niya ang mga ito ay laking gulat niya na walang salitang lumabas sa kaniyang bibig. Pinisil niya ang kanyang lalamunan, pinihit ang leeg at sinubukang tumawa ngunit kumibut-kibot lamang ang kanyang mga labi. Ang ingkong ni Juli ay napipi.</p>
