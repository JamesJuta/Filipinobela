                                    <br><center><h2>KAYAMANAN AT KARALITAAN</h2></center> 
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nakipanuluyan si Simoun sa bahay ni Kabesang Tales na nasa pagitan ng bayan ng San Diego at ng Tiyani. Naghihirap na noon si Kabesang Tales samantalang dala nang lahat ni Simoun ang pagkain at ibang kailangan at dalawang kaban ng mga alahas. Ipinagmalaki rin niya sa Kabesa ang dala niyang rebolber. Maya-maya pa’y nagdatingan na ang mga mamimili ng alahas. Doon ay dumating sina Kapitan Basilio, ang anak nitong si Sinang at asawa nito, at si Hermana Penchang na mamimili ng isang singsing na brilyante para sa birhen ng Antipolo. Binuksan ni Simoun ang dalawang maleta ng alahas na may iba’t ibang uri, ayos, at kasaysayan. Inilabas ni Simoun ang mga bago niyang hiyas at doon namili si Sinang. Sinabi rin ni Simoun na namimili rin siya ng alahas. Tinanong niya si Kabesang Tales kung may ipagbibigli itong alahas. Iminungkahi ni Sinang ang kwintas na agad tinawaran ni Simoun ng limandaang piso nang makilala niyang kay Maria Clara nga iyon na kanyang kasintahan na nagmongha. Ngunit ayon kay Hermana Penchang ay hindi daw dapat iyon ipagbili ni Kabesang Tales dahil minabitu pa ni Huli ang paalila kaysa ipagbili iyon. Isangguni raw muna ni Kabesang Tales sa anak ang bagay na iyon na sinang-ayunan naman ni Simoun.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Nang lumabas ng bahay si Kabesang Tales ay natanawan niya ang prayle at ang bagong may-ari ng kanyang lupa. Kinabukasan, wala na si Kabesang Tales sa kanyang tirahan. Nawawala rin ang rebolber ni Simoun at ang naroroon lamang ay isang sulat at kuwintas ni Maria Clara. Humingi ng paumanhin si Kabesang Tales sa pagkuha niya ng baril ng mag-aalahas dahil kinailangan niyang sumapi sa mga tulisan. Pinagbilinan din niya si Simoun na mag-ingat sa mga tulisan dahil mapahamak ang mga ito.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Si Tandang Selo ay hinuli ng mga gwardiya sibil. Natuwa naman si Simoun dahil sa wakas ay natagpuan na niya ang taong kanyang kailangan, isang pangahas ngunit marunong tumupad sa mga pangako. Samatala, tatlo ang pinatay ni Kabesang Tales ng gabing iyon: ang prayle, ang lalaking gumagawa sa lupa, at ang asawa nito na putol ang leeg at puno ng lupa ang bibig. Sa tabi ng bangkay ng babae ay may papel na may nakasulat na “Tales” na isinulat ng daliring isinawsaw sa dugo.</p>
