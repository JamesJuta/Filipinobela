                                    <br><center><h2>SI JULI</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Balita sa buong San Diego ang pagkamatay ni Kapitan Tiyago at pagkakadakip kay Basilio. Dinamdam ito ng buong bayan lalo na si Juli. Hindi mawala sa kanyang isip ang sinapit ng katipan. Nais niyang makalaya ito kaya naisip niyang lapitan si Padre Camorra dahil alam niyang isang salita lamang ng pari ay makakalabas ng kulungan si Basilio. Gabi-gabing hindi pinapatulog ng pangamba ang dalaga at madalas din siyang bangungutin. Urong-sulong siya kung hihingi ng tulong sa pari o hindi.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                       Makaraan ang ilang araw ay nabalitaan niyang nag-iisa na lamang sa kulungan si Basilio dahil nakalaya na ang ibang mga nakapiit. Wala tagapagtanggol si Basilio at wala rin naman kamag-anak lalo pa’t kamakailan lang ay namatay na si Kapitan Tiyago. Ipatatapon daw ito sa Carolinas. Ayaw mang pumunta ni Juli sa kumbento upang humingi ng tulong kay Padre Camorra dahil natatakot siya sa binabalak at hinihinging kapalit nito ay napilitan pa rin siyang humingi ng tulong dito.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Pinilit din siya ni Hermana Bali sa pag-aakalang ang pari na lamang ang pag-asa ni Basilio. Nang hapong iyon ay nagawa ni Padre Camorra ang panghahalay kay Juli. Kinagabihan, usap-usapan si Juli at ang pagtalon niya sa bintana ng kumbento na kanyang ikinamatay. Patakbo siyang nilapitan ni Hermana Bali at bumaba sa pinto ng kumbento. Ang lolo ni Juli na si Tandang Selo ay nagsisigaw at nagwala sa harap ng kumbento. Hindi niya kinaya ang nangyari sa apo. Bigo rin siya sa paghahanap ng hustisiya kaya naisipan na lang ng matanda na sumama sa mga tulisan.</p>
