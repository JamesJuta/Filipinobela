                                    <br><center><h2>TAWANAN AT IYAKAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang piging ay idinaos ng mga mag-aaral sa Panciteria Macanista de Buen Gusto. Labing-apat silang lahat kabilang na si Sandoval. Matatalim magsalita ang mga estudyante. Kahit pa nagtatawanan sila ay ramdam pa din ang kanilang hinanakit.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Dumating na din si Isagani. Si Pelaez na lang ang wala. Sana daw ay si Basilio na lang ang inimbitahan kaysa kay Juanito, ani Tadeo. Malalasing pa daw sana nila si Basilio at baka sakaling mapaamin ang lihim tungkol sa nawawalang bata at sa isang mongha. Habang kumakain ay inihandog nila kay Don Custodio ang pansit lang-lang, ang sopas ay tinaguriang sopas ng panukala, ang lumpiang intsik ay inalay kay Padre Irene at ang torta’y inukol sa prayle (torta de Frailes). Tumutol si Isagani. Di daw dapat isama sa panunumpa ang isang pari. Sinegundahan ito ni Tadeo. Kahabag-habag daw na inihambing ang alimasag sa mga prayle.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Ang pansit gisado naman ay inukol sa pamahalaan at sa bayan. Ani Makaraig, ang pansit ay katutubong lutuing Pilipino. May gusto daw mag-alay ng pansit kay Quiroga na isa raw sa apat na kapangyarihan sa Pilipinas. Samantalang may nagsabi na sa Eminencia Negra (Simoun) raw dapat ialay ang pansit. Hindi handa si Tadeo ng mahilingan siyang magtalumpati. Nagtalumpati din si Pecson na inatake ang mga pari. Aniya, mula raw sa kamusmusan hanggang sa libingan ay mga pari ang kasama natin. May nakakita sa isang utusan ni Padre Sibyla (biserektor sa Unibersidad) na sumakay sa karwahe ni Simoun. Nagtitiktik pala ito sa mga mag-aaral. Nasambit tuloy ni Makaraig ang mga katagang, “Ang busabos ng bise-rektor na pinglilingkuran ng panginoon ng Heneral!”</p>
