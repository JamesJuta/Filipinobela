                                    <br><center><h2>ISANG BANGKAY</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Hindi pumunta sa dulaan si Simoun. Noong ika-pito ng gabi ay makalawang beses umalis at dumating si Simoun sa bahay na may iba’t ibang taong kasama. Nang mag-iikawalo na ay nakita siya ni Makaraig sa may daang Ospital malapit sa kumbento ng Sta. Clara. Nakita naman siya ni Camarroncocido sa may dulaan nang mag-iikasiyam na may kausap na tila estudyante. Hindi rin pumunta sa teatro si Basilio dahil siya ay papunta sa San Diego. Pupunta siya sa bahay ni Kapitan Tiyago upang gamutin ito. Habang inaalagaan ni Basilio ang may sakit ay bigla na lang itong sinusumpong dahil sa sobrang paghithit ng opyo. Nagbilin naman sina Simoun at si Padre Irene kay Basilio na pagalingin ang may sakit at pagtiisan ito sa pag-aalaga.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Ibinunton naman ni Hermana Penchang ang sisi sa lolo ni Juli. Aniya, parusa raw ito dahil sa kakulangan ng pagdadasal at hindi pagturo ni Tandang Selo kay Juli nang maayos. Nang mabalitaang ng Hermana na tutubusin ni Basilio ang kasintahan ay sinabi niyang ang binata ay isang demonyong nag-aanyong estudyante na ibig magpahamak sa kaluluwa ng dalaga. Sa pagrerepaso ni Basilio ay dumating si Simoun. Noon lamang sila muling nagkaharap mula nang huli silang magkita sa San Diego. Kinamusta ni Simoun si Kapitan Tiyago. Ibinalita naman ni Basilio na malubha na ang lagay ng Kapitan dahil kalat na ang lason sa katawan nito.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Muling hinimok ni Simoun si Basilio na makiisa sa himagsikan laban sa pamahalaang Kastila. Ang hindi raw kasi kakampi sa kanila ay ituturing nilang kaaway na dapat patayin. Si Basilio daw ang magtatakas kay Maria Clara sa kumbento ni Sta. Clara habang nagkakagulo ang buong lungsod. Ngunit huli na daw ang lahat, ani Basilio. Nalaman niyang nagpakamatay na raw si Maria Clara dahil naroon daw siya sa kumbento upang makibalita kaya niya nalaman ang nangyari. Nang bumalik siya ay nakita niya ang liham na padala ni Padre Salvi kay Padre Irene na siyang nagpabasa kay Kapitan Tiyago na nag-iiyak nang mabatid na patay na ang anak. Nagulat si Simoun sa narinig niyang balita ngunit ayaw niyang maniwala na totoo ito at pinipilit na buhay pa si Maria Clara. Nang huminahon na ay umalis agad siya ng silid. Naririnig ni Basilio ang mga sigaw ng hinagpis ni Simoun habang paalis ito. Nawala na sa isip ni Basilio ang pag-aaral at sa halip inisip ang kasalukuyang kalagayan ni Simoun.</p>
