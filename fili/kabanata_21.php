                                    <br><center><h2>MGA ANYO NG TAGA-MAYNILA</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang Les Choches de Corneville ng bantog na mga Pranses ay may pagtatanghal sa Teatro de Variendades ng gabing iyon. Mabilis na naubos ang mga tiket para sa dula.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Hindi abalang pumasok sa tanghalan ang Kastila na tila pulubi na si Camarroncocido. Nilapitan siya ni Tiyo Kiko, isang matandang lalaki na nakasuot ng amerikanang hanggang tuhod at ipinakita ang anim na pisong galing sa mga Pranses na kinita niya sa pagdidikit ng mga paskil. Ngunit sinumbatan ni Camarroncocido si Tiyo Kiko at sinabing ang kikitain sa buong palabas ay mapupunta sa mga pari. Ang pagtatanghal ay humati sa Maynila. Masagwa daw at laban sa moralidad ang palabas sabi ng mga tutol na sina Don Custodio, mga pari, pati na mga babaeng may asawa at may kasintahan. May mga taong nagtatanggol naman sa palabas kagaya ng mga pinuno ng hukbo, mga marino, mga matataas na tao, mga kawani at mga babaeng walang kasintahan. Naging malaking bulung-bulungan ang palabas at kasamang nababanggit ang Kapitan Heneral, si Simoun, si Quiroga at ang mga artista. Pinuna ni Camarroncocido ang mga nais manood na kaya daw naroon ay para malaman kung bakit bawal at dapat ipagbawal ang palabas. Nang umalis na si Tiyo Kiko ay may napansin si Camarroncocido na mga taong tila hindi sanay mag-amerikana at sa wari niya ay umiiwas na mapuna. May isang kagawad ng hukbo ang kumausap sa mga di-kilalang tao na apat o lima sa bawat pulutong.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Pagkaraan ay masiglang lumapit ang kagawad sa isang karwahe na lulan si Simoun. May narinig si Camarroncocido na wikang Kastila na ang ibig sabihin ay “Ang hudyat ay isang putok”, saka niya nasabi sa sarili na tila may binabalak ang mga ito. Nagpatuloy sa paglalakad ang pulubi. May dalawang tao siyang naririnig na nag-uusap at sinabing ang mga kura ay malakas kaysa Heneral. Umaalis daw ang Heneral samantalang ang mga pari ay naiiwan at ang hudyat na isang putok ang kanilang ikayayaman. Naaawa man sa bayan ngunit wala namang pakiaalam si Camarroncocido sa mga narinig. Samantala, sa labas ng dulaan ay makikita si Tadeo na niloloko ang isang kababayang tanga sa pagsasabi ng mga kahanga-hangang kasinungalingan. Sinabi ni Tadeo sa kausap na kilala niya ang mga malalaking tao na nadaan at kaibigan niya ang mga ito kahit hindi naman totoo.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Dumating ang mga mag-tiyahin na sina Donya Victorina at Paulita Gomez. Kahit na nagbabalatkayo ay nakilala pa rin ni Tadeo si Padre Irene na ‘di naitago ng tunay na katauhan dahil sa mahaba niyang ilong. Dumating din si Don Custodio. Nang dumating sina Makaraig, Pecson, Sandoval at Isagani ay lumapit si Tadeo sa kanila saka bumati. May sobra silang isang tiket dahil hindi sumama si Basilio. Dahil dito’y inanyayahan nila sa Tadeo sa loob, agad siyang sumama sa apat, at iniwan ang kababayang kanina lamang ay kausap ngunit ngayon ay nag-iisa na.</p>
