                                    <br><center><h2>SI DON CUSTODIO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Nasa mga kamay ni Don Custodio ang usapin sa akademya ng wikang Kastila. Siya ang pinagkatiwalaan lumutas sa suliraning ito. Si Don Custodio de Salazar y Sanchez de Monteredondo o kilala sa tawag na “Buena Tinta” ay tanyag sa bahagi ng lipunan sa Maynila. Nakapag-asawa siya ng mayaman at sa pamamagitan nito ay nakapag-negosyo. Siya ay pinupuri kahit kulang sa kaalaman sa mga tungkuling kanyang hinahawakan dahil si Don Custodio ay masipag.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Nang magtungo sa Espanya ay walang pumansin sa kanya dahil sa kakulangan niya sa pinag-aralan. Wala pang isang taon ay bumalik na rin siya sa Pilipinas at nagyabang sa mga Pilipino sa kanyang kunwaring magandang karanasan sa Madrid. Ipinalagay niyang siya’y isang amo at manananggol. Naniniwala siyang may ipinanganak upang mag-utos at ang iba’y upang sumunod. Para sa kanya, ang mga Pilipino ay ipinanganak upang maging utusan. Pagkaraan ng labinlimang araw ay nakabuo na ng pasya si Don Custodio at handa na niya itong ipaalam sa lahat.</p>
