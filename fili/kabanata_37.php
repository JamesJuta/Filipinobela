                                    <br><center><h2>ANG HIWAGAAN</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Sa kabila ng pagpipigil sa balita sa mga kaganapan noong gabi sa piging ay nalaman pa rin ito ng madla. Naging usap-usapan ito ng lahat ngunit palihim nga lamang.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                       Ang payat na platerong si Chikoy ay nagdala kasi ng hikaw kay Paulita. Tinatanggal na noon ang mga palamuti at sa mga hapag sa bahay ni Kapitan Tiyago at nakita niya mismo ang maraming supot na pulbura sa ilalim ng mesa, sa silong, sa bubungan, at sa likod ng mga upuan. Ayon daw kay Ginoong Pasta ay iisa ang maaring gumawa ng gayon. Maaring isang kaaway ni Don Timoteo o kaagaw ni Juanito kay Paulita. Binalaan agad ng may-ari ng bahay na si Kapitan Loleng kung saan nanunuluyan si Isagani na magtago ito. Ngunit ngumiti lamang ang binata.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Ipinagpatuloy ni Chikoy ang pagbabalita. Aniya, dumating daw ang mga sibil ngunit wala namang mapagbintangan. Si Don Timoteo lamang daw at si Simoun ang nangasiwa sa pag-aayos ng bahay na pinagdausan ng piging. Pinaalis daw ang lahat ng mga ‘di kailangan sa imbestigasyon. Natakot ang mga babaeng nakikinig sa ibinalita ni Chikoy. Hinulaan nila kung sino ang may kagagawan ng lahat. May nagsabing ang mga prayle daw. Ang iba ay si Quiroga daw, o kaya ay ang mga mag-aaral. Baka daw si Makaraig. At tumingin si Kapitan Toringgoy kay Isagani. Ang sabi ni Chikoy, ayon daw sa ilang kawani ay si Simoun ang may kagagawan noon. Kaya naman nagtaka ang lahat. Biglang naalala ni Momoy, isa sa mga dumalo sa piging, ang pag-alis ni Simoun bago magsimula ang hapunan. Ang sabi naman ni Chikoy ay nawawala daw si Simoun at kasalukukang pinaghahahanap ng mga sibil. Lalong nabuo sa isip ng mga kababaihan ang pagiging demonyo ni Simoun na nagkatawan umanong tao. Dagdag pa ni Chikoy, iniisip umano ng mga may kapangyarihan na ang ilawan ang siyang magpapasiklab sa pulbura na nasa buong kabahayan. Biglang natakot si Momoy ngunit nang makita ang kasintahan na si Siensa na nakatingin sa kanya ay nagtapang-tapangan ito. Makaraan ng ilang sandali ay nagpaalam na si Isagani at umalis. Hindi na ito kaylan man bumalik pa sa kanyang amain.</p>
