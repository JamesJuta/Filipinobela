                                    <br><center><h2>ANG MGA PILATO</h2></center> 

                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Naging usap-usapan sa bayan ang mga nangyari kay Tandang Selo. Karamihan sa mga tao ay walang pakialam samantalang ang iba ay walang habas kung pagtsismisan ang matanda. Anang ilan, kung di lamang daw umalis si Kabaesang Tales ay baka hindi hindi daw nangyayari iyon kay Tandang Selo. Nag-usap-usap din ang mga ito kung sino ba ang may kasalanan kaya nangyari ang kamalasan kay Tandang Selo.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;
                                        Ibinunton naman ni Hermana Penchang ang sisi sa lolo ni Juli. Aniya, parusa raw ito dahil sa kakulangan ng pagdadasal at hindi pagturo ni Tandang Selo kay Juli nang maayos. Nang mabalitaang ng Hermana na tutubusin ni Basilio ang kasintahan ay sinabi niyang ang binata ay isang demonyong nag-aanyong estudyante na ibig magpahamak sa kaluluwa ng dalaga.</p>
                                    <br>
                                    <p>&nbsp;&nbsp;  
                                        Samantala, nakauwi na si Kabesang Tales dahil sa tulong ng salaping napagbilhan ng mga alahas ni Juli at nautang ng dalaga kay Hermana Penchang. Nalaman din niyang nagpaupang ○ utusan si Juli, ang kanyang lupa ay pagmamay-ari na ng iba, at napipi ang amang si Tandang Selo. Pinapaalis na din siya sa kanyang bahay sa utos na rin ng hukuman at binigyan lamang ng tatlong araw para maialis ang lahat ng kanilang gamit. Ito nama’y ikinatuwa ng mga pari at ng bagong may-ari ng kanyang lupa. Dahil sa mga kaganapang ito ay naupo lamang sa isang tabi ang Kabesa at nanatiling walang kibo.</p>
