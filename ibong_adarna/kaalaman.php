                            <h1 class="katanungan_title title"><i class='bx bx-help-circle icon'></i> Ano ang Korido?</h1>
                                
                                <div class="katanungan_second_container">

                                    <p> &nbsp;&nbsp;
                                        Ang salitang “korido” ay nagmula sa Kastila na “corrido” na nangangahulugang “tulad ng kuwento” o “katulad ng koridor.” Ito ay isang tradisyunal na anyo ng panitikan na popular noong panahon ng Espanyol.</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang "korido" ay isang uri ng tanyag na panitikan sa Pilipinas. Ito ay binubuo ng mga salaysay na nagpapakita ng mga pangyayari at kaganapan sa buhay ng mga bayani o mga tauhan nito. Madalas na nagsasaad ng mga pakikipagsapalaran, paglalakbay, at mga pagsubok na kinakaharap ng mga pangunahing tauhan.</p>
                                    <br><br>

                                    <h1>Uri ng Korido</h1><br>
                                    <p> 
                                        Mayroong dalawang pangunahing uri ng korido:</p>
                                    <p> 
                                        - ang korido ng pamamahayag</p>
                                    <p> 
                                        - ang korido ng pamamasyal</p>
                                    <br>
                                    <p> &nbsp;&nbsp;
                                        Ang korido ng pamamahayag ay tumatalakay sa mga dakilang pangyayari sa kasaysayan ng isang bansa o lipunan, samantalang ang korido ng pamamasyal ay naglalarawan ng mga pakikipagsapalaran ng mga tauhan na naglalakbay sa iba’t ibang lugar. Ang bawat uri ng korido ay naglalaman ng sariling mga tema at mga elemento na nagbibigay-buhay sa kuwento.</p>
                                    <br><br>
                                    
                                    <h1>Halimbawa ng Korido</h1><br>
                                    <p> &nbsp;&nbsp;
                                        Isa sa mga halimbawa ng korido ay ang “Ibong Adarna”.</p>
                                    <br>
                                    <p> &nbsp;
                                        Ang koridong ito ay tungkol sa kuwento ng paghahanap ng isang prinsipe sa ibong Adarna upang mapagaling ang kanyang amang hari. Sa bawat pagsubok na kanyang hinarap, ipinapakita ng korido ang katatagan ng kanyang pagkatao at ang halaga ng pagtitiis at katapangan.</p>
