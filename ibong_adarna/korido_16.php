                                                [567] Sa palasyo'y nakadungaw <br>
                                                Si Leonorang matimtiman, <br>
                                                ang prinsipe, nang matanaw <br>
                                                biglang nagulumihanan. <br>
                                                <br>
                                                [568] Nabigla itong prinsesa  <br>
                                                sa taong kanyang nakita, <br>
                                                si Don Jua'y napatanga <br>
                                                sa palasyong pagkaganda. <br>
                                                <br>
                                                [569] Ang palasyo kung munti man  <br>
                                                ay malaking kayamanan, <br>
                                                walang hindi gintong lantay <br>
                                                ang doon ay tititigan. <br>
                                                <br>
                                                [570] Palamuti sa bintana  <br>
                                                palamuti'y isang mutya; <br>
                                                perlas, rubing tila Iuha <br>
                                                ng langit sa abang lupa! <br>
                                                <br>
                                                [571] Sa gitna ng mga perlas  <br>
                                                tala manding namanaag <br>
                                                si Leonorang pagkarilag, <br>
                                                ang prinsipe'y napakurap!  <br>
                                                <br>
                                                [572] Natikom ang kanyang bibig <br>
                                                dila ay parang nadikit, <br>
                                                mga matang nakatitig<br>
                                                alitaptap na namitig! <br>
                                                <br>
                                                [573] Kaya lamang nakahuma <br>
                                                nang simulan ni Leonora: <br>
                                                "O, pangahas, sino ka ba, <br>
                                                at ano ang iyong pita?" <br>
                                                <br>
                                                [574] "Aba, Palabà ng Buwan,  <br>
                                                Tala sa madaling -araw, <br>
                                                hingi ko'y kapatawaran <br>
                                                sa aking kapangahasan. <br>
                                                <br>
                                                [575] "Sa mahal mong mga yakap  <br>
                                                alipin mo akong tapat, <br>
                                                humahalik at ang hangad, <br>
                                                maglingkod sa iyong dilag." <br>
                                                <br>
                                                [576]. "Di mo baga nalalamang  <br>
                                                mapanganib iyang buhay; <br>
                                                sa serp'yente kong matapang, <br>
                                                walang salang mamamatay?" <br>
                                                <br>
                                                [577] "Mapanganib man ngang lubha  <br>
                                                ano pa ang magagawa, <br>
                                                kung palad kong magsaliwa<br>
                                                tanggapin ang pagkadusta. <br>
                                                <br>
                                                [578] "Hindi gaanong masaklap <br>
                                                na mapatay ng kalamas,<br>
                                                sa akin ang dusa't hirap, <br>
                                                masawi sa iyong lingap." <br>
                                                <br>
                                                [579] "Ikaw baga'y nagbibiro  <br>
                                                O ako'y sinisiphayo? <br>
                                                Hayo't dito ay lumayo <br>
                                                taong lubhang mapaglako. <br>
                                                <br>
                                                [580] "Hindi kita kailangan  <br>
                                                ni makita sa harapan, <br>
                                                umalis ka't manghinayang <br>
                                                sa makikitil mong buhay." <br>
                                                <br>
                                                [581] Ang prinsipe'y di tuminag  <br>
                                                sa anyong kahabag-habag, <br>
                                                idinaing din ang hirap <br>
                                                ng pagsinta niyang tapat. <br>
                                                <br>
                                                [582] "Pinopoon kong Prinsesa,  <br>
                                                galit mo po ay magbawa,<br>
                                                kung ako'y nagkasala <br>
                                                ito'y dahil sa pagsinta. <br>
                                                <br>
                                                [583] "Danga't ako'y nagkapuso  <br>
                                                na ginising ng pagsuyo <br>
                                                sa dilag mo'y kailan ko po <br>
                                                matanggap ang pagsiphayo? <br>
                                                <br>
                                                [584] "Labis-labis ang paggalang  <br>
                                                sa iyo pong kamahalan, <br>
                                                hingin man nga yaring buhay <br>
                                                sa galit mo po ay kulang. <br>
                                                <br>
                                                [585] "Gasino na yaring palad  <br>
                                                na hamak sa lalong hamak,<br>
                                                kung may daan pang tumaas <br>
                                                nang sa iyo'y maging dapat. <br>
                                                <br>
                                                [586] "Sa iyong kapangyarihan  <br>
                                                sino kaya ang susuway? <br>
                                                ngunit ang di ko malama'v <br>
                                                ang gagawin kong pagpanaw. <br>
                                                <br>
                                                [587] "Suwayin ang iyong nais,  <br>
                                                pinid sa akin ang langit; <br>
                                                lumayo sa iyong titig, <br>
                                                hininga ko'y mapapatid. <br>
                                                <br>
                                                [588] "Sa gipit kong kalagayang <br>
                                                walang hindi kabiguan  <br>
                                                ikaw na Prinsesang mahal <br>
                                                ang magbigay kapasiyahan." <br>
                                                <br>
                                                [589] Itong mga huling hiling <br>
                                                kay Leonora nang marinig <br>
                                                nawala ang angking galit <br>
                                                pagsinta'y napasadibdib. <br>
                                                <br>
                                                [590] Sa matinding pagkaawa  <br>
                                                ang puso ay lumuluha, <br>
                                                danga't hindi nahihiya <br>
                                                niyapos ang may dalita. <br>
                                                <br>
                                                [591] Lihim niyang pagkahabag  <br>
                                                sa titig naipahayag <br>
                                                isang titig na malingap <br>
                                                na langit na ng pagliyag. <br>
                                                <br>
                                                [592] Saka masuyong lumapit  <br>
                                                sa prinsipeng nahahapis,<br>
                                                at ang wikang pagkatamis: <br>
                                                "Di rin ako nakatiis..."  <br>
                                                <br>
                                                [593] Isang titig na mairog  <br>
                                                matamis pa kaysa pulot,<br>
                                                nang tumama sa may lunos <br>
                                                sa puso nito tumagos.  <br>
                                                <br>
                                                [594] Nang tumagos na sa puso  <br>
                                                Saka lamang napaghulong <br>
                                                silang dati'y magkalayo <br>
                                                sa sandali ay nabuo. <br>
                                                <br>
                                                [595] Nabuo at nang huwag nang  <br>
                                                paglayuin ng pagsinta; <br>
                                                ang magtaksil sa kanila <br>
                                                sa Diyos ay may parusa. <br>
                                                <br>
                                                [596] Ito na nga ang bumasag  <br>
                                                katahimikang maluwat, <br>
                                                si Don Juan ay tinawag <br>
                                                ni Leonorang marilag. <br>
                                                <br>
                                                [597] "Prinsipe, ikaw'y pumanhik  <br>
                                                dito na tayo magniig,<br>
                                                bahay ko ma'y di marikit, <br>
                                                paya pa't di maligalig." <br>
                                                <br>
                                                [598] Sa prinsipe ay nabuksan  <br>
                                                ang pinto ng kalangitan, <br>
                                                noon niya naramdamang <br>
                                                hirap niya'y napalitan. <br>
                                                <br>
                                                [599] "Prinsesa kong pinopoon, <br>
                                                salamat sa pag-aampon, <br>
                                                mag-utos ka't umaayon <br>
                                                itong lingkod mula ngayon." <br>
                                                <br>
                                                [600] "Unang ibig kong malaman <br>
                                                kung pa'no mo natuklasan  <br>
                                                itong lihim kong tahanan <br>
                                                sa Iiblib ng kabundukan?"<br>
                                                <br>
                                                [601] "Prinsesa kong kasi't mutya,  <br>
                                                ang nangyari'y talinghaga; <br>
                                                Hamak yaring aking dila <br>
                                                na magsaysay ng himala. <br>
                                                <br>
                                                [602] "Isang gabing kalaliman  <br>
                                                na ako'y nahihimlay, <br>
                                                ginising ng panagimpang <br>
                                                balòng ito ay tinuran. <br>
                                                <br>
                                                [603] "Ito, anya, ay lakbayi't  <br>
                                                pagsikapan kong hanapin, <br>
                                                magdusa mang sapin-sapin<br>
                                                may ligayang tatamuhin. <br>
                                                <br>
                                                [604] "Narito raw yaong talang  <br>
                                                lunas sa aking dalita, <br>
                                                talang ito ay ikaw nga, <br>
                                                O Leonora kong mutya. <br>
                                                <br>
                                                [605] "Pagkat lihim itong balon  <br>
                                                sinong taong sakdal dunong <br>
                                                ang dito'y makatutulong <br>
                                                kundi Diyos ang may ampon? <br>
                                                <br>
                                                [606] "Sa Diyos na ngang talaga  <br>
                                                ang sa iyo'y pagkakita, <br>
                                                kaya, mabunying prinsesa, <br>
                                                lunasan mo yaring dusa." <br>
                                                <br>
                                                [607] "A, Don Juan, di ko nais  <br>
                                                hamakin ka sa paghibik,<br>
                                                kung sa iyo ma'y nagalit <br>
                                                subok lamang ng pag-ibig.  <br>
                                                <br>
                                                [608] "Sinubok ko nga lamang  <br>
                                                kung ang puso mo'y marangal, <br>
                                                ugali ng alinlanga't <br>
                                                "Pipitasin ang bulaklak. <br>
                                                <br>
                                                [609] "Pagkat marami sa puso  <br>
                                                talusira sa pangako, <br>
                                                sa pagsinta'y mapagbiro't <br>
                                                matuwaing sumiphayo.  <br>
                                                <br>
                                                [610] "Pipitasin ang bulaklak  <br>
                                                sa tangkay na nag-iingat,<br>
                                                mahal habang di pa kupas, <br>
                                                pag nalanta ay sa layak!" <br>
                                                <br>
                                                [611] "Leonora kong minamahal, <br>
                                                O buhay ng aking buhay, <br>
                                                sa puso ko't katauha'y <br>
                                                wala ka nang kalantahan. <br>
                                                <br>
                                                [612] "Bulaklak ka ng pag-ibig,  <br>
                                                pabango sa aking dibdib, <br>
                                                tuwing ako'y mahahapis, <br>
                                                lunas na ang iyong titig. <br>
                                                <br>
                                                [613] "Sa pagtulog at paggising,  <br>
                                                ikaw ang aking salamin; <br>
                                                mata ko may mangulimlim <br>
                                                liwanag mo'y iilawin. <br>
                                                <br>
                                                [614] "Kaya pawiin na, giliw ko,  <br>
                                                alapaap sa puso mo; <br>
                                                sa tibay ng iyong 'oo' <br>
                                                ikaw'y aki't ako'y iyo." <br>
                                                <br>
                                                [615] Pag-uusap na matamis  <br>
                                                ng magsinta ay napatid <br>
                                                nang umugong at yumanig <br>
                                                lupa't palasyong matarik. <br>
                                                <br>
                                                [616] Dumating na ang serp'yenteng  <br>
                                                kay Leonora'y may kandili,<br>
                                                kakila-kilabot ang laki, <br>
                                                umuungal na mabuti. <br>
                                                <br>
                                                [617] Sa takot ni Leonora  <br>
                                                napasigaw kapagdaka: <br>
                                                "Ay, Don Juan, aking sinta, <br>
                                                buhay nati'y paano na?"  <br>
                                                <br>
                                                [618] Ang prinsipe'y di umimik  <br>
                                                pinagbuti yaong tindig. <br>
                                                ang serp'yente'y sinisilip <br>
                                                sa gagawing di matuwid. <br>
                                                <br>
                                                [619] Sa hagdanan iyong ahas  <br>
                                                pati ulo'y nangagtaas, <br>
                                                mga mata'y nandidilat<br>
                                                tiyak na may hinahanap. <br>
                                                <br>
                                                [620] Pagalit na nagsalita:  <br>
                                                "Dito ay amoy manusya,<br>
                                                Leonora, bakit kaya <br>
                                                may tao'y ikinaila?" <br>
                                                <br>
                                                [621] Dinaluhong ng prinsipe  <br>
                                                ng espada ang serp'yente <br>
                                                kasabay ang pagsasabing <br>
                                                "Ang buhay mo'y mapùputi!" <br>
                                                <br>
                                                [622] Sagot ng serp'yente'y ito:  <br>
                                                "Iyan ang hinahanap ko, <br>
                                                magsisi ka at totoong <br>
                                                makikitil ang buhay mo."  <br>
                                                <br>
                                                [623] Ang dalawa ay naglaban  <br>
                                                nagtagpo ang kapwa tapang <br>
                                                subalit kaliksihan <br>
                                                namayani si Don Juan. <br>
                                                <br>
                                                [624] Kaya't hindi nga malingkis, <br>
                                                ang serp'yente'y nadaraig,<br>
                                                tuwing sila'y maglalapit <br>
                                                ang espada'y parang lintik. <br>
                                                <br>
                                                [625] Mga sugat sa katawan  <br>
                                                sa ahas ay walang patlang, <br>
                                                patuloy rin sa paglaba't <br>
                                                parang walang kapansanan. <br>
                                                <br>
                                                [626] Lalong nakapagtataka  <br>
                                                galing nitong dinadala, <br>
                                                ulong put'lin ng espada <br>
                                                buhay ri't nasusugpong pa. <br>
                                                <br>
                                                [627] Kaya't mahirap mapatay  <br>
                                                kahit sinong makalaban, <br>
                                                kung wala ring kalaruang <br>
                                                engkantong may kabagsikan. <br>
                                                <br>
                                                [628] Ibig-ibig nang masindak  <br>
                                                ni Don Juang walang gulat, <br>
                                                pagkat kung tingnan ang ahas <br>
                                                nag-iibayo ang dahas. <br>
                                                <br>
                                                [629] Anhin man niyang malasin  <br>
                                                ahas na ibig patayin, <br>
                                                may buhay na Sapin-sapi't <br>
                                                hindi yata makikitil. <br>
                                                <br>
                                                [630] Dito na siya tumawag  <br>
                                                sa Diyos, haring mataas, <br>
                                                sa kalaban niyang ahas <br>
                                                huwag nawang mapahamak.  <br>
                                                <br>
                                                [631] Di naman siya maigupo  <br>
                                                huwag siyang masiphayo,<br>
                                                ni maubusan ng dugo't <br>
                                                pagkatao'y maitayo. <br>
                                                <br>
                                                [632] Mataimtim palibhasa  <br>
                                                ang pagtawag kay Bathala, <br>
                                                sindak niya ay nawala't <br>
                                                katapangan ay lumubha. <br>
                                                <br>
                                                [633] Noon din ay naramdamang  <br>
                                                nawala ang kanyang pagal,<br>
                                                para bagang bago lamang <br>
                                                sa ahas ay lumalaban. <br>
                                                <br>
                                                [634] Lalo niyang nakilalang  <br>
                                                ang Diyos ay nasa kanya <br>
                                                nang hapuin ang kalaban <br>
                                                hingi'y mamahinga muna.  <br>
                                                <br>
                                                [635] Sa tagal na tatlong oras  <br>
                                                Na kanilang paglalamas,<br>
                                                nakaramdam itong ahas <br>
                                                sa katawan ng pulikat. <br>
                                                <br>
                                                [636] At kung di muna titigil  <br>
                                                lakas niya'y uubusin, <br>
                                                anupa ang mararating <br>
                                                kundi siya'y magupiling.  <br>
                                                <br>
                                                [637] Si Don Juan ay umayon  <br>
                                                ang sandata'y isinalong, <br>
                                                ang ahas sa pagkatukol <br>
                                                binayaang mahinahon.  <br>
                                                <br>
                                                [638] Noon ay isang pagdungaw  <br>
                                                ni Leonorang nalulumbay, <br>
                                                magiliw na tinawagan <br>
                                                ang prinsipe niyang mahal. <br>
                                                <br>
                                                [639] "Don Juan, tingna't narito  <br>
                                                ang mabagsik na balsamo, <br>
                                                na sa bawat isang ulong <br>
                                                mapuputol, ibuhos mo. <br>
                                                <br>
                                                [640] "Ulong putol na mabusan <br>
                                                ay hindi na mabubuhay <br>
                                                at siya nang pagkamatay <br>
                                                ng serp'yenteng tampalasan." <br>
                                                <br>
                                                [641] Nang makita ng serp'yente  <br>
                                                ang inabot ng prinsipe, <br>
                                                nanghilakbot ang sarili't <br>
                                                ang galit ay di masabi. <br>
                                                <br>
                                                [642] Pitong ulo'y itinaas  <br>
                                                mga mata'y pinag-alab, <br>
                                                lingkisin ang kanyang hangad <br>
                                                ang sa kanya ay nagsukab. <br>
                                                <br>
                                                [643] Sinalakay si Don Juan,  <br>
                                                ito'y nakaiwas naman; <br>
                                                at sa muling lalabanan <br>
                                                ang serp'yente'y nagulapay. <br>
                                                <br>
                                                [644] Sa ulos na walang puknat,  <br>
                                                tagang iwinawasiwas, <br>
                                                isa-isang natitigpas <br>
                                                mga ulo niyong ahas.  <br>
                                                <br>
                                                [645] Anim na ang nangaputol  <br>
                                                katapanga'y nag-uulol,<br>
                                                kung dumamba'y umuugong <br>
                                                daluhong din nang daluhong. <br>
                                                <br>
                                                [646] Malakas na nagsalita:  <br>
                                                "Mag-ingat, mga kuhila,<br>
                                                sa galit ko't pagkadusta, <br>
                                                magugunaw itong lupa. <br>
