                                                may bali ang mga tadyang  <br>
                                                at ang lalong dinaramdam  <br>
                                                ay gutom at sobrang uhaw.<br>
                                                <br>
                                                [277] Sa ilang na pagkalawak <br>
                                                na wala ni kubong hamak,  <br>
                                                sino kaya ng matatawag  <br>
                                                dumamay sa gayong hirap?<br>
                                                <br>
                                                [278] Sa kawalan ng pag-asa, <br>
                                                sa Diyos na tumalaga; <br>
                                                kung gumaling ay ligaya,  <br>
                                                kung masawi'y palad niya. <br>
                                                <br>
                                                [279] Hindi niya nalimutang <br>
                                                tumawag sa Birheng Mahal,  <br>
                                                lumuluhang nanambitang <br>
                                                tangkilikin kung mamatay.<br>
                                                <br>
                                                [280] "O, Birheng Inang marilag <br>
                                                tanggulan ng nasa hirap,  <br>
