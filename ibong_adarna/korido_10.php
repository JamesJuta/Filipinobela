                                                sa pagsuyo nananagana, <br>
                                                munting magkabahid-luha  <br>
                                                ama’t ina’y may dalita. <br>
                                                <br>
                                                [301] Nadama ang laking dusa <br>
                                                malayo sa isang ina,  <br>
                                                sa ilang ay nag-iisa,  <br>
