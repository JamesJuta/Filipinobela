                                                <br>
                                                [281] "Kung wala nang kapalarang <br>
                                                humaba pa yaring buhay,  <br>
                                                loobin mo, Inang mahal,  <br>
                                                ang ama ko ang mabuhay.<br>
                                                <br>
                                                [282] "Madlang hirap at parusa <br>
                                                di ko sasapitin sana,  <br>
                                                kung di po sa aking pitang <br>
                                                magulang ko'y guminhawa.<br>
                                                <br>
