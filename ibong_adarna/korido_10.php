                                                o pagsumbat sa sarili;  <br>
                                                salamat kung makabuti  <br>
                                                ang munti kong naisilbi.<br>
                                                <br>
                                                [284] “Di ko maubos-isipin <br>
                                                kapatid ko ay nagtaksil,  <br>
                                                kung sa ibon po ang dahil <br>
                                                kanila na’t din na akin <br>
                                                <br>
                                                [285] “Kung sa bagay ay di iba <br>
                                                at ako nga ang kumuha,  <br>
                                                maging ako’t maging sila  <br>
                                                kung tutuusin ay iisa. <br>
                                                <br>
                                                [286] “Di nga ba kaming tatlo <br>
                                                anak ng iisang tao,  <br>
                                                iwasan ang pagtatalo  <br>
                                                At di gawang maginoo <br>
                                                <br>
                                                [287] “Kaya kami nangaglakbay <br>
                                                sa kay amang kagalingan,  <br>
                                                ano’t ngayong magtagumpay  <br>
                                                hahangga sa pag-aaway? <br>
                                                <br>
                                                [288] “Sila nawa’y patawarin <br>
                                                ng Diyos na maawain;  <br>
                                                kung sila man ay nagtaksil,  <br>
                                                kamtin nila ang magaling. <br>
                                                <br>
                                                [289] “Sa akin po ay ano na <br>
                                                sinadlak man nga sa dusa  <br>
                                                kung may daan pang magkita  <br>
                                                pag-ibig ko'y kanila pa."<br>
                                                <br>
                                                [290] Kirot ng buong katawan<br>
                                                sa tindi’y di natagalan,  <br>
                                                sa bato’y napalupaypay  <br>
                                                nahinti ang panambitan.<br>
                                                <br>
                                                [291] Ipinako ang paningin <br>
                                                sa taas ng panginorin;  <br>
                                                pagkaganda ng bituing  <br>
                                                ilaw na lubhang maningning.<br>
                                                <br>
                                                [292] “O, bituing nasa langit, <br>
                                                bulaklak na walang hapis; <br>
                                                inyo kayang nasisilip  <br>
                                                akong sawi't nasa sakit? <br>
                                                <br>
                                                [293] "Kaila kaya sa inyo <br>
                                                na rito ang mga tao,  <br>
                                                kapatid man o katoto  <br>
                                                ay lihim na kaaway mo? <br>
                                                <br>
                                                [294] “Kung ikaw ay masasawi <br>
                                                sa lupa ay mapagawi,  <br>
                                                ang sa iyo ay ngumiti  <br>
                                                may paglibak at aglahi.<br>
                                                <br>
                                                [295] “Lahat dito’y pasaliwa <br>
                                                walang hindi balintuna,  <br>
                                                ang mabuti ay masama’t  <br>
                                                ang masama ay dakila. <br>
                                                <br>
                                                [296] “Dito mo nga makikita <br>
                                                ang papuring palamara,  <br>
                                                ang yakap na lumayas ka  <br>
                                                at pagsuyong lason pala. <br>
                                                <br>
