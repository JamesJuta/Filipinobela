                                                gagapang-gapang sa gulod, <br>
                                                tumatawag ng kukupkop  <br>
                                                walang sinumang dumulog. <br>
                                                <br>
                                                [305] “Sino ang mag-aakalang <br>
                                                ang bunso mo’y madudusta; <br>
                                                sa ganito kong pagluha, <br>
                                                anak mo rin ang may gawa.” <br>
                                                <br>
