                                            <p class="novel-text">
                                                [197] Yumao na si Don Juan  <br>
                                                sa Tabor na kabundukan,  <br>
                                                nang maagang maabangan <br>
                                                yaong ibong kanyang pakay. <br>
                                                <br>
                                                [198] Dumating sa punongkahoy  <br>
                                                nang wala pa yaong ibon,  <br>
                                                kaya't sandaling nagnuynoy <br>
                                                ng marapat gawin doon. <br>
                                                <br>
                                                [199] Ngunit hindi natagalan <br>
                                                sa ganitong paghihintay <br>
                                                sa kanya nang natanawan <br>
                                                ang Adarna’y dumaratal. <br>
                                                <br>
                                                [200] Napuna pa nang dumapo  <br>
                                                ang Adarna'y tila hapo,  <br>
                                                kaya't kanyang napaghulo  <br>
                                                ibo'y galing sa malayo. <br>
                                                <br>
                                                [201] Pagkalapag ay naghusay  <br>
                                                ng kanyang buong katawan;  <br>
                                                ang pagkanta'y sinimulan,  <br>
                                                tinig ay pinag-iinam. <br>
                                                <br>
                                                [202] Ginamit na unang gayak  <br>
                                                sa prinsipe'y nakabihag,  <br>
                                                kung malasi'y sadyang perlas  <br>
                                                nagniningning ang liwanag. <br>
                                                <br>
                                                [203] Nagbago ng kanyang bihis  <br>
                                                na lumalo pa ang rikit,  <br>
                                                katugon ng inaawit na  <br>
                                                malambing at matamis. <br>
                                                <br>
                                                [204] Natutukso nang matulog  <br>
                                                si Don Juang nanunubok;  <br>
                                                ang labaha ay dinukot  <br>
                                                at ang palad ay binusbos. <br>
                                                <br>
                                                [205] Lamang ngumiti sa balat  <br>
                                                pinigaan na ng dayap,  <br>
                                                sa hapdi'y halos maiyak  <br>
                                                dugo'y bumukal sa sugat. <br>
                                                <br>
                                                [206] Napawi ang pag-aantok  <br>
                                                dahil sa tindi ng kirot;  <br>
                                                si Don Juan ay lumuhod,  <br>
                                                nagpasalamat sa Diyos. <br>
                                                <br>
                                                [207] Pitong kanta'y pinagwakas  <br>
                                                nitong ibong sakdal dilag,  <br>
                                                pito rin ang naging sugat  <br>
                                                ni Don Juang nagpupuyat. <br>
                                                <br>
                                                [208] Ang ibon ay nagbawas  <br>
                                                na ugali pagtulog niya,  <br>
                                                ang prinsipeng nakakita  <br>
                                                ay umilag kapagdaka. <br>
                                                <br>
                                                [209] Kaya hindi tinamaa't  <br>
                                                naligtas sa kasawian,  <br>
                                                inantay nang mapahimlay  <br>
                                                ang Adarnang susunggaban. <br>
                                                <br>
                                                [210] Kung matulog ang Adarna  <br>
                                                ang pakpak ay nakabuka,  <br>
                                                dilat ang dalawang mata  <br>
                                                kaya't gising ang kapara. <br>
                                                <br>
                                                [211] Nang prinsipe'y makatiyak  <br>
                                                tulog ng ibo'y panatag,  <br>
                                                dahan-dahan nang umakyat  <br>
                                                sa puno ng Piedras Platas. <br>
                                                <br>
                                                [212] Agad niyang sinunggaban  <br>
                                                sa paa'y biglang tinangnan <br>
                                                at ginapos nang matibay  <br>
                                                ng sintas na gintong lantay. <br>
                                                <br>
                                                [213] Sa katuwaang tinamo <br>
                                                halos di magkantututo;  <br>
                                                ang Adarna ay pinangko, <br>
                                                dinala sa ermitanyo. <br>
                                                <br>
                                                [214] Magalak namang kinuha  <br>
                                                ang nahuli nang Adarna,  <br>
                                                hinimas pa nang masaya  <br>
                                                nang ipasok na sa hawla. <br>
                                                <br>
                                                [215] Saka anang ermitanyo:  <br>
                                                "Iyang banga ay kunin mo,  <br>
                                                madali ka at sa iyo'y  <br>
                                                merong iuutos ako. <br>
                                                <br>
                                                [216] "Punuin mo ng tubig iya't  <br>
                                                ang dalawang bato'y buhusan,  <br>
                                                nang sa bato'y magsilitaw  <br>
                                                ang dalawang iyong mahal." <br>
                                                <br>
                                                [217] Si Don Juan ay sumalok <br>
                                                ng tubig na inutos, <br>
                                                at sa batong nakapuntod <br>
                                                dahan-dahang ibinuhos. <br>
                                                <br>
                                                [218] Si Don Pedro ay nagtindig  <br>
                                                at niyakap ang kapatid, <br>
                                                sa pagkadaop ng dibdib <br>
                                                kapwa silá nananangis. <br>
                                                <br>
                                                [219] Isinunod si Don Diego <br>
                                                na nang muling maging tao <br>
                                                di mawari itong mundo  <br>
                                                kung ang dati o nabago. <br>
                                                <br>
                                                [220] Tuwa'y umaapaw sa dibdib, <br>
                                                sa tatlong magkapatid,  <br>
                                                bawat isa ay may sambit <br>
                                                ng sa puso ay pag-ibig. <br>
                                                <br>
                                                [221] Lalo na nga ang dalawang <br>
                                                sa dalita ay natubos na, <br>
                                                anuman ang ialala <br>
                                                kay Don Juan ay kulang pa. <br>
                                                <br>
                                                [222] Wala silang mahagilap <br>
                                                na salitang matitimyas <br>
                                                o anumang maitumbas <br>
                                                kay Don Juang mga hirap. <br>
                                                <br>
                                                [223] Ang kanilang pagsasaya <br>
                                                Di na hangad matapos pa, <br>
                                                Ngunit biglang naalala <br>
                                                Ang may sakít nilang ama. <br>
                                                <br>
                                                [224] Kaya agad napatungo <br>
                                                sa bahay ng ermitanyo,  <br>
                                                upang ipagsulit dito <br>
                                                ang ligaya nilang tatlo. <br>
                                                <br>
                                                [225] Sila nama’y hinainan <br>
                                                ng pagkaing inilaan, <br>
                                                bílang isang pagdiriwang  <br>
                                                sa tagumpay ni Don Juan. <br>
                                                <br>
                                                [226] Ang piging nang matapos na <br>
                                                ermitanyo ay kumuha  <br>
                                                ng lamáng nasa botelya  <br>
                                                lunas na kataka-taka. <br>
                                                <br>
                                                [227] Mga sugat ni Don Jua’y <br>
                                                magiliw na pinahiran, <br>
                                                gumaling at nanghiluman <br>
                                                walang bakas bahagya man. <br>
                                                <br>
                                                [228] “Ngayon,” anang ermitanyo,  <br>
                                                “maghanda nang umuwi kayo, <br>
                                                magkasundo kayong tatlo, <br>
                                                wala sanang may maglilo. <br>
                                                <br>
                                                [229] “At, Don Juan, ay kunin mo na <br>
                                                itong marikit na hawla,  <br>
                                                baka di datning buháy pa <br>
                                                ang inyong mahal na ama.” <br>
                                                <br>
                                                [230] Nang sila ay magpaalam <br>
                                                ay lumuhod si Don Juan, <br>
                                                hiniling na bendisyunan  <br>
                                                ng ermitanyong marangal. <br>
                                                <br>
                                                [231] Ermitanyo ay naakit <br>
                                                sa gayong banal na nais, <br>
                                                nagsaamang sa pag-ibig <br>
                                                sa anak ay di nagkait. <br>
