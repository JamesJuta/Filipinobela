                                                kung di kayo magkaayon: <br>
                                                O ikaw ay ipatapon  <br>
                                                o patayin ka na roon!"<br>
                                                <br>
                                                [1337] Nanaog na si Don Juan <br>
                                                upang kunin ang sasakyan <br>
                                                ngunit hindi natandaan <br>
                                                ang kabayong kailangan. <br>
                                                <br>
                                                [1338] Ang nakuha'y ikawalo <br>
                                                sa halip na ikapito, <br>
                                                ang sisiha'y di gaano <br>
                                                nang makita ang kabayo. <br>
                                                <br>
