                                                sa Inglatera malagak. <br>
                                                <br>
                                                [1320] Sa piling din ng magulang <br>
                                                ang prinsesa'y maiiwan, <br>
                                                sa hari ay isang subyang <br>
                                                sa prinsipe ay makasal. <br>
                                                <br>
                                                [1321] Hari ay mayroong isang <br>
                                                kapatid sa Inglatera, <br>
                                                bata pa rin at maganda  <br>
                                                kay Don Juan ay bagay na.<br>
                                                <br>
                                                [1322] Kung sila'y magkaibigan <br>
                                                dito siya ipakasal, <br>
                                                matamis sa kaloobang <br>
                                                tangkilikin habambuhay. <br>
                                                <br>
