                                                sa Berbanya patutungo, <br>
                                                magpaumat-umat dito <br>
                                                masama na sa haka ko. <br>
                                                <br>
                                                [1335] "Yari na sa aking amang <br>
                                                dalhin ka sa Inglatera, <br>
                                                naroon ang aking tiya  <br>
                                                pakakasal ka sa kanya.<br>
                                                <br>
