                                                [681] Si Leonora'y walang kibo, <br>
                                                dugo niya'y kumukulo; <br>
                                                lason sa dibdib at puso <br>
                                                kay Don Pedrong panunuyo.<br>
                                                <br>
                                                [682] Ang nasok sa alaala <br>
                                                nang ayaw bitiwan siya,<br>
                                                yaong lobong engkantada <br>
                                                sa balon ay pawalan na.<br>
                                                <br>
                                                [683] Lobo ay pinagbilinang <br>
                                                "Ang Prinsipe kung nasaktan,<br>
                                                gamutin mong madalian,<br>
                                                siya'y aking hinihintay.<br>
                                                <br>
                                                [684] "Sabihin mong di ko ibig <br>
                                                dito ang aming pag-alis, <br>
                                                kami lamang ay pinilit <br>
                                                ng taksil niyang kapatid."<br>
                                                <br>
                                                [685] Palibhasa si Don Jua'y <br>
                                                mutyang-mutya sa magulang, <br>
                                                ang nangyaring kataksila'y <br>
                                                nabatid sa panagimpan.<br>
                                                <br>
                                                [686] Napanaginip ng hari <br>
                                                sa pag-idlip nang tanghali <br>
                                                na sa isang yungib wari <br>
                                                bunso niya'y itinali. <br>
                                                <br>
                                                [687] Nang gapos na ang katawan <br>
                                                ng lubid na pagkatibay, <br>
                                                sinipa na't tinadyakan <br>
                                                sa mukha pa ay tinampal.<br>
                                                <br>
                                                [688] Walang sala't di salari'y <br>
                                                ayaw namang patawarin, <br>
                                                sa hukuman nong litisin, <br>
                                                lagdang hatol ay patayin. <br>
                                                <br>
                                                [689] Saka biglang itinulak <br>
                                                sa banging kagulat-gulat, <br>
                                                sa ilalim nang lumagpak <br>
                                                ang hininga ay nautas.<br>
                                                <br>
                                                [691] At siya nang pagkapukaw <br>
                                                nitong hari sa hihigan, <br>
                                                ang puso at gunam-gunam<br>
                                                sapupo ng kalumbayan. <br>
                                                <br>
                                                [691] Gayunman ang panaginip <br>
                                                inaliw-aliw ang dibdib, <br>
                                                sinisikap na maalis <br>
                                                ang sindak sa kanyang isip.<br>
                                                <br>
                                                [692] Nanunton ang kalooban <br>
                                                sa matandang kasabihang: <br>
                                                madalas na magbulaan<br>
                                                ang sa taong panagimpan.<br>
                                                <br>
                                                [693] "Ano baga't gagayunin <br>
                                                ang bunso kong ginigiliw, <br>
                                                ito nama'y di salarin <br>
                                                na marapat pagbayarin?<br>
                                                <br>
                                                [694] "Ang bunso kong si Don Juan <br>
                                                may loob na malumanay, <br>
                                                matapat sa kaibiga't <br>
                                                uliran sa kabaitan.<br>
                                                <br>
                                                [695] "Kaya't kung may maglililo<br>
                                                sadyang taksil na lang ito, <br>
                                                huwag nawang magtotoo <br>
                                                malupit na pangarap ko." <br>
                                                <br>
                                                [696] Sa ganitong pagbubulay <br>
                                                ang hari ay napadungaw, <br>
                                                sa malayo'y natanawan <br>
                                                ang anak niyang panganay.<br>
                                                <br>
                                                [697] Sa lakara'y siyang una't <br>
                                                natatakpan ang kasama, <br>
                                                inasahang naroon na <br>
                                                bunsong nawalay sa mata.<br>
                                                <br>
                                                [698] Sa laki ng kagalakan, <br>
                                                madlang lungkot ay naparam;<br>
                                                malayo pa'y inaasam <br>
                                                na yakapin si Don Juan.<br>
                                                <br>
                                                [699] Ngunit anong dusa't sakit <br>
                                                nang sa mata'y mapalapit, <br>
                                                wala rin ang ninanais, <br>
                                                nabuhay ang panaginip.<br>
                                                <br>
                                                [700] Kaya't di rin nasiyahan<br>
                                                sa dalawang inutusan, <br>
                                                bagama't may kaalakbay <br>
                                                na dalawang kagandahan.<br>
                                                <br>
                                                [701] Malungkot na sumalubong <br>
                                                at may luha nang magtanong: <br>
                                                "Ano't kayo ay naglaon <br>
                                                sa bundok at mga burol?"<br>
                                                <br>
                                                [702] Sagot ng dalawang anak: <br>
                                                "Ama naming nililiyag, <br>
                                                sinagasa naming hirap <br>
                                                di madaling maiulat.<br>
                                                <br>
                                                [703] "Walang burol; nayo't bundok <br>
                                                na di namin nahalughog; <br>
                                                siniyasat bawat tumok, <br>
                                                mga sapa, batis, ilog. <br>
                                                <br>
                                                [704] "Di rin namin nasumpungan <br>
                                                ang bunso mong minamahal, <br>
                                                at sa aming kapaguran <br>
                                                ito po ang natagpuan.<br>
                                                <br>
                                                [705] "Magkapatid na Prinsesang <br>
                                                Sa balon po nagtitira, <br>
                                                nilusong naming dalawa; <br>
                                                balong ito'y engkantada.<br>
                                                <br>
                                                [706] "Doo'y aming nakalaban <br>
                                                dalawan ganid sa parang,<br>
                                                ang Higanteng mamamatay <br>
                                                at Serp'yenteng tampalasan.<br>
                                                <br>
                                                [707] "Sa amin pong mga kalis <br>
                                                tapang nila ay nagahis; <br>
                                                sa kay Kuya namang bagsik <br>
                                                buhay nila'y nangapatid.<br>
                                                <br>
                                                [708] "Nang mapatay namin sila<br>
                                                iniuwi ang Prinsesa <br>
                                                nang aming mapangasawa't <br>
                                                mapabilang sa Berbanya."<br>
                                                <br>
                                                [709] Haring mayro'ng kalungkutan <br>
                                                sa balitang napakinggan <br>
                                                siya nga ay nasiyahan <br>
                                                sa anak na katapangan.<br>
                                                <br>
                                                [710] Binendisyunang puspos <br>
                                                mga anak niyang irog, <br>
                                                at ang sabi: "O, sa Panginoon, <br>
                                                pasalamat kayong lubos."<br>
                                                <br>
                                                [711] At sa balak ng dalawa <br>
                                                ukol sa pag-aasawa, <br>
                                                si Don Pedro' y nagpauna't <br>
                                                ganito ang sabi niya: <br>
                                                <br>
                                                [712] "Amang makapangyarihan, <br>
                                                puno nitong kaharian,<br>
                                                ang iyo pong kalooban<br>
                                                siya naming igagalang.<br>
                                                <br>
                                                [713] "Kung ako po'y tatanungin<br>
                                                si Leonora na ang akin; <br>
                                                si Don Diego'y ikasal din <br>
                                                kay Donya Juanang butihin.<br>
                                                <br>
                                                [714] Nang marinig ni Leonora <br>
                                                kayarian ng mag-ama, <br>
                                                lumuluhang lumuhod na <br>
                                                at sa hari'y may pinita.<br>
                                                <br>
                                                [715] "Ako po'y di sumusuway<br>
                                                sa atas mo, Haring mahal, <br>
                                                ngunit hiling ko po lamang <br>
                                                iliban muna ang kasal.<br>
                                                <br>
                                                [716] "Sa aki'y ipahintulot <br>
                                                ng mahal mong pagkukupkop, <br>
                                                na bayaan kong matapos <br>
                                                panata sa Panginoon.<br>
                                                <br>
                                                [717] "Mulang ako'y maulila <br>
                                                sa akin pong ama't ina, <br>
                                                pitong taon kong panatang <br>
                                                mamumuhay nang mag-isa.<br>
                                                <br>
                                                [718] "Hinihiling ko rin naman <br>
                                                sa inyo pong kamahalan, <br>
                                                isang silid ako'y bigyan <br>
                                                na sadya kong tutulugan.<br>
                                                <br>
                                                [719] "Matapos ko ang panata, <br>
                                                Haring bunyi sa Berbanya, <br>
                                                sa anak mong siyang kuya, <br>
                                                handa akong tumalima. <br>
                                                <br>
                                                [720] "Kaya, Haring mapagmahal, <br>
                                                di man dapat sa kalakhan, <br>
                                                kung ito po'y kasalanan <br>
                                                patawad mo'y aking hintay."<br>
                                                <br>
                                                [721] Palibhasa'y mahalaga <br>
                                                itong hiling ng prinsesa <br>
                                                may ibig mang itanong pa <br>
                                                hari'y di na nakahuma.<br>
                                                <br>
                                                [722] Napagliming sa sariling <br>
                                                kasalanang pagkalaki <br>
                                                na ang nasang sakdal buti <br>
                                                ay tugunin ng pagtanggi<br>
                                                <br>
                                                [723] Ilang saglit nagdaang <br>
                                                hari'y naggugunam-gunam, <br>
                                                tumingala at pagkuwa'y <br>
                                                mga anak ang minasdan.<br>
                                                <br>
                                                [724] Kasabay ang pagwiwika: <br>
                                                "Mga bunsong minumutya, <br>
                                                huwag tayong sumaliwa <br>
                                                sa mabuting paniwala.<br>
                                                <br>
                                                [725] "Pairugan si Leonorang <br>
                                                magpatuloy sa panata; <br>
                                                Pedro'y pasasaan bagang <br>
