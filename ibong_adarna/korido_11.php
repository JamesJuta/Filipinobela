                                                [318] Sa libis ng isang bundok <br>
                                                may matandang sa-susulpot, <br>
                                                mahina't uugud-ugod <br>
                                                sa prinsipe ay dumulog. <br>
                                                <br>
                                                [319] Paglapit ay hinawakan <br>
                                                tiningnan ang kalagayan <br>
                                                saka kanyang dahan-dahang <br>
                                                inihiga nang mahusay. <br>
                                                <br>
                                                [320] Ang salanta at nalamog <br>
                                                na katawan ay hinagod; <br>
                                                sugat at lamang nalasog <br>
                                                pinaglalagyan ng gamot. <br>
                                                <br>
                                                [321] Samantalang ginagawa <br>
                                                ang ganitong kawanggawa, <br>
                                                kay Don Juan ang matanda <br>
                                                ay masuyong nagsalita: <br>
                                                <br>
                                                [322] "O, prinsipe, pagtiisan <br>
                                                ang madla mong kahirapan <br>
                                                di maglalaong araw <br>
                                                ang ginhawa ay kakamtan. <br>
                                                <br>
                                                [323] Parang isang panaginip <br>
                                                ang nangyari sa maysakit, <br>
                                                noon din ay nakatindig, <br>
                                                dating lakas ay nagbalik. <br>
                                                <br>
                                                [324] Ang sarili ay minalas <br>
                                                bakas ma'y wala ang sugat <br>
                                                naayos ang butong linsad <br>
                                                kiyas niya'y walang bawas. <br>
                                                <br>
                                                [325] Di nasukat and paghanga <br>
                                                sa nakitang talinhaga't <br>
                                                sa sarili ay nawika: <br>
                                                "Tila Diyos ang matanda." <br>
                                                <br>
                                                [326] Kung hindi man ay totoong <br>
                                                himala ng Diyos ito <br>
                                                na pakita nga sa tao't <br>
                                                nang ang loob ay magbago. <br>
                                                <br>
                                                [327] Saka makailang saglit <br>
                                                sa matanda ay lumapit, <br>
                                                yumapos nang buong higpit <br>
                                                at ang wikang nananangis: <br>
                                                <br>
                                                [328] "Utang ko sa inyong habag <br>
                                                ang buhay kong di nautas, <br>
                                                ano kaya ang marapat <br>
                                                iganti ng abang palad?" <br>
                                                <br>
                                                [329] Ang matanda ay tumugon: <br>
                                                "Kawanggawa ay hindi gayon <br>
                                                kung di iya'y isang layon <br>
                                                ang damaya'y walang gugol. <br>
                                                <br>
                                                [330] "Saka yang kawanggawa <br>
                                                na sa Diyos na tadhana, <br>
                                                di puhunang magagawa <br>
                                                nang sa yama'y magpasasa. <br>
                                                <br>
                                                [331] "Huwag natin tutularan <br>
                                                ang ugaling di mainam <br>
                                                na kaya lang dumaramay <br>
                                                ay nang upang madamayan. <br>
                                                <br>
                                                [332] "Lalong banal na tungkulin <br>
                                                nasa dusa'y tangkilikin; <br>
                                                sa mundo ang buhay nati'y <br>
                                                parang nagdaraang hangin! <br>
                                                <br>
                                                [333] "Don Juan, hindi ko hangad <br>
                                                tapusin ang pag-uusap, <br>
                                                ngunit iyong isahagap <br>
                                                ang ama mong nililiyag. <br>
                                                <br>
                                                [334] "Malaon nang naiisip <br>
                                                sa hindi mo pagbabalik, <br>
                                                karamdama'y lumalawig <br>
                                                baka di na makatawid. <br>
                                                <br>
                                                [335] "Kaya nga magmadali ka <br>
                                                ng pag-uwi sa Berbanya, <br>
                                                ikaw lamang ang lagi nang <br>
                                                pangarap ng iyong ama." <br>
                                                <br>
                                                [336] Ang dalawa ay nagkamay <br>
                                                bago sila naghiwalay; <br>
                                                matanda'y sa kabundukan, <br>
                                                sa Berbanya si Don Juan. <br>
                                                <br>
                                                [337] Sa tulin ng kanyang lakad <br>
                                                wari'y ibong lumilipad, <br>
                                                ang hangari'y agad-agad <br>
                                                sapitin ang bayang liyag. <br>
                                                <br>
                                                [338] Abutan pa niyang buhay <br>
                                                ang amang may karamdaman; <br>
                                                inang lubhang namamanglaw <br>
                                                maaliw ng pagmamahal. <br>
                                                <br>
                                                [339] Sa palasyo nang sumapit, <br>
                                                ang tuwa ng ina'y tigib <br>
                                                sindak namang di malirip <br>
                                                ang sa dalawang kapatid. <br>
                                                <br>
                                                [340] Lumuhod sa nakaratay <br>
                                                upang humalik ng kamay; <br>
                                                ang hari sa kalubhaan <br>
                                                bunso'y hindi namukhaan. <br>
                                                <br>
                                                [341] Magkagayo'y ang Adarna <br>
                                                namayagpag na sa hawla, <br>
                                                balahibong pangit niya'y <br>
                                                hinalinhan ng maganda. <br>
                                                <br>
                                                [342] Umawit na nang matamis <br>
                                                kawili-wili ang tinig, <br>
                                                mga matang may pag-ibig <br>
                                                sa monarka nakatitig. <br>
                                                <br>
                                                [343] Bawat isang pangungusap <br>
                                                anong linaw at liwanag, <br>
                                                kaya sa tanang kaharap <br>
                                                daming bagay ang nabunyag. <br>
                                                <br>
                                                [344] "Aba, Haring Don Fernando, <br>
                                                monarka ng buong reyno, <br>
                                                si Don Juan pong bunso mo <br>
                                                kaharap na't naririto. <br>
                                                <br>
                                                [345] "Ang iyo pong bunsong anak <br>
                                                nagtiis ng madlang hirap, <br>
                                                kamatayan ay hinamak, <br>
                                                sa utos mo ay tumupad. <br>
                                                <br>
                                                [346] "Yaong anak mong dalawang <br>
                                                inutusang nangauna, <br>
                                                kabiguan ang nakuha't <br>
                                                kapwa naging bato sila." <br>
                                                <br>
                                                [347] Ibo'y muling namayagpag <br>
                                                nagbihis ng bagong kiyas, <br>
                                                higit sa una ang dilag <br>
                                                kanta'y lalong pinatimyas. <br>
                                                <br>
                                                [348] "Sa kasawiang tinamo <br>
                                                ni Don Pedro't ni Don Diego, <br>
                                                Kung hindi po sa bunso mo <br>
                                                Habang araw silang bato. <br>
                                                <br>
                                                [349] "Sila nga'y binusan lamang  <br>
                                                g tubig na merong 'lalang, <br>
                                                nang mabasa'y nangabuhay <br>
                                                at sa bato'y nagsilitaw. <br>
                                                <br>
                                                [350] "Ang nagturo nitong tubig <br>
                                                ay ermitanyong mabait, <br>
                                                nahabag sa kahihibik <br>
                                                ng bunso mong iniibig. <br>
                                                <br>
                                                [351] "Sinalok sa isang ilog <br>
                                                sa ibis ng isang bundok <br>
                                                sa linaw ay parang bubog, <br>
                                                mabisa ang dalang gamot. <br>
                                                <br>
                                                [352] "Silang tatlo ay piniging <br>
                                                ng ermitanyong butihin, <br>
                                                kasayaha'y walang maliw <br>
                                                habang sila'y kumakain." <br>
                                                <br>
                                                [353] Ang Adarna ay nagbihis <br>
                                                ng pang-apat na kilatis, <br>
                                                sa kislap na umaakit <br>
                                                ay d'yamanteng pagkarikit. <br>
                                                <br>
                                                [354] "Kasayahan nang matapos <br>
                                                ermitanyo ay pumasok <br>
                                                sa silid na maalindog <br>
                                                at kumuha pa ng gamot. <br>
                                                <br>
                                                [355] "Hari, iyo pong alaming <br>
                                                si Don Jua'y nagtiis ding <br>
                                                palad niya ay hiwain <br>
                                                nang gabi'y ako'y hulihin. <br>
                                                <br>
                                                [356] "Pitong malalim na sugat <br>
                                                na pinigan pa ng dayap, <br>
                                                nang humapdi, o kay-antak <br>
                                                lama'y parang nginangatngat. <br>
                                                <br>
                                                [357] "Dusang ito ay tiniis <br>
                                                nang dahil sa kanyang nais <br>
                                                na ang taglay mo pong sakit <br>
                                                ay huwag pong pakalawig." <br>
                                                <br>
                                                [358] Nanahimik ang Adarna <br>
                                                nagbihis ng ikalimang <br>
                                                balahibong tinumbaga <br>
                                                at ang sabi sa monarka: <br>
                                                <br>
                                                [359] "Nang pauwi silang tatlo <br>
                                                dito po sa iyong reyno, <br>
                                                nagbilin ang ermitanyong <br>
                                                huwag nawang may maglilo. <br>
                                                <br>
                                                [360] "Masasaya sa lakaran <br>
                                                kung may sukat ikalumbay <br>
                                                ito ay sa kainipant <br>
                                                sa haba ng nilalakbay. <br>
                                                <br>
                                                [361] "Patuloy ang saya nila <br>
                                                ngunit noong sumapit na <br>
                                                sa bundok na pangalawa'y <br>
                                                nagbago ang pagsasama. <br>
                                                <br>
                                                [362] "Sa apoy ng kainggitan <br>
                                                nitong anak mong panganay, <br>
                                                nagbuko' ng kataksilan <br>
                                                na patayin si Don Juan. <br>
                                                <br>
                                                [363] "Dahilan po nito'y ako, <br>
                                                nahihiya si Don Pedrong <br>
                                                mabalitang maging bato't <br>
                                                kabiguan ang tinamo. <br>
                                                <br>
                                                [364] "Salamat at nasansala <br>
                                                ni Don Diego yaong nasa <br>
                                                at kung hindi ang kawawang <br>
                                                bunso mo po ay nawala. <br>
                                                <br>
                                                [365] "Nasansala ang pagpatay <br>
                                                ngunit sila'y nagbulungan <br>
                                                nang malayo'y may kindatang <br>
                                                babala ng kasawian." <br>
                                                <br>
                                                [366] Awit ng ibo'y pinatid <br>
                                                muling nagbago ng bihis, <br>
                                                ito'y lalo pang marikit <br>
                                                sa limang naipamasid. <br>
                                                <br>
                                                [367] "O, kay lupit! Haring mahal, <br>
                                                ng nangyari sa kindatan, <br>
                                                pagdaka'y pinagtulungang <br>
                                                umugin po si Don Juan. <br>
                                                <br>
                                                [368] "Bunso mo po'y humihibik <br>
                                                na tigilan ang pasakit, <br>
                                                ngunit itong mga ganid <br>
                                                ay lalo pang nagmabangis." <br>
                                                <br>
                                                [369] "Sa suntok at mga tadyak <br>
                                                si Don Juang kulang-palad, <br>
                                                mapasigaw, mapaliyad, <br>
                                                at sa bato'y masungadngad." <br>
                                                <br>
                                                [370] "Kaya lamang tinigila'y <br>
                                                nang lugmok na't walang malay, <br>
                                                iniwan sa kaparangan <br>
                                                at ako nga ang tinaglay. <br>
                                                <br>
                                                [371] "Laking galak ng dalawa <br>
                                                nang ako po'y maagaw na, <br>
                                                habang daa'y anong sigla <br>
                                                ng halakhak at pagtawa." <br>
                                                <br>
                                                [372] Ikapito't katapusang <br>
                                                awit nitong ibong mahal, <br>
                                                bago niya sinimulan <br>
                                                sa tayo'y nagpakahusay. <br>
                                                <br>
                                                [373] Balahibong inilabas <br>
                                                ay karbungkong nangniningas <br>
                                                kaya't lalong nanggilalas <br>
                                                ang tanang doo'y kaharap. <br>
                                                <br>
                                                [374] "Tatapusin ko ang awit <br>
                                                ang sa bunsong may sakit, <br>
                                                mahal na Hari'y makinig, <br>
                                                nang lubusan mong mabatid. <br>
                                                <br>
                                                [375] "Sa habag ng Poong Diyos <br>
                                                sa prinsipe ay dumulog <br>
                                                ang isang uugod-ugod <br>
                                                na matandang manggagamot. <br>
                                                <br>
                                                [376] "Ang matanda ay nahabag <br>
                                                sa daing ng iyong anak, <br>
                                                kaya kahit anong hirap <br>
                                                ang Prinsipe ay hinanap. <br>
                                                <br>
                                                [377] "Sa parang nang matagpuan <br>
                                                ang anyo ay tila bangkay, <br>
                                                buong suyong pinagyaman <br>
                                                ginamot ang karamdaman. <br>
                                                <br>
                                                [378] "Sa himala po ng Langit <br>
                                                matapos ang ilang saglit, <br>
                                                si Don Jua'y nakatindig <br>
                                                malakas na't walang sakit. <br>
                                                <br>
                                                [379] "Tuluyan nang umuwi na <br>
                                                dito sa reynong Berbanya, <br>
                                                ang nasa ay makita kang <br>
                                                buhay pa rin at masigla. <br>
                                                <br>
                                                [380] "Ang sinapit ni Don Jua'y <br>
                                                dinamdam ko, Haring mahal, <br>
                                                kaya nga po gayon na lang <br>
                                                yaring dusa't kalumbayan. <br>
                                                <br>
                                                [381] "Kaya rin nga namasdan mong <br>
                                                ako'y laging nanlulugo, <br>
                                                ni kumain ay ayoko <br>
                                                o maringgan ng awit ko. <br>
                                                <br>
                                                [382] "Pagkat di pa dumarating <br>
                                                ang may-ari po sa akin, <br>
                                                ayoko sa mga taksil <br>
                                                na anak mo pong masasakim. <br>
                                                <br>
                                                [383] "Yamang ngayo'y natalos mo <br>
                                                ang matapat at ang lilo <br>
                                                kay Don Juan, O Hari ko, <br>
                                                ipamana itong reyno." <br>
                                                <br>
                                                [384] Nang matapos na ang awit <br>
                                                hari'y tila nanaginip, <br>
                                                nagbalikwas na sa banig <br>
                                                parang hindi nagkasakit. <br>
                                                <br>
                                                [385] Ang Adarna'y nilapitan <br>
                                                at niyakap si Don Juan, <br>
                                                ibo't anak ay hinagkan <br>
                                                sa laki ng katuwaan. <br>
                                                <br>
                                                [386] Nang balingan ang dalawang <br>
                                                katabi noon ng ina, <br>
                                                nangamutla at nangamba <br>
                                                sa darating na parusa. <br>
                                                <br>
                                                [387] Gayon na lamang ang poot <br>
                                                sa dalawang nagbalakyot, <br>
                                                at kung walang isang Diyos <br>
                                                ang talaga'y ipasunog. <br>
                                                <br>
                                                [388] Pinulong kara-karaka <br>
                                                lahat ng kagawad niya, <br>
                                                inilagda ang parusang <br>
                                                ipatapon ang dalawa. <br>
                                                <br>
                                                [389] Ipatapon at bawian <br>
                                                ng lahat ng karapatan, <br>
                                                upang hindi pamarisan <br>
                                                ng pinuno't mamamayan. <br>
                                                <br>
                                                [390] Ang hatol nang maigawad <br>
                                                si Don Juan ay nahabag, <br>
                                                sa ama agad humarap <br>
                                                at hiningi ang patawad. <br>
                                                <br>
                                                [391] Lumuluha nang sabihing <br>
                                                "O, ama kong ginigiliw, <br>
                                                ang puso mong mahabagin <br>
                                                sa kanila'y buksan mo rin. <br>
                                                <br>
                                                [392] "Malaki man po ang sala <br>
                                                sa aki'y nagawa nila, <br>
                                                yaon po ay natapos na't <br>
                                                dapat kaming magkasama. <br>
                                                <br>
                                                [393] "Ako naman ay narito <br>
                                                buhay pa ri't kapiling mo <br>
                                                wala rin ngang nababago <br>
                                                sa samahan naming tatlo. <br>
                                                <br>
                                                [394] "Sila'y aking minamahal <br>
                                                karugtong ng aking buhay, <br>
                                                kami'y pawang anak naman <br>
                                                sa lingap mo nananangan. <br>
                                                <br>
                                                [395] "Hindi ko po mababatang- <br>
                                                sa aki'y malayo sila, <br>
                                                kaya po ibigay mo na <br>
                                                ang patawad sa kanila <br>
                                                <br>
                                                [396] Haring noon ay may galit <br>
                                                nabagbagan din ng dibdib, <br>
                                                ang dalawang napipiit <br>
                                                sa palasyo'y pinapanhik. <br>
                                                <br>
                                                [397] Haring ama'y nagsalita <br>
                                                mabalasik yaong mukha: <br>
                                                "Kayo ngayon ay lalaya, <br>
                                                sa pangakong magtatanda. <br>
                                                <br>
                                                [398] "Sa araw na kayong muli <br>
                                                magkasala kahit munti, <br>
                                                patawarin kayo'y hindi <br>
                                                sinuman nga ang humingi. <br>
                                                <br>
                                                [399] "Kaya nga pakaingatan, <br>
                                                sasabihin ko'y tandaan: <br>
                                                magkasala'y minsan lamang, <br>
                                                pag umulit, kamatayan! <br>
