                                                [1382] Kaya ba ani Don Juan <br>
                                                sa prinsesang kanyang buhay,  <br>
                                                "Kita muna'y maiiwan,<br>
                                                huwag sanang mamamanglaw. <br>
                                                <br>
                                                [1383] "Ako ngayon ay haharap <br>
                                                sa ama kong nililiyag <br>
                                                upang kanyang matalastas<br>
                                                ang sa atin ay marapat. <br>
                                                <br>
                                                [1384] "Katungkulan ng palasyo <br>
                                                ang pagsalubong sa iyo, <br>
                                                ito naman ay dangal kong  <br>
                                                masasabi ng ama mo."<br>
                                                <br>
                                                [1385] Pakli naman ng prinsesa: <br>
                                                "Bakit kaya ibig mo pang  <br>
                                                magulang mo'y maabala<br>
                                                gayong ito ay labis na? <br>
                                                <br>
                                                [1386] "Sa aki'y di kailangang <br>
                                                handugan pa ng parangal, <br>
                                                mayro'n nito o wala man  <br>
                                                Wala tayong kabaguhan."<br>
                                                <br>
                                                [1387] Kay Don Juan namang sagot: <br>
                                                "Tunay na nga, aking irog,  <br>
                                                ngunit bigyan nating lugod<br>
                                                ang bayan kong nasa lungkot. <br>
                                                <br>
                                                [1388] "Alamin mong matagal nang <br>
                                                hinihintay ako nila  <br>
                                                sa laong di pagkikita<br>
                                                ang nawala ay buhay pa. <br>
                                                <br>
                                                [1389] "Saka laking kababaan <br>
                                                ang di ko parangalan, <br>
                                                ang Berbanya'y malalagay<br>
                                                sa hamak na kalagayan. <br>
                                                <br>
                                                [1390] "Ano na ang sasabihin <br>
                                                ng ama mo kung malining <br>
                                                siyang galit na sa atin<br>
                                                ang pagsumpa'y sapin-sapin? <br>
                                                <br>
                                                [1391] "Kaya giliw, mayag ka nang <br>
                                                dito'y iwan muna kita, <br>
                                                pangako ko at umasang  <br>
                                                mamaya ri'y kapiling ka."<br>
                                                <br>
                                                [1392] "Kung gayon ay isang hiling," <br>
                                                ang kay Donya Mariang turing,  <br>
                                                "ipangako mo sa aking<br>
                                                ito'y di mo lilimutin.  <br>
                                                <br>
                                                [1393] "Hinihingi ko sa iyong <br>
                                                pagdating mo sa palasyo  <br>
                                                iwasan sanang totoo<br>
                                                sa babae'y makitungo. <br>
                                                <br>
                                                [1394] "Maging sa ina mong tunay <br>
                                                ang malapit ay iwasan, <br>
                                                mabigat ito, Don Juan,<br>
                                                ngunit siyang kailangan. <br>
                                                <br>
                                                [1395] "Ang hiling ko pag nilabag<br>
                                                asahan mong mawawakwak <br>
                                                dangal ko't yaring palad  <br>
                                                sa basahan matutulad.<br>
                                                <br>
                                                [1396] "O, Don Juan, aking kasi, <br>
                                                alaala ko'y malaki:  <br>
                                                karaniwan sa lalaki<br>
                                                ang mabihag ng babae." <br>
                                                <br>
                                                [1397] "Iwalay sa alaala't <br>
                                                ako'y itangi sa iba, <br>
                                                sa buhay ko ay sino pa  <br>
                                                kundi ikaw ang ligaya.<br>
                                                <br>
                                                [1398] "Limutin ka'y kataksilan <br>
                                                magawa ko kaya iyan? <br>
                                                O, buhay ng aking buhay,  <br>
                                                magsabi ang kamatayan."<br>
                                                <br>
                                                [1399] Prinsipe ay humayo na <br>
                                                sa palasyo'y mangyari pa, <br>  
                                                nang dumating anong sigla't  <br>
                                                kaharian ay nagsaya.<br>
                                                <br>
                                                [1400] Ang lahat na'y nagdumugang <br>
                                                sumalubong kay Don Juan, <br>
                                                arna't inang nabuhayan  <br>
                                                yakap na sa bunsong mahal.<br>
                                                <br>
                                                [1401] Maging mga kapatid <br>
                                                na malayo yaong dibdib  <br>
                                                pagkikita'y di man nais<br>
                                                nagsaya rin at lumapit. <br>
                                                <br>
                                                [1402] Lalo itong si Leonorang  <br>
                                                pitong taong nagdurusa, <br>
                                                nang marinig yaong sinta'y  <br>
                                                hinimatay na sa saya.<br>
                                                <br>
                                                [1403] Labas na sa kanyang silid <br>
                                                katauha'y nagtatalik,  <br>
                                                ang pagdating ng pag-ibig<br>
                                                paglaya sa madlang sakit. <br>
                                                <br>
                                                [1404] Lumapit na kay Don Juan <br>
                                                at umupong kaagapay, <br>
                                                nilimot ang kahihiyan  <br>
                                                sa harap ng kapulungan.<br>
                                                <br>
                                                [1405] Ang ginawang iyon niya <br>
                                                di kagaspangan aniya,  <br>
                                                pusong uhaw sa pagsinta<br>
                                                ang hiya'y nalilimot na. <br>
                                                <br>
                                                [1406] Si Leonora'y may matwid <br>
                                                gawin yaon kahit pangiti <br>
                                                ano nga't ang kanyang dibdib  <br>
                                                ibibigay sa di ibig?<br>
                                                <br>
                                                [1407] Pitong taong nagbabata<br>
                                                maligtasan lamang niya <br>
                                                ang masaklap na pagsinta  <br>
                                                ni Don Pedrong palamara.<br>
                                                <br>
                                                [1408] At ngayong ang hinihintay <br>
                                                narito na'y bakit naman  <br>
                                                iibigin pang tumagal<br>
                                                ang kimkim na kahirapan? <br>
                                                <br>
                                                [1409] At noon na nagpahayag <br>
                                                na ang luha'y nalalaglag, <br>
                                                "Mahal na Hari'y patawad  <br>
                                                sa gawa kong hindi dapat.<br>
                                                <br>
                                                [1410] "Dinggin po ng kamahalan <br>
                                                yaring munti kong hinakdal,  <br>
                                                kung mali o may katwiran,<br>
                                                hatol ninyo'y igagalang. <br>
                                                <br>
                                                [1411] "Pagkat naririto na nga <br>
                                                ang puso ko po'y mutya, <br>
                                                panata kong di sinira'y  <br>
                                                tapos na po alipala.<br>
                                                <br>
                                                [1412] "Hiningi sa kamahalan <br>
                                                pitong taong pagbabanal,  <br>
                                                pag-iwas po na makasal<br>
                                                sa hindi ko minamahal <br>
                                                <br>
                                                [1413] "Ngayon ay ipagtatapat <br>
                                                sa inyo po at sa lahat,  <br>
                                                sa puso ko'y ang may hawak<br>
                                                si Don Juan, inyong anak. <br>
                                                <br>
                                                [1414] "Dito ako pakakasal <br>
                                                pagkat isang kataksilang  <br>
                                                hanggang langit isisigaw<br>
                                                ang sumpa ko'y talikuran. <br>
                                                <br>
                                                [1415] "Si Don Jua't dili iba <br>
                                                ang sa akin ay kumuha  <br>
                                                sa balon ang palamara<br>
                                                ay anak mo pong dalawa. <br>
                                                <br>
                                                [1416] "Hanggang dito, Haring mahal, <br>
                                                yaring akin pong hinakdal, <br>
                                                di ko ibig na masalang <br>
                                                ang iba pang mga suyang."<br>
                                                <br>
                                                [1417] Hari sa kanyang narinig <br>
                                                matagal ding di umimik,  <br>
                                                ang hiwaga'y iniisip<br>
                                                na tutop ang kanyang dibdib. <br>
                                                <br>
                                                [1418] Sampung mga kamahalang <br>
                                                nakarinig sa hinakdal  <br>
                                                ay pawang nangatigilan<br>
                                                sa gayong kababalaghan. <br>
                                                <br>
                                                [1419] Nangapako ang paningin <br>
                                                sa dalawang mga taksil,  <br>
                                                hindi nila akalaing<br>
                                                kapwa pala mayro'ng lihim. <br>
                                                <br>
                                                [1420] Kapwa hindi humuhuma <br>
                                                halata ang hiya nila;  <br>
                                                noon lalong nakilalang<br>
                                                may katwiran si Leonora. <br>
                                                <br>
                                                [1421] Nagpahayag itong hari <br>
                                                matapos mapaglimi: <br>
                                                "Leonora, sa iyong hingi  <br>
                                                walang tutol yaring budhi.<br>
                                                <br>
                                                [1422] "Sinuman nga sa dalawa <br>
                                                ang pilii'y pakasal ka, <br>
                                                ikaw naman ay umasang  <br>
                                                manugang ding mahal kita.<br>
                                                <br>
                                                [1423] "Pag-iisang dibdib ninyo'y <br>
                                                gagawin sa linggong ito, <br>
                                                ganito ang pasiya ko't  <br>
                                                nang matapos na ang gulo.<br>
                                                <br>
                                                [1424] "Samantalang naghihintay <br>
                                                ako sana'y pagtapatan,  <br>
                                                ang pinutol na hinakdal<br>
                                                may malaking kahulugan. <br>
                                                <br>
                                                [1425] "Isang bagay na dapat kong <br>
                                                liwanagin kung totoo,  <br>
                                                akong Haring ama ninyo<br>
                                                sa masama ay ayoko. <br>
                                                <br>
                                                [1426] "Anak ko man ay suwail<br>
                                                ang marapat ay itakwil,  <br>
                                                kasamaang pausbungin<br>
                                                sa bayan ay pagtataksil." <br>
                                                <br>
                                                [1427] "Tunay nga po, Haring mahal," <br>
                                                kay Leonorang, katugunan, <br>
                                                "ngunit ako'y pairugan  <br>
                                                pagkaraos na ng kasal."<br>
                                                <br>
                                                [1428] Anupa nga't naayos din <br>
                                                ang pusong suliranin, <br>
                                                kasayahang nangulimlim <br>
                                                nagpatuloy nang maningning.<br>
