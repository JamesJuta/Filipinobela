                                            <p class="novel-text">
                                                [400] Nanumbalik ang liwanag <br>
                                                na sa palasyo'y tumakas: <br>
                                                hari at reynang marila <br>
                                                may ngiti nang masasarap. <br>
                                                <br>
                                                [401] At ang tatlong magkapatid <br>
                                                sa dati ring pagniniig <br>
                                                pasunura'y anong tamis <br>
                                                bahagya ma'y di nag-alit. <br>
                                                <br>
                                                [402] Muling ipinagpatuloy <br>
                                                ng hari ang panunungkol, <br>
                                                kaharia'y mahinaho' <br>
                                                ang lahat ay umaayon. <br>
                                                <br>
                                                [403] Buha't nang siya'y gumaling <br>
                                                ang Adarna'y naging aliw <br>
                                                oras-oras kung dalawi' <br>
                                                parang bata kung laruin. <br>
                                                <br>
                                                [404] At sa kanyang pagmamahal <br>
                                                pati reyna'y namamaang <br>
                                                kung ang ibo'y tao laman <br>
                                                pagseselos ay naglatang. <br>
                                                <br>
                                                [405] Sa sarili'y di nagkasya <br>
                                                ng pagdalaw sa Adarna, <br>
                                                naisipang pag gabi n <br>
                                                pabantayan ang hawla. <br>
                                                <br>
                                                [406] Di sa iba ibinigay <br>
                                                ang ganitong katungkulan, <br>
                                                baka anya pabayaan <br>
                                                makawala o mamatay. <br>
                                                <br>
                                                [407] Hinirang ang tatlong anak <br>
                                                At nagbanta nang marahas: <br>
                                                "Ang sa inyo ay magsuka <br>
                                                sa akin ay magbabayad." <br>
                                                <br>
                                                [408] Nakatakda sa utos <br>
                                                ang gawaing pagtatanod; <br>
                                                ang tatlo ay sunod-sunod <br>
                                                sa magdamag walang tulog. <br>
                                                <br>
                                                [409] Tatlong hati sa magdamag <br>
                                                bawat isa'y tatlong oras; <br>
                                                para nilang hinahata <br>
                                                ang gabi sa pagliwanag. <br>
                                                <br>
                                                [410] Ang panaho'y pumapanaw <br>
                                                araw ay di mapigilan, <br>
                                                ang tatlo sa halinhina' <br>
                                                panatag sa katungkulan <br>
                                                <br>
                                                [411] Subalit O! yaong inggit,  <br>
                                                sawang maamo'y bumabangis!  <br>
                                                Pag biglaang maging sakim <br>
                                                panginoo'y nililingkis.  <br>
                                                <br>
                                                [412] Si Don Pedrong pinatawad  <br>
                                                sa gawaing di mararapat, <br>
                                                sa sarili'y naging galak <br>
                                                kapatid ay ipahamak!  <br>
                                                <br>
                                                [413] Naisipan isang gabi <br>
                                                sa kanyang pagsasarili,  <br>
                                                kahihiyan ng sarili  <br>
                                                lihim na ipaghiganti <br>
                                                
                                                [414] Kapatid na pangalawa'y  <br>
                                                niyayang magsabay sila  <br>
                                                ng pagtanod sa Adarna't <br>
                                                magsabay ring mamahinga.  <br>
                                                <br>
                                                [415] Si Don Diego ay nagtanong:  <br>
                                                "Sasabay ba ako ngayon?  <br>
                                                Mamaya'y sino kung gayon <br>
                                                ang magbabantay sa ibon?"  <br>
                                                <br>
                                                [416] Kay Don Diegong kasaguta'y  <br>
                                                "Gisingin mo si Don Juan  <br>
                                                pagdating dito ay iwa't <br>
                                                huwag na siyang palitan." <br>
                                                <br>
                                                [417] "At paano naman siya  <br>
                                                tatanod nang makalawa?"  <br>
                                                "Huwag ka lang mag-alala't <br>
                                                bukas tayo magkikita." <br>
                                                <br>
                                                [418] Ang dalawa'y nagkasundo, <br>
                                                sila'y nagtabi sa upo;  <br>
                                                sa k'wentuha't mga biro,  <br>
                                                tumugtog ang ikasampu <br>
                                                <br> 
                                                [419] Ginising na si Don Juan  <br>
                                                sa tulog na kasarapan,  <br>
                                                di man oras ng pagbantay <br>
                                                nagbigay na sa pumukaw.  <br>
                                                <br>
                                                [420] Sa silid ay lumabas na  <br>
                                                bagaman nag-aantok pa,  <br>
                                                at hiniling sa dalawang <br>
                                                palitan siyang maaga. <br>
                                                <br>
                                                [421] Palibhasa'y nahirapan  <br>
                                                nang mga gabing sinundan,  <br>
                                                mga mata ma'y tukuran <br>
                                                antok din ay di mapigilan. <br>
                                                <br>
                                                [422] Bakit ba nang gabing yao'y  <br>
                                                pagkasarap pa ng simoy,  <br>
                                                ang prinsipe'y napabulon <br>
                                                matulog nang mahinahon. <br>
                                                <br>
                                                [423] Walang kaba kamunti mang  <br>
                                                humilig na sa upuan,  <br>
                                                himbing niya'y gayon lamang <br>
                                                nang magmamadaling-araw. <br>
                                                <br>
                                                [424] Lumapit na ang dalawa't <br>
                                                pinawalan ang Adarna,  <br>
                                                kaya't nang magising siya  <br>
                                                takot agad ang nadama <br>
                                                <br> 
                                                [425] Di takot sa kagalitan  <br>
                                                o parusa ng magulang,  <br>
                                                kundi pa'nong matatakpan <br>
                                                ang nangyaring kataksilan.  <br>
                                                <br>
                                                [426] Noon niya napagsukat  <br>
                                                ang sa tao palang palad  <br>
                                                magtiwala ay mahirap <br>
                                                daan ng pagkapahamak. <br>
                                                <br>
                                                [427] Bago mitak ang umaga  <br>
                                                si Don Jua'y umalis na,  <br>
                                                wika'y "Ito ang maganda, <br>
                                                natatago ang may sala."  <br>
                                                <br>
                                                [428] Nang magising yaong hari  <br>
                                                araw'y masaya ang ngiti,  <br>
                                                pagbangon ay dali-daling <br>
                                                ibon ang kinaurali. <br>
                                                <br>
                                                [429] Gaano ang panginginig,  <br>
                                                mga mata'y nanlilisik  <br>
                                                nang sa hawla'y di mamasi <br>
                                                Adarnang saya ng dibdib.  <br>
                                                <br>
                                                [430] Nagngangalit na tinawag  <br>
                                                ang tatlong prinsipeng anak,  <br>
                                                dadalawa ang humarap <br>
                                                kapwa hindi nangapuyat. <br>
                                                <br>
                                                [431] Ang dalawa'y binantaang  <br>
                                                huwag siyang paglihiman,  <br>
                                                sagot nila'y "Ama, ewan, <br>
                                                ang bantay po'y si Don Juan." <br>
                                                <br>
                                                [432] Ipinahanap ang bunso,  <br>
                                                ngunit saan sya' nagtungo?  <br>
                                                matagal nang nakalayo, <br>
                                                di sa hangad na magtago. <br>
                                                <br>
                                                [433] Saka bakit hahanapin  <br>
                                                sa kaharap yaong taksil?  <br>
                                                Itong anak na suwail, <br>
                                                at isang ngang sinungaling <br>
                                                <br>
                                                [434] Umalis na ang dalawang  <br>
                                                nagmamagaling sa ama,  <br>
                                                ang pangako'y pag nakita <br>
                                                iuuwi't nang magdusa.  <br>
                                                <br>
                                                [435] Mga bukid, burol, bundok,  <br>
                                                bawat dako'y hinalughog <br>
                                                lakad nila'y walang lagot, <br>
                                                sinisipat bawat tumok. <br>
                                                <br>
                                                [436] Wala, wala si Don Juan,  <br>
                                                napagod na ang pananaw...  <br>
                                                "Siya kaya'y napasaa't <br>
                                                hindi natin matagpuan?" <br>
                                                <br>
                                                [437] Lakad, tanaw, silip, sipat  <br>
                                                sa kahuyan at talampas,  <br>
                                                sa kanilang kahahanap <br>
                                                nangapagod at namayat. <br>
                                                <br>
                                                [438] Gayumpama'y patuloy rin  <br>
                                                tulad nila'y mamamansing <br>
                                                nang sa dagat ay alatin. <br>
                                                walang huli'y naroon din.  <br>
                                                <br>
                                                [439] Ngunit sila'y may matuwid  <br>
                                                na huwag munang magbalik,  <br>
                                                kung ang bawat pagsasakit <br>
                                                di man dapat ay may langit. <br>
                                                <br>
                                                [440] At tunay ngang nagtagumpay <br> 
                                                ang tiyagang pinuhunan;  <br>
                                                nakita rin si Don Juan <br>
                                                sa Armenyang kabunduka <br>
